package checksums

import (
	"os"
	"path/filepath"
	"testing"
)

// History-based auto-heal contract (FORGE_BACKLOG "stale codegen vs
// user-modified" gap):
//
// When the on-disk content of a Tier-1 file matches ANY prior render in
// the entry's History — not just the most recent Hash — the file is
// stale codegen, not a user edit. Both the pre-pipeline stomp guard
// (CheckTier1Drift) and the per-write modified-file check
// (WriteGeneratedFile via IsFileModified) must treat it as clean and let
// regeneration proceed without --force / --accept.
//
// The three on-disk states the tests below pin:
//
//   - current:    on-disk == entry.Hash            → clean, regenerate
//   - historical: on-disk == some History[i]       → auto-heal, regenerate
//   - unknown:    on-disk matches nothing recorded → real drift, guard fires

// seedHistory builds an entry whose History contains every render in
// `renders` (oldest first) and whose Hash mirrors the most recent one —
// the same invariant RecordFile maintains.
func seedHistory(cs *FileChecksums, relPath string, renders ...[]byte) {
	for _, r := range renders {
		cs.RecordFile(relPath, r)
	}
	entry := cs.Files[relPath]
	entry.Tier = 1
	cs.Files[relPath] = entry
}

func TestCheckTier1Drift_HistoryAutoHeal(t *testing.T) {
	renderV1 := []byte("// Code generated by forge. DO NOT EDIT.\npackage app // v1\n")
	renderV2 := []byte("// Code generated by forge. DO NOT EDIT.\npackage app // v2\n")
	handEdit := []byte("// Code generated by forge. DO NOT EDIT.\npackage app // user edit\n")

	tests := []struct {
		name      string
		onDisk    []byte
		wantDrift bool
	}{
		{
			name:      "on-disk matches current hash — clean",
			onDisk:    renderV2,
			wantDrift: false,
		},
		{
			name:      "on-disk matches historical hash — auto-heal, no drift",
			onDisk:    renderV1,
			wantDrift: false,
		},
		{
			name:      "on-disk matches no recorded render — real drift",
			onDisk:    handEdit,
			wantDrift: true,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			root := t.TempDir()
			cs := &FileChecksums{Files: make(map[string]FileChecksumEntry)}
			seedHistory(cs, "pkg/app/app_gen.go", renderV1, renderV2)

			full := filepath.Join(root, "pkg", "app", "app_gen.go")
			if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
				t.Fatal(err)
			}
			if err := os.WriteFile(full, tt.onDisk, 0o644); err != nil {
				t.Fatal(err)
			}

			drift := cs.CheckTier1Drift(root)
			if got := len(drift) > 0; got != tt.wantDrift {
				t.Errorf("CheckTier1Drift drift=%v, want %v (entries: %+v)", got, tt.wantDrift, drift)
			}
		})
	}
}

func TestWriteGeneratedFile_HistoryAutoHeal(t *testing.T) {
	renderV1 := []byte("package app // v1\n")
	renderV2 := []byte("package app // v2\n")
	renderV3 := []byte("package app // v3\n")
	handEdit := []byte("package app // user edit\n")

	tests := []struct {
		name      string
		onDisk    []byte
		wantWrote bool
	}{
		{
			name:      "on-disk matches current hash — clean, write proceeds",
			onDisk:    renderV2,
			wantWrote: true,
		},
		{
			name:      "on-disk matches historical hash — auto-heal, write proceeds",
			onDisk:    renderV1,
			wantWrote: true,
		},
		{
			name:      "on-disk matches no recorded render — drift, write skipped",
			onDisk:    handEdit,
			wantWrote: false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			ResetSkipWrite()
			root := t.TempDir()
			cs := &FileChecksums{Files: make(map[string]FileChecksumEntry)}
			seedHistory(cs, "pkg/app/app_gen.go", renderV1, renderV2)

			full := filepath.Join(root, "pkg", "app", "app_gen.go")
			if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
				t.Fatal(err)
			}
			if err := os.WriteFile(full, tt.onDisk, 0o644); err != nil {
				t.Fatal(err)
			}

			wrote, err := WriteGeneratedFile(root, "pkg/app/app_gen.go", renderV3, cs, false)
			if err != nil {
				t.Fatalf("WriteGeneratedFile: %v", err)
			}
			if wrote != tt.wantWrote {
				t.Errorf("wrote=%v, want %v", wrote, tt.wantWrote)
			}

			got, _ := os.ReadFile(full)
			if tt.wantWrote {
				if string(got) != string(renderV3) {
					t.Errorf("on-disk content not regenerated; got %q", got)
				}
				if cs.Files["pkg/app/app_gen.go"].Hash != Hash(renderV3) {
					t.Errorf("recorded hash not updated to new render")
				}
			} else {
				if string(got) != string(tt.onDisk) {
					t.Errorf("drifted content was overwritten; got %q", got)
				}
			}
		})
	}
}

// TestIsFileModified_HistoryAutoHeal pins the underlying predicate both
// callers above delegate to.
func TestIsFileModified_HistoryAutoHeal(t *testing.T) {
	renderV1 := []byte("a\n")
	renderV2 := []byte("b\n")

	tests := []struct {
		name   string
		onDisk []byte
		want   bool
	}{
		{"current hash", renderV2, false},
		{"historical hash", renderV1, false},
		{"unknown hash", []byte("c\n"), true},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			root := t.TempDir()
			cs := &FileChecksums{Files: make(map[string]FileChecksumEntry)}
			seedHistory(cs, "f.go", renderV1, renderV2)
			if err := os.WriteFile(filepath.Join(root, "f.go"), tt.onDisk, 0o644); err != nil {
				t.Fatal(err)
			}
			if got := cs.IsFileModified(root, "f.go"); got != tt.want {
				t.Errorf("IsFileModified = %v, want %v", got, tt.want)
			}
		})
	}
}
