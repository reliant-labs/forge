package checksums

import (
	"os"
	"path/filepath"
	"testing"
)

// Stale-codegen-vs-user-edit contract, self-certifying era.
//
// The legacy manifest kept a render History per path; "on-disk matches
// ANY prior render" meant stale codegen. With the embedded forge:hash
// marker the same question is answered locally: a marker that VERIFIES
// proves the bytes are an unedited forge render of SOME vintage —
// every vintage self-certifies, so the explicit history list is gone.
//
// The three on-disk states the tests below pin:
//
//   - current:   stamped render of the incoming content → clean, regenerate
//   - stale:     stamped render of an OLDER vintage      → auto-heal, regenerate
//   - hand-edit: marker present but body changed         → real drift, guard fires
//
// A file with NO marker at a Tier-1 path is the legacy untracked state:
// the writer overwrites it and the drift scan never flags it.

// writeStamped writes Stamp(rel, content) at root/rel — the on-disk
// state "forge previously wrote this file".
func writeStamped(t *testing.T, root, rel string, content []byte) {
	t.Helper()
	stamped, ok := Stamp(rel, content)
	if !ok {
		t.Fatalf("Stamp(%q): unstampable", rel)
	}
	full := filepath.Join(root, rel)
	if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(full, stamped, 0o644); err != nil {
		t.Fatal(err)
	}
}

// handEditStamped returns a stamped render mutated AFTER stamping (the
// marker survives, the body no longer matches it) — a hand-edit the
// stomp guard must catch.
func handEditStamped(t *testing.T, rel string, content []byte) []byte {
	t.Helper()
	stamped, ok := Stamp(rel, content)
	if !ok {
		t.Fatalf("Stamp(%q): unstampable", rel)
	}
	return append(stamped, []byte("// hand edit\n")...)
}

func TestScanTier1Drift_VintageAutoHeal(t *testing.T) {
	renderV1 := []byte("// Code generated by forge. DO NOT EDIT.\npackage app // v1\n")
	renderV2 := []byte("// Code generated by forge. DO NOT EDIT.\npackage app // v2\n")
	const rel = "pkg/app/app_gen.go"

	tests := []struct {
		name      string
		write     func(t *testing.T, root string)
		wantDrift bool
	}{
		{
			name:      "on-disk is the stamped current render — clean",
			write:     func(t *testing.T, root string) { writeStamped(t, root, rel, renderV2) },
			wantDrift: false,
		},
		{
			name: "on-disk is a stamped OLDER vintage — pristine, no drift (heals at write time)",
			write: func(t *testing.T, root string) {
				writeStamped(t, root, rel, renderV1)
			},
			wantDrift: false,
		},
		{
			name: "marker present but body edited — real drift",
			write: func(t *testing.T, root string) {
				full := filepath.Join(root, rel)
				if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
					t.Fatal(err)
				}
				if err := os.WriteFile(full, handEditStamped(t, rel, renderV2), 0o644); err != nil {
					t.Fatal(err)
				}
			},
			wantDrift: true,
		},
		{
			// Legacy untracked behavior: no marker means forge never
			// certified these bytes — never drift; the writer overwrites.
			name: "no marker — untracked, never drift",
			write: func(t *testing.T, root string) {
				full := filepath.Join(root, rel)
				if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
					t.Fatal(err)
				}
				if err := os.WriteFile(full, renderV2, 0o644); err != nil {
					t.Fatal(err)
				}
			},
			wantDrift: false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			root := t.TempDir()
			cs := &FileChecksums{}
			tt.write(t, root)

			drift := ScanTier1Drift(root, cs)
			if got := len(drift) > 0; got != tt.wantDrift {
				t.Errorf("ScanTier1Drift drift=%v, want %v (entries: %+v)", got, tt.wantDrift, drift)
			}
		})
	}
}

func TestScanTier1Drift_UnstampableFallback(t *testing.T) {
	// Comment-incapable formats (.json) are tracked in the scoped
	// .forge/hashes.json fallback: a body-hash mismatch is drift, a
	// match is clean, a missing file is not a stomp candidate.
	const rel = "config/app.json"
	render := []byte("{\"a\":1}\n")

	t.Run("recorded hash matches — clean", func(t *testing.T) {
		root := t.TempDir()
		cs := &FileChecksums{Unstampable: map[string]string{rel: BodyHash(render)}}
		full := filepath.Join(root, rel)
		if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(full, render, 0o644); err != nil {
			t.Fatal(err)
		}
		if drift := ScanTier1Drift(root, cs); len(drift) != 0 {
			t.Errorf("unexpected drift: %+v", drift)
		}
	})

	t.Run("recorded hash mismatches — drift", func(t *testing.T) {
		root := t.TempDir()
		cs := &FileChecksums{Unstampable: map[string]string{rel: BodyHash(render)}}
		full := filepath.Join(root, rel)
		if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(full, []byte("{\"a\":2}\n"), 0o644); err != nil {
			t.Fatal(err)
		}
		drift := ScanTier1Drift(root, cs)
		if len(drift) != 1 || drift[0].Path != rel {
			t.Errorf("drift = %+v, want exactly %s", drift, rel)
		}
	})

	t.Run("tracked but deleted — not a stomp candidate", func(t *testing.T) {
		root := t.TempDir()
		cs := &FileChecksums{Unstampable: map[string]string{rel: BodyHash(render)}}
		if drift := ScanTier1Drift(root, cs); len(drift) != 0 {
			t.Errorf("deleted tracked file must not drift: %+v", drift)
		}
	})
}

func TestWriteGeneratedFile_VintageAutoHeal(t *testing.T) {
	renderV1 := []byte("package app // v1\n")
	renderV2 := []byte("package app // v2\n")
	renderV3 := []byte("package app // v3\n")
	const rel = "pkg/app/app_gen.go"

	tests := []struct {
		name      string
		onDisk    func(t *testing.T) []byte
		wantWrote bool
	}{
		{
			name: "on-disk is a stamped recent vintage — clean, write proceeds",
			onDisk: func(t *testing.T) []byte {
				s, _ := Stamp(rel, renderV2)
				return s
			},
			wantWrote: true,
		},
		{
			name: "on-disk is a stamped older vintage — auto-heal, write proceeds",
			onDisk: func(t *testing.T) []byte {
				s, _ := Stamp(rel, renderV1)
				return s
			},
			wantWrote: true,
		},
		{
			name:      "marker mismatches body — hand-edit, write skipped",
			onDisk:    func(t *testing.T) []byte { return handEditStamped(t, rel, renderV2) },
			wantWrote: false,
		},
		{
			// Legacy untracked-path behavior preserved: no marker → write.
			name:      "no marker — untracked existing file, write proceeds",
			onDisk:    func(t *testing.T) []byte { return []byte("package app // user never certified\n") },
			wantWrote: true,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			ResetSkipWrite()
			ResetPerRunState()
			t.Cleanup(ResetPerRunState)
			root := t.TempDir()
			cs := &FileChecksums{}

			onDisk := tt.onDisk(t)
			full := filepath.Join(root, rel)
			if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
				t.Fatal(err)
			}
			if err := os.WriteFile(full, onDisk, 0o644); err != nil {
				t.Fatal(err)
			}

			wrote, err := WriteGeneratedFile(root, rel, renderV3, cs, false)
			if err != nil {
				t.Fatalf("WriteGeneratedFile: %v", err)
			}
			if wrote != tt.wantWrote {
				t.Errorf("wrote=%v, want %v", wrote, tt.wantWrote)
			}

			got, _ := os.ReadFile(full)
			if tt.wantWrote {
				wantStamped, _ := Stamp(rel, renderV3)
				if string(got) != string(wantStamped) {
					t.Errorf("on-disk content not regenerated+stamped; got %q", got)
				}
				if Verify(got) != Pristine {
					t.Errorf("regenerated file must self-certify; Verify = %v", Verify(got))
				}
			} else {
				if string(got) != string(onDisk) {
					t.Errorf("drifted content was overwritten; got %q", got)
				}
			}
		})
	}
}

// TestVerify_VintageClassification pins the underlying predicate both
// the drift scan and the writer delegate to (the replacement for the
// manifest-era IsFileModified): Verify answers from the bytes alone.
func TestVerify_VintageClassification(t *testing.T) {
	const rel = "f.go"
	renderV1 := []byte("a\n")
	renderV2 := []byte("b\n")
	stampedV1, _ := Stamp(rel, renderV1)
	stampedV2, _ := Stamp(rel, renderV2)

	tests := []struct {
		name    string
		content []byte
		want    VerifyStatus
	}{
		{"stamped current render", stampedV2, Pristine},
		{"stamped older vintage", stampedV1, Pristine}, // every vintage self-certifies
		{"marker mutated after stamping", append(append([]byte{}, stampedV2...), []byte("c\n")...), Modified},
		{"no marker", []byte("c\n"), NoMarker},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := Verify(tt.content); got != tt.want {
				t.Errorf("Verify = %v, want %v", got, tt.want)
			}
		})
	}
}
