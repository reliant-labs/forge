// ── Fixture ───────────────────────────────────────────────────────────
//
// A representative Tier-1 Go render whose import block is deliberately
// NON-canonical: one flat group mixing stdlib, third-party, and local
// imports — exactly the shape the contract-mock template emits before
// formatting, and the shape that produced the control-plane 2026-07-08
// stomp-guard false positives once a goimports pass regrouped it.

const goFixtureModule = "example.com/proj"

const goFixtureRel = "internal/svc/handlers_gen.go"

const goFixtureFlatRender = `// Code generated by forge. DO NOT EDIT.
// Source: contract.go in this package.
package svc

import (
	"context"
	"example.com/proj/internal/other"
	"github.com/third/party"
	"strings"
)

// Frob frobs the widget.
func Frob(ctx context.Context) string {
	return strings.ToUpper(party.Name(other.Thing{}))
}
`

// canonicalImportBlock is what the canonical formatter (goimports with
// -local example.com/proj) makes of the flat block above: stdlib /
// third-party / local, blank-line separated, sorted within groups.
const canonicalImportBlock = `import (
	"context"
	"strings"

	"github.com/third/party"

	"example.com/proj/internal/other"
)`

// regroupedImportBlock simulates a DIFFERENTLY-configured formatter pass
// over the canonical file (e.g. goimports without -local, or gci with a
// two-group config): the third-party and local groups merged into one,
// re-sorted lexically. Byte drift, zero semantic drift.
const regroupedImportBlock = `import (
	"context"
	"strings"

	"example.com/proj/internal/other"
	"github.com/third/party"
)`

// newGoFixtureProject seeds a temp project root with a go.mod declaring
// goFixtureModule and writes the flat render through the Tier-1
// chokepoint. Returns the root and the stamped on-disk bytes.
func newGoFixtureProject(t *testing.T) (root string, onDisk []byte) {
	t.Helper()
	ResetSkipWrite()
	ResetPerRunState()
	t.Cleanup(func() {
		ResetSkipWrite()
		ResetPerRunState()
	})

	root = t.TempDir()
	if err := os.WriteFile(filepath.Join(root, "go.mod"),
		[]byte("module "+goFixtureModule+"\n\ngo 1.24\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	wrote, err := WriteGeneratedFile(root, goFixtureRel, []byte(goFixtureFlatRender), &FileChecksums{}, false)
	if err != nil || !wrote {
		t.Fatalf("seed write: wrote=%v err=%v", wrote, err)
	}
	onDisk, err = os.ReadFile(filepath.Join(root, goFixtureRel))
	if err != nil {
		t.Fatal(err)
	}
	return root, onDisk
}

// mutateOnDisk replaces old with new in the fixture file, failing the
// test if old is not present (keeps the literal-block edits honest).
func mutateOnDisk(t *testing.T, root, oldStr, newStr string) {
	t.Helper()
	full := filepath.Join(root, goFixtureRel)
	content, err := os.ReadFile(full)
	if err != nil {
		t.Fatal(err)
	}
	if !bytes.Contains(content, []byte(oldStr)) {
		t.Fatalf("fixture drifted: on-disk content does not contain:\n%s\n--- got ---\n%s", oldStr, content)
	}
	mutated := bytes.Replace(content, []byte(oldStr), []byte(newStr), 1)
	if err := os.WriteFile(full, mutated, 0o644); err != nil {
		t.Fatal(err)
	}
}

// silenceNotices swaps the heal/no-heal notice funcs for counters for
// the duration of the test.
func silenceNotices(t *testing.T) (healed, skipped *int) {
	t.Helper()
	oldHeal, oldSkip := HealNoticeFn, NoHealSkipFn
	h, s := 0, 0
	HealNoticeFn = func(string) { h++ }
	NoHealSkipFn = func(string) { s++ }
	t.Cleanup(func() { HealNoticeFn, NoHealSkipFn = oldHeal, oldSkip })
	return &h, &s
}

// ── Part 1: format-before-stamp ───────────────────────────────────────

// TestWriteGeneratedFile_CanonicalFormatsGoBeforeStamp: a .go render
// with a flat, non-canonical import block is canonical-formatted BEFORE
// the stamp is computed — the on-disk file carries goimports-style
// grouping (module path as local prefix), verifies Pristine, and is a
// fixed point of the canonical formatter.
func TestWriteGeneratedFile_CanonicalFormatsGoBeforeStamp(t *testing.T) {
	_, onDisk := newGoFixtureProject(t)

	if !bytes.Contains(onDisk, []byte(canonicalImportBlock)) {
		t.Fatalf("written file lacks the canonical (stdlib / third-party / local) import grouping:\n%s", onDisk)
	}
	if got := Verify(onDisk); got != Pristine {
		t.Fatalf("Verify(written) = %v, want Pristine", got)
	}
	// The invariant part 2 pins across emitters, asserted here at the
	// chokepoint itself: written output == CanonicalGoSource(output).
	formatted, err := CanonicalGoSource(goFixtureModule, goFixtureRel, onDisk)
	if err != nil {
		t.Fatalf("CanonicalGoSource(written): %v", err)
	}
	if !bytes.Equal(formatted, onDisk) {
		t.Fatalf("written .go file is not a fixed point of the canonical formatter:\n--- written ---\n%s\n--- formatted ---\n%s", onDisk, formatted)
	}
}

// TestGoImportsLocalPrefix: module path resolution — absent go.mod
// yields "", a later-appearing go.mod is picked up (no negative
// caching), and results are stable.
func TestGoImportsLocalPrefix(t *testing.T) {
	root := t.TempDir()
	if got := GoImportsLocalPrefix(root); got != "" {
		t.Fatalf("prefix without go.mod = %q, want empty", got)
	}
	if err := os.WriteFile(filepath.Join(root, "go.mod"), []byte("module example.com/late\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	if got := GoImportsLocalPrefix(root); got != "example.com/late" {
		t.Fatalf("prefix after go.mod appeared = %q, want example.com/late", got)
	}
	if got := GoImportsLocalPrefix(root); got != "example.com/late" {
		t.Fatalf("cached prefix = %q, want example.com/late", got)
	}
}

// ── Part 3: normalize-on-compare in the guard and the writer ──────────

// TestGoFormatterTolerance_RegroupedImportsPass: a goimports-style
// regroup of the import block (byte drift, zero semantic drift) does
// NOT trip the stomp guard, and the next write heals the file back to
// canonical bytes with a fresh stamp — silently, because nothing was
// destroyed.
func TestGoFormatterTolerance_RegroupedImportsPass(t *testing.T) {
	root, _ := newGoFixtureProject(t)
	healed, skipped := silenceNotices(t)

	mutateOnDisk(t, root, canonicalImportBlock, regroupedImportBlock)
	onDisk, _ := os.ReadFile(filepath.Join(root, goFixtureRel))

	// Exact-byte verification MUST still say Modified — the tolerance is
	// deliberately not baked into Verify (non-Go callers keep exact-byte
	// semantics); it lives at the path-aware sites below.
	if got := Verify(onDisk); got != Modified {
		t.Fatalf("Verify(regrouped) = %v, want Modified (exact-byte)", got)
	}

	// The guard's scan classifies the file clean.
	if info := ScanMarkers(root)[goFixtureRel]; info.Status != Pristine {
		t.Fatalf("ScanMarkers status = %v, want Pristine (formatter-only drift)", info.Status)
	}
	cs := &FileChecksums{}
	if drift := ScanTier1Drift(root, cs); len(drift) != 0 {
		t.Fatalf("ScanTier1Drift flagged formatter-only drift: %+v", drift)
	}

	// The next write (same render, NO force, NO heal) self-heals: it
	// overwrites, restoring canonical bytes and a valid stamp.
	ResetSkipWrite()
	wrote, err := WriteGeneratedFile(root, goFixtureRel, []byte(goFixtureFlatRender), cs, false)
	if err != nil {
		t.Fatalf("healing write: %v", err)
	}
	if !wrote {
		t.Fatal("healing write skipped a formatter-drifted file (should overwrite)")
	}
	final, _ := os.ReadFile(filepath.Join(root, goFixtureRel))
	if !bytes.Contains(final, []byte(canonicalImportBlock)) {
		t.Fatalf("heal did not restore canonical import grouping:\n%s", final)
	}
	if got := Verify(final); got != Pristine {
		t.Fatalf("Verify(healed) = %v, want Pristine", got)
	}
	FlushHealNotices(root)
	if *healed != 0 || *skipped != 0 {
		t.Fatalf("formatting-only heal must be silent; got heal=%d skip=%d notices", *healed, *skipped)
	}
}

// TestGoFormatterTolerance_HandEditStillFlags: a genuine code edit
// (surviving canonical formatting) still trips the guard and the writer
// still refuses to overwrite without --force.
func TestGoFormatterTolerance_HandEditStillFlags(t *testing.T) {
	root, _ := newGoFixtureProject(t)
	silenceNotices(t)

	mutateOnDisk(t, root,
		"return strings.ToUpper(party.Name(other.Thing{}))",
		"return strings.ToLower(party.Name(other.Thing{}))")

	drift := ScanTier1Drift(root, &FileChecksums{})
	if len(drift) != 1 || drift[0].Path != goFixtureRel {
		t.Fatalf("hand-edit not flagged by the stomp guard: %+v", drift)
	}

	edited, _ := os.ReadFile(filepath.Join(root, goFixtureRel))
	ResetSkipWrite()
	wrote, err := WriteGeneratedFile(root, goFixtureRel, []byte(goFixtureFlatRender), &FileChecksums{}, false)
	if err != nil {
		t.Fatalf("write over hand-edit: %v", err)
	}
	if wrote {
		t.Fatal("writer overwrote a hand-edited file without --force")
	}
	final, _ := os.ReadFile(filepath.Join(root, goFixtureRel))
	if !bytes.Equal(final, edited) {
		t.Fatal("hand-edited bytes were not preserved")
	}
}

