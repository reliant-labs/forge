// TestGoFormatterTolerance_OlderVintageKeepsNonDestructiveDefault: a
// formatter-drifted file whose certified body is an OLDER render than
// the current template gets the same treatment as Pristine-but-stale —
// skip loudly by default, overwrite only under --heal.
func TestGoFormatterTolerance_OlderVintageKeepsNonDestructiveDefault(t *testing.T) {
	root, _ := newGoFixtureProject(t)
	_, skipped := silenceNotices(t)

	mutateOnDisk(t, root, canonicalImportBlock, regroupedImportBlock)

	// A NEW render vintage: the function body changed.
	v2 := strings.Replace(goFixtureFlatRender,
		"return strings.ToUpper(party.Name(other.Thing{}))",
		"return strings.ToLower(party.Name(other.Thing{}))", 1)

	ResetSkipWrite()
	wrote, err := WriteGeneratedFile(root, goFixtureRel, []byte(v2), &FileChecksums{}, false)
	if err != nil {
		t.Fatalf("v2 write: %v", err)
	}
	if wrote {
		t.Fatal("older-vintage formatter drift was overwritten without --heal/--force")
	}
	if *skipped != 1 {
		t.Fatalf("no-heal skip notice fired %d times, want 1", *skipped)
	}

	// --heal opts in: the write advances the file to v2, canonical.
	AutoHeal = true
	wrote, err = WriteGeneratedFile(root, goFixtureRel, []byte(v2), &FileChecksums{}, false)
	if err != nil {
		t.Fatalf("v2 heal write: %v", err)
	}
	if !wrote {
		t.Fatal("--heal did not advance an older-vintage formatter-drifted file")
	}
	final, _ := os.ReadFile(filepath.Join(root, goFixtureRel))
	if !bytes.Contains(final, []byte("strings.ToLower")) || !bytes.Contains(final, []byte(canonicalImportBlock)) {
		t.Fatalf("healed file is not the canonical v2 render:\n%s", final)
	}
	if got := Verify(final); got != Pristine {
		t.Fatalf("Verify(healed v2) = %v, want Pristine", got)
	}
}

// TestFormatterTolerance_NonGoKeepsExactByteSemantics: the tolerance is
// Go-only. A whitespace-only touch to a stamped YAML file is still a
// hand-edit.
func TestFormatterTolerance_NonGoKeepsExactByteSemantics(t *testing.T) {
	ResetSkipWrite()
	ResetPerRunState()
	t.Cleanup(func() {
		ResetSkipWrite()
		ResetPerRunState()
	})

	root := t.TempDir()
	rel := ".github/workflows/ci.yml"
	render := []byte("# Code generated by forge. DO NOT EDIT.\nname: ci\non: push\n")
	if wrote, err := WriteGeneratedFile(root, rel, render, &FileChecksums{}, false); err != nil || !wrote {
		t.Fatalf("seed yaml write: wrote=%v err=%v", wrote, err)
	}
	onDisk, _ := os.ReadFile(filepath.Join(root, rel))
	mutated := bytes.Replace(onDisk, []byte("name: ci"), []byte("name:  ci"), 1)
	if err := os.WriteFile(filepath.Join(root, rel), mutated, 0o644); err != nil {
		t.Fatal(err)
	}
	drift := ScanTier1Drift(root, &FileChecksums{})
	if len(drift) != 1 || drift[0].Path != rel {
		t.Fatalf("non-Go whitespace edit not flagged: %+v", drift)
	}
}

// TestCanonicalGoSource_Idempotent: the canonical formatter is a true
// normalizer — applying it twice equals applying it once, for both the
// flat and the regrouped variants, and both converge to the SAME bytes
// (that convergence is what lets normalize-on-compare recognize
// formatter noise).
func TestCanonicalGoSource_Idempotent(t *testing.T) {
	flat := []byte(goFixtureFlatRender)
	flatBlock := "import (\n\t\"context\"\n\t\"example.com/proj/internal/other\"\n\t\"github.com/third/party\"\n\t\"strings\"\n)"
	if !bytes.Contains(flat, []byte(flatBlock)) {
		t.Fatalf("fixture drifted: flat render lacks the expected import block")
	}
	regrouped := bytes.Replace(flat, []byte(flatBlock), []byte(regroupedImportBlock), 1)

	once, err := CanonicalGoSource(goFixtureModule, goFixtureRel, flat)
	if err != nil {
		t.Fatal(err)
	}
	twice, err := CanonicalGoSource(goFixtureModule, goFixtureRel, once)
	if err != nil {
		t.Fatal(err)
	}
	if !bytes.Equal(once, twice) {
		t.Fatalf("CanonicalGoSource is not idempotent:\n--- once ---\n%s\n--- twice ---\n%s", once, twice)
	}
	fromRegrouped, err := CanonicalGoSource(goFixtureModule, goFixtureRel, regrouped)
	if err != nil {
		t.Fatal(err)
	}
	if !bytes.Equal(once, fromRegrouped) {
		t.Fatalf("flat and regrouped variants do not converge to the same canonical form:\n--- from flat ---\n%s\n--- from regrouped ---\n%s", once, fromRegrouped)
	}
}
