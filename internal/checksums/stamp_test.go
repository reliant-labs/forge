// stampSamples covers every Tier-1 output format forge emits (the
// comment-syntax matrix). Content deliberately includes the standard
// generated banner in some samples and omits it in others.
var stampSamples = []struct {
	path    string
	content string
	leader  string // expected marker-line prefix
}{
	{"pkg/app/wire_gen.go", "// Code generated by forge. DO NOT EDIT.\n// Source: forge.yaml\n\npackage app\n\nfunc Wire() {}\n", "// forge:hash="},
	{"frontends/web/src/lib/apiurl_gen.ts", "// Code generated by forge. DO NOT EDIT.\nexport const apiURL = \"/\";\n", "// forge:hash="},
	{"frontends/web/src/components/nav_gen.tsx", "\"use client\";\n\n// Code generated by forge. DO NOT EDIT.\nexport function Nav() { return null; }\n", "// forge:hash="},
	{".github/workflows/ci.yml", "# Code generated by forge. DO NOT EDIT.\nname: ci\non: push\n", "# forge:hash="},
	{"deploy/grafana/dashboard.yaml", "# Code generated by forge. DO NOT EDIT.\napiVersion: 1\n", "# forge:hash="},
	{"deploy/kcl/dev/config_gen.k", "\"\"\"\nGenerated per-environment config.\n\"\"\"\nschema Config:\n    name: str\n", "# forge:hash="},
	{"ingress/gateway_k3d.kcl", "# Code generated by forge. DO NOT EDIT.\nports = [80]\n", "# forge:hash="},
	{"deploy/alloy-config.alloy", "// Code generated by forge. DO NOT EDIT.\nlogging {}\n", "// forge:hash="},
	{"db/plans/users_plan_gen.sql", "-- Code generated by forge. DO NOT EDIT.\nSELECT 1;\n", "-- forge:hash="},
	{".claude/skills/db/SKILL.md", "---\nname: db\n---\n<!-- Code generated by forge. DO NOT EDIT. -->\n\n# DB skill\n", "<!-- forge:hash="},
	{"proto/forge/v1/forge.proto", "// Code generated by forge. DO NOT EDIT.\nsyntax = \"proto3\";\n", "// forge:hash="},
}

// TestStampVerifyFixedPoint: stamp → verify → restamp is a fixed point
// for every supported format, and the stamped output verifies Pristine.
func TestStampVerifyFixedPoint(t *testing.T) {
	for _, s := range stampSamples {
		t.Run(s.path, func(t *testing.T) {
			stamped, ok := Stamp(s.path, []byte(s.content))
			if !ok {
				t.Fatalf("Stamp(%s) reported unstampable", s.path)
			}
			if !strings.Contains(string(stamped), s.leader) {
				t.Fatalf("stamped %s missing marker leader %q:\n%s", s.path, s.leader, stamped)
			}
			if got := Verify(stamped); got != Pristine {
				t.Fatalf("Verify(stamped) = %v, want Pristine", got)
			}
			restamped, ok := Stamp(s.path, stamped)
			if !ok {
				t.Fatalf("restamp reported unstampable")
			}
			if !bytes.Equal(stamped, restamped) {
				t.Fatalf("stamp is not a fixed point:\n--- first ---\n%s\n--- second ---\n%s", stamped, restamped)
			}
			// The marker must appear exactly once.
			if n := strings.Count(string(stamped), "forge:hash="); n != 1 {
				t.Fatalf("marker appears %d times, want 1:\n%s", n, stamped)
			}
		})
	}
}

// TestStampSingleByteMutation: flipping any single non-marker-line byte
// of a stamped file breaks verification (Modified), for every format.
func TestStampSingleByteMutation(t *testing.T) {
	rng := rand.New(rand.NewSource(42))
	for _, s := range stampSamples {
		stamped, ok := Stamp(s.path, []byte(s.content))
		if !ok {
			t.Fatalf("Stamp(%s) unstampable", s.path)
		}
		// Identify the marker line's byte range so mutations stay in the
		// body (mutating the marker itself is covered separately).
		markerStart := bytes.Index(stamped, []byte("forge:hash="))
		lineStart := bytes.LastIndexByte(stamped[:markerStart], '\n') + 1
		lineEnd := markerStart + bytes.IndexByte(stamped[markerStart:], '\n')
		if lineEnd < markerStart {
			lineEnd = len(stamped)
		}
		mutations := 0
		for try := 0; try < 200 && mutations < 25; try++ {
			i := rng.Intn(len(stamped))
			if i >= lineStart && i <= lineEnd {
				continue // inside the marker line
			}
			if stamped[i] == '\n' || stamped[i] == '\r' {
				continue // newline flips can merge lines into the marker line; covered by design doc
			}
			mut := append([]byte(nil), stamped...)
			mut[i] ^= 0x01
			if got := Verify(mut); got != Modified {
				t.Fatalf("%s: single-byte flip at %d not detected: Verify = %v\noriginal byte %q",
					s.path, i, got, stamped[i])
			}
			mutations++
		}
		if mutations == 0 {
			t.Fatalf("%s: no mutations exercised", s.path)
		}
	}
}

// TestStampMarkerTamper: editing the embedded hash itself is Modified,
// and deleting the marker line entirely is NoMarker.
func TestStampMarkerTamper(t *testing.T) {
	s := stampSamples[0]
	stamped, _ := Stamp(s.path, []byte(s.content))
	tampered := bytes.Replace(stamped, []byte("forge:hash="), []byte("forge:hash=0000"), 1)
	if got := Verify(tampered); got != Modified {
		t.Fatalf("tampered marker: Verify = %v, want Modified", got)
	}
	if got := Verify(StripMarker(stamped)); got != NoMarker {
		t.Fatalf("stripped marker: Verify = %v, want NoMarker", got)
	}
	if got := Verify([]byte("package app\n")); got != NoMarker {
		t.Fatalf("plain content: Verify = %v, want NoMarker", got)
	}
}

// TestStampLineEndingAndTrailingNewlineNormalization pins the
// documented normalization rules: CRLF line endings (git autocrlf
// checkouts) and trailing-newline count do not affect verification.
func TestStampLineEndingAndTrailingNewlineNormalization(t *testing.T) {
	s := stampSamples[0]
	stamped, _ := Stamp(s.path, []byte(s.content))

	crlf := bytes.ReplaceAll(stamped, []byte("\n"), []byte("\r\n"))
	if got := Verify(crlf); got != Pristine {
		t.Fatalf("CRLF re-encoding flagged: Verify = %v, want Pristine", got)
	}

	extraNL := append(append([]byte(nil), stamped...), '\n', '\n')
	if got := Verify(extraNL); got != Pristine {
		t.Fatalf("extra trailing newlines flagged: Verify = %v, want Pristine", got)
	}

	missingNL := bytes.TrimRight(stamped, "\n")
	if got := Verify(missingNL); got != Pristine {
		t.Fatalf("stripped final newline flagged: Verify = %v, want Pristine", got)
	}
}

// TestStampInsertionPosition pins where the marker line lands:
// immediately after the generated banner when present; after YAML
// frontmatter for markdown; after the shebang for scripts; at the top
// otherwise.
func TestStampInsertionPosition(t *testing.T) {
	cases := []struct {
		name, path, content, wantBefore string
		wantLine                        int // 0-based line index of the marker
	}{
		{"after banner", "a.go", "// Code generated by forge. DO NOT EDIT.\npackage a\n", "", 1},
		{"banner not first line", "n.tsx", "\"use client\";\n\n// Code generated by forge. DO NOT EDIT.\nexport {};\n", "", 3},
		{"frontmatter md", "s/SKILL.md", "---\nname: s\n---\nbody\n", "", 3},
		{"shebang", "tools/run.sh", "#!/bin/sh\necho hi\n", "", 1},
		{"plain top", "deploy/x.yaml", "apiVersion: 1\n", "", 0},
	}
	for _, c := range cases {
		t.Run(c.name, func(t *testing.T) {
			stamped, ok := Stamp(c.path, []byte(c.content))
			if !ok {
				t.Fatalf("unstampable")
			}
			lines := strings.Split(string(stamped), "\n")
			for i, l := range lines {
				if strings.Contains(l, "forge:hash=") {
					if i != c.wantLine {
						t.Fatalf("marker on line %d, want %d:\n%s", i, c.wantLine, stamped)
					}
					return
				}
			}
			t.Fatalf("no marker emitted:\n%s", stamped)
		})
	}
}

// TestStampUnstampableFormats: comment-incapable formats must refuse to
// stamp so callers can route them to the scoped fallback manifest.
func TestStampUnstampableFormats(t *testing.T) {
	for _, p := range []string{"gen/mcp/manifest.json", "data.json", "img.png"} {
		if _, ok := Stamp(p, []byte("{}")); ok {
			t.Errorf("Stamp(%s) must report unstampable", p)
		}
		if Stampable(p) {
			t.Errorf("Stampable(%s) must be false", p)
		}
	}
	for _, p := range []string{"a.go", "b.yaml", "c.sql", "d.md", "e.k", "Dockerfile", ".gitignore"} {
		if !Stampable(p) {
			t.Errorf("Stampable(%s) must be true", p)
		}
	}
}

// TestUnverifiedMarker: the migration's "provenance unknown" sentinel
// always fails verification (Modified) so the guard keeps applying
