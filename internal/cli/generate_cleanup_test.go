// TestCleanupStaleArtifacts_RemovesStaleFrontendHooks targets <fe>/src/hooks/.
func TestCleanupStaleArtifacts_RemovesStaleFrontendHooks(t *testing.T) {
	dir := t.TempDir()

	mustMkdir(t, filepath.Join(dir, "frontends", "web", "src", "hooks"))
	mustWrite(t, filepath.Join(dir, "frontends", "web", "src", "hooks", "kept-service-hooks.ts"), "// kept hooks")
	mustWrite(t, filepath.Join(dir, "frontends", "web", "src", "hooks", "stale-service-hooks.ts"), "// stale hooks")

	cfg := &config.ProjectConfig{
		Frontends: []config.FrontendConfig{{Name: "web", Type: "nextjs", Path: "frontends/web"}},
	}
	services := []codegen.ServiceDef{
		{Name: "KeptService"},
	}

	removed, err := cleanupStaleArtifacts(cfg, services, dir)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	if _, err := os.Stat(filepath.Join(dir, "frontends", "web", "src", "hooks", "kept-service-hooks.ts")); err != nil {
		t.Errorf("kept hook removed: %v", err)
	}
	if _, err := os.Stat(filepath.Join(dir, "frontends", "web", "src", "hooks", "stale-service-hooks.ts")); !os.IsNotExist(err) {
		t.Errorf("stale hook not removed: %v (removed=%v)", err, removed)
	}
}

// TestCleanupStaleArtifacts_NoServices_DoesNothing is a guard: when called
// with no services and no preexisting state, the cleanup is a clean no-op.
func TestCleanupStaleArtifacts_NoServices_DoesNothing(t *testing.T) {
	dir := t.TempDir()
	removed, err := cleanupStaleArtifacts(nil, nil, dir)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	if len(removed) != 0 {
		t.Errorf("expected 0 removed, got %d: %v", len(removed), removed)
	}
}

func mustMkdir(t *testing.T, path string) {
	t.Helper()
	if err := os.MkdirAll(path, 0o755); err != nil {
		t.Fatalf("mkdir %s: %v", path, err)
	}
}

func mustWrite(t *testing.T, path, content string) {
	t.Helper()
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatalf("write %s: %v", path, err)
	}
}
