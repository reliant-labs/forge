// TestCleanupStaleArtifacts_RemovesStaleGenServices asserts gen/services/<svc>/
// directories are removed when no proto service of that name remains.
func TestCleanupStaleArtifacts_RemovesStaleGenServices(t *testing.T) {
	dir := t.TempDir()

	// Layout: gen/services/{kept, stale}/v1/somefile.go
	mustMkdir(t, filepath.Join(dir, "gen", "services", "kept", "v1"))
	mustMkdir(t, filepath.Join(dir, "gen", "services", "stale", "v1"))
	mustWrite(t, filepath.Join(dir, "gen", "services", "kept", "v1", "f.go"), "package keptv1")
	mustWrite(t, filepath.Join(dir, "gen", "services", "stale", "v1", "f.go"), "package stalev1")

	services := []codegen.ServiceDef{
		{Name: "KeptService"},
	}

	removed, err := cleanupStaleArtifacts(nil, services, dir)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	if len(removed) != 1 {
		t.Fatalf("expected 1 removed path, got %d: %v", len(removed), removed)
	}

	// kept must remain
	if _, err := os.Stat(filepath.Join(dir, "gen", "services", "kept", "v1", "f.go")); err != nil {
		t.Errorf("kept gen file removed: %v", err)
	}
	// stale must be gone
	if _, err := os.Stat(filepath.Join(dir, "gen", "services", "stale")); !os.IsNotExist(err) {
		t.Errorf("stale gen dir not removed: %v", err)
	}
}

// TestCleanupStaleArtifacts_RemovesStaleHandlerDir_OnlyGenerated removes the
// handler dir when every file in it is generated.
func TestCleanupStaleArtifacts_RemovesStaleHandlerDir_OnlyGenerated(t *testing.T) {
	dir := t.TempDir()

	mustMkdir(t, filepath.Join(dir, "handlers", "stale"))
	mustWrite(t, filepath.Join(dir, "handlers", "stale", "handlers_gen.go"), "// Code generated by forge. DO NOT EDIT.\npackage stale")
	mustWrite(t, filepath.Join(dir, "handlers", "stale", "service.go"), "// Code generated by forge. DO NOT EDIT.\npackage stale")

	removed, err := cleanupStaleArtifacts(nil, nil, dir)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	if _, err := os.Stat(filepath.Join(dir, "handlers", "stale")); !os.IsNotExist(err) {
		t.Errorf("stale handler dir not removed: %v (removed=%v)", err, removed)
	}
}

// TestCleanupStaleArtifacts_PreservesUserOwnedHandlerFiles leaves user files
// in place when a handler dir contains hand-edited code.
func TestCleanupStaleArtifacts_PreservesUserOwnedHandlerFiles(t *testing.T) {
	dir := t.TempDir()

	mustMkdir(t, filepath.Join(dir, "handlers", "stale"))
	mustWrite(t, filepath.Join(dir, "handlers", "stale", "handlers_gen.go"), "// Code generated by forge. DO NOT EDIT.\npackage stale")
	// User-owned file (no banner)
	mustWrite(t, filepath.Join(dir, "handlers", "stale", "handlers.go"), "package stale\n\n// my code")

	removed, err := cleanupStaleArtifacts(nil, nil, dir)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	// handlers_gen.go removed
	if _, err := os.Stat(filepath.Join(dir, "handlers", "stale", "handlers_gen.go")); !os.IsNotExist(err) {
		t.Errorf("handlers_gen.go not removed: %v", err)
	}
	// handlers.go preserved
	if _, err := os.Stat(filepath.Join(dir, "handlers", "stale", "handlers.go")); err != nil {
		t.Errorf("user-owned handlers.go was removed: %v", err)
	}
	if len(removed) != 1 {
		t.Errorf("expected 1 removed gen file, got %d: %v", len(removed), removed)
	}
}

