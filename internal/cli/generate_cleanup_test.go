// TestCleanupStaleArtifacts_CPForgeRegression is the cp-forge dogfood
// case that motivated this guard. Post-2026-06-08, the canonical Go-
// package form for proto service "AdminServerService" is the snake-case
// "admin_server" (aligning with the universal proto / KCL / handler dir
// convention). Pre-2026-06-08 forge briefly emitted the compact form
// "adminserver" — that's the stale layout this run's cleanup must wipe.
//
// Setup mirrors the on-disk state of a project upgrading past the
// compact-form interlude: the compact handlers/adminserver/ tree
// carries forge's certification marker from a prior run, this run wrote
// the snake handlers/admin_server/<files>. The OLD compact _gen.go path
// is stale. User-owned authorizer.go in the compact dir (no marker)
// MUST be preserved — cleanup deletes only forge-certified files.
//
// Pre-rewrite, the cleanup walked handlers/ and re-derived
// `serviceNameToPackage` from forge.yaml to decide what to keep; the
// derived name didn't match the legacy on-disk dir, so the whole
// stale tree got wiped — INCLUDING user-owned files.
func TestCleanupStaleArtifacts_CPForgeRegression(t *testing.T) {
	resetCleanupRunState(t)
	dir := t.TempDir()

	// Compact "old" path — certified (forge wrote it on a prior run
	// under the pre-2026-06-08 compact convention), not in WrittenThisRun.
	mustMkdir(t, filepath.Join(dir, "handlers", "adminserver"))
	oldGen := filepath.Join(dir, "handlers", "adminserver", "handlers_crud_gen.go")
	mustWriteStamped(t, oldGen, "handlers/adminserver/handlers_crud_gen.go",
		"// Code generated by forge. DO NOT EDIT.\npackage adminserver\n")

	// User-owned file in the same compact dir — no marker.
	// Must survive cleanup.
	userAuthz := filepath.Join(dir, "handlers", "adminserver", "authorizer.go")
	mustWrite(t, userAuthz, "package adminserver\n// hand-written\n")

	// Snake_case path — written this run (the post-2026-06-08 canonical form).
	mustMkdir(t, filepath.Join(dir, "handlers", "admin_server"))
	newGen := filepath.Join(dir, "handlers", "admin_server", "handlers_crud_gen.go")
	mustWriteStamped(t, newGen, "handlers/admin_server/handlers_crud_gen.go",
		"// Code generated by forge. DO NOT EDIT.\npackage admin_server\n")

	cs := &checksums.FileChecksums{}
	checksums.MarkWrittenThisRun("handlers/admin_server/handlers_crud_gen.go")

	ctx := newCleanupCtx(dir, cs, []codegen.ServiceDef{{Name: "AdminServerService"}}, nil)
	removed, _, err := cleanupStaleArtifacts(ctx)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}

	// OLD generated path removed.
	if _, err := os.Stat(oldGen); !os.IsNotExist(err) {
		t.Errorf("old generated path not removed: %v", err)
	}
	// User-owned file in the same dir preserved.
	if _, err := os.Stat(userAuthz); err != nil {
		t.Errorf("user-owned authorizer.go was deleted: %v", err)
	}
	// New generated path preserved.
	if _, err := os.Stat(newGen); err != nil {
		t.Errorf("new generated path was deleted: %v", err)
	}

	// Confirm `removed` reports the old path exactly.
	wantRemoved := []string{oldGen}
	if !reflect.DeepEqual(removed, wantRemoved) {
		t.Errorf("removed = %v, want %v", removed, wantRemoved)
	}
}

