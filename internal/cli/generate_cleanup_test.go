// Tests for the marker-driven stale-artifact cleanup
// (generate_cleanup.go). In the self-certifying era the candidate set
// comes from scanning for embedded forge:hash markers (plus the scoped
// .forge/hashes.json fallback), not from a manifest walk: a file is a
// deletion candidate only when it carries forge's certification, the
// current run did not re-emit it, its owning emitter step is active,
// it is not disowned / upgrade-managed, and its marker still VERIFIES
// (pristine machine output). Hand-edited stale files are reported in
// the handEdited slice and never deleted.
package cli

import (
	"os"
	"path/filepath"
	"reflect"
	"strings"
	"testing"

	"github.com/reliant-labs/forge/internal/checksums"
	"github.com/reliant-labs/forge/internal/codegen"
	"github.com/reliant-labs/forge/internal/config"
)

// resetCleanupRunState clears the package-level WrittenThisRun /
// SkipWrite maps so individual test cases don't leak through the
// shared `checksums` package state.
func resetCleanupRunState(t *testing.T) {
	t.Helper()
	checksums.ResetSkipWrite()
}

// mustWriteStamped writes content at path carrying its embedded
// forge:hash marker — the "forge wrote this on a prior run and nobody
// touched it" (pristine) shape. rel is the project-relative slash path
// used to pick the marker's comment syntax.
func mustWriteStamped(t *testing.T, path, rel, content string) {
	t.Helper()
	stamped, ok := checksums.Stamp(rel, []byte(content))
	if !ok {
		t.Fatalf("stamp %s: format is unstampable", rel)
	}
	mustWrite(t, path, string(stamped))
}

// newCleanupCtx builds a minimal pipelineContext sufficient for the
// marker-driven cleanup pass. ProjectDir/AbsPath are the temp dir,
// Checksums holds the disowned/unstampable state, HasServices is set
// to true so the owner gates that key on HasServices behave normally
// for cleanup tests.
//
// ForceCleanup defaults to true here so the pre-2026 deletion-asserting
// test cases continue to exercise the destructive branch. Tests covering
// the new report-only default behavior construct the context inline
// (or flip the field back to false) — see
// TestCleanupStaleArtifacts_ReportOnlyByDefault below.
func newCleanupCtx(projectDir string, cs *checksums.FileChecksums, services []codegen.ServiceDef, cfg *config.ProjectConfig) *pipelineContext {
	return &pipelineContext{
		ProjectDir:   projectDir,
		AbsPath:      projectDir,
		Cfg:          cfg,
		Checksums:    cs,
		HasServices:  len(services) > 0,
		Services:     services,
		ConfigFields: map[string]bool{},
		ForceCleanup: true,
	}
}

// TestCleanupStaleArtifacts_DeletesCertifiedFileNotWrittenThisRun
// verifies the core marker-driven rule: a pristine certified file this
// run did NOT re-write gets deleted, and an unmarked (user-owned) file
// is left alone.
func TestCleanupStaleArtifacts_DeletesCertifiedFileNotWrittenThisRun(t *testing.T) {
	resetCleanupRunState(t)
	dir := t.TempDir()

	// stale: certified (pristine marker), not written this run → deleted
	mustMkdir(t, filepath.Join(dir, "handlers", "stale"))
	stale := filepath.Join(dir, "handlers", "stale", "handlers_crud_gen.go")
	mustWriteStamped(t, stale, "handlers/stale/handlers_crud_gen.go",
		"// Code generated by forge. DO NOT EDIT.\npackage stale\n")

	// user-owned: no marker → preserved
	userFile := filepath.Join(dir, "handlers", "stale", "user_code.go")
	mustWrite(t, userFile, "package stale\n// user code\n")

	cs := &checksums.FileChecksums{}
	// Nothing marked written-this-run: the certified file is the candidate.

	ctx := newCleanupCtx(dir, cs, []codegen.ServiceDef{{Name: "KeptService"}}, nil)
	removed, handEdited, err := cleanupStaleArtifacts(ctx)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	if len(removed) != 1 || removed[0] != stale {
		t.Errorf("removed = %v, want exactly %q", removed, stale)
	}
	if len(handEdited) != 0 {
		t.Errorf("handEdited = %v, want empty", handEdited)
	}
	if _, err := os.Stat(stale); !os.IsNotExist(err) {
		t.Errorf("stale not removed: %v", err)
	}
	if _, err := os.Stat(userFile); err != nil {
		t.Errorf("user file was removed: %v", err)
	}
	// The authorship record (the embedded marker) is deleted WITH the
	// file — there is no manifest entry left to prune.
}

