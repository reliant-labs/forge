// NOTE: the manifest-era TestTrackedMissingFiles is gone with the
// manifest — there is no record of absent files to report
// (`tracked_missing_files` left the audit surface too). Deletion of a
// generated file now simply means "re-emit on the next generate", and
// for disowned files it is the documented re-adoption signal.

func mustMkdir(t *testing.T, path string) {
	t.Helper()
	if err := os.MkdirAll(path, 0o755); err != nil {
		t.Fatalf("mkdir %s: %v", path, err)
	}
}

func mustWrite(t *testing.T, path, content string) {
	t.Helper()
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatalf("write %s: %v", path, err)
	}
}

