// TestCleanupStaleArtifacts_ReportOnlyByDefault verifies the new
// default-deny posture: ForceCleanup=false returns the stale candidate
// list (so stepCleanupStale can print a warning) but does NOT delete
// the file or prune the manifest entry.
func TestCleanupStaleArtifacts_ReportOnlyByDefault(t *testing.T) {
	resetCleanupRunState(t)
	dir := t.TempDir()

	mustMkdir(t, filepath.Join(dir, "handlers", "stale"))
	stale := filepath.Join(dir, "handlers", "stale", "handlers_crud_gen.go")
	mustWrite(t, stale, "// Code generated by forge. DO NOT EDIT.\npackage stale\n")

	cs := &checksums.FileChecksums{Files: map[string]checksums.FileChecksumEntry{
		"handlers/stale/handlers_crud_gen.go": {Hash: "abc", History: []string{"abc"}, Tier: 1},
	}}

	ctx := newCleanupCtx(dir, cs, []codegen.ServiceDef{{Name: "KeptService"}}, nil)
	ctx.ForceCleanup = false // explicit: report-only mode (the new default)

	candidates, missing, err := cleanupStaleArtifacts(ctx)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	if len(missing) != 0 {
		t.Errorf("missing = %v, want empty", missing)
	}
	// The candidate is reported so the caller can warn.
	if len(candidates) != 1 || candidates[0] != stale {
		t.Errorf("candidates = %v, want exactly %q", candidates, stale)
	}
	// File still on disk — report-only must not delete.
	if _, err := os.Stat(stale); err != nil {
		t.Errorf("stale file deleted in report-only mode: %v", err)
	}
	// Manifest entry preserved so a subsequent --force-cleanup run can
	// still find and delete it.
	if _, ok := cs.Files["handlers/stale/handlers_crud_gen.go"]; !ok {
		t.Errorf("manifest entry pruned in report-only mode")
	}
}

