// File: internal/cli/generate_cleanup_upgrade_managed_test.go
//
// Tests that `forge generate`'s stale-artifact sweep does NOT flag
// files managed by `forge upgrade` as stale. The regression these
// tests pin: pre-fix, `forge generate` warned that 7 actively-used
// files (cmd/main.go, .golangci.yml, .github/CODEOWNERS, etc.) were
// "stale" and would-be-deleted with --force-cleanup. The user's only
// workaround was hand-flipping `forked: true` in checksums.json, which
// silenced the warning but also broke `forge upgrade`'s ability to
// re-emit the file on the next version bump.
//
// The fix consults generator.UpgradeManagedPaths() — the union over
// every kind/binary combination of the upgrade-managed file list —
// and excludes those paths from the deletion candidate list. (The
// manifest-era inverse "tracked but missing" finding is gone with the
// manifest: nothing records absent files anymore.)
package cli

import (
	"path/filepath"
	"reflect"
	"sort"
	"testing"

	"github.com/reliant-labs/forge/internal/checksums"
	"github.com/reliant-labs/forge/internal/codegen"
	"github.com/reliant-labs/forge/internal/generator"
)

// TestCleanupStaleArtifacts_SkipsUpgradeManagedPaths is the headline
// pin: cmd/main.go, .golangci.yml, and .github/CODEOWNERS all carry
// forge's certification marker AND none are written by this `forge
// generate` run AND no owner gate covers them — pre-fix the cleanup
// pass would have flagged them as stale. Post-fix they're excluded
// because UpgradeManagedPaths() lists them.
func TestCleanupStaleArtifacts_SkipsUpgradeManagedPaths(t *testing.T) {
	resetCleanupRunState(t)
	dir := t.TempDir()

	managed := []string{
		"cmd/main.go",
		"cmd/db.go",
		"cmd/version.go",
		".golangci.yml",
		".github/CODEOWNERS",
		".github/pull_request_template.md",
		".github/workflows/e2e.yml",
	}
	// Put each managed file on disk WITH a pristine marker so the
	// marker scan really does see them — we want to prove the
	// UpgradeManagedPaths exclusion fires, not "no marker, no scan".
	for _, p := range managed {
		full := filepath.Join(dir, p)
		mustMkdir(t, filepath.Dir(full))
		mustWriteStamped(t, full, p, "// Code generated by forge. DO NOT EDIT.\npackage main\n")
	}
	cs := &checksums.FileChecksums{}

	ctx := newCleanupCtx(dir, cs, []codegen.ServiceDef{{Name: "SomeService"}}, nil)
	removed, handEdited, err := cleanupStaleArtifacts(ctx)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	if len(removed) != 0 {
		t.Errorf("removed = %v, want empty (upgrade-managed paths must not be flagged stale)", removed)
	}
	if len(handEdited) != 0 {
		t.Errorf("handEdited = %v, want empty", handEdited)
	}
}

// TestUpgradeManagedPaths_CoversReportedFiles confirms the helper
// returns at least the 7 paths the cp-forge audit-cleanup agent
// reported as false-positive stale candidates. Pins the contract that
// these paths are recognized as upgrade-managed (and so cleanup
// excludes them). If a future refactor moves any of these to be
// `forge generate`-emitted, that emitter will also call
// WrittenThisRun for the path and the exclusion becomes redundant
// (safe) — but we still want explicit coverage here so the
// inventory doesn't silently shrink.
func TestUpgradeManagedPaths_CoversReportedFiles(t *testing.T) {
	got := generator.UpgradeManagedPaths()
	want := []string{
		"cmd/main.go",
		"cmd/db.go",
		"cmd/version.go",
		".golangci.yml",
		".github/CODEOWNERS",
		".github/pull_request_template.md",
		".github/workflows/e2e.yml",
		// Tier-1 cmd files that also live under upgrade ownership.
		"cmd/server.go",
		"cmd/otel.go",
		"buf.yaml",
		// Tier-2 scaffolds also upgrade-managed.
		"Taskfile.yml",
		"Dockerfile",
		"docker-compose.yml",
		".gitignore",
		"deploy/alloy-config.alloy",
	}
	var missing []string
	for _, p := range want {
		if !got[p] {
			missing = append(missing, p)
		}
	}
	if len(missing) > 0 {
		sort.Strings(missing)
		t.Errorf("UpgradeManagedPaths() missing expected entries: %v", missing)
	}
}

// TestCleanupStaleArtifacts_StillFlagsRealStaleAfterExclusion is the
// negative case: the exclusion is narrowly scoped to upgrade-managed
// paths. A real codegen-emitted (certified) file that the run didn't
// re-write must still surface as a candidate so the user can either
// --force-cleanup or rename their service back.
func TestCleanupStaleArtifacts_StillFlagsRealStaleAfterExclusion(t *testing.T) {
	resetCleanupRunState(t)
	dir := t.TempDir()

	// Mix one upgrade-managed path with one real stale codegen file —
	// both certified.
	mustMkdir(t, filepath.Join(dir, "cmd"))
	mustWriteStamped(t, filepath.Join(dir, "cmd", "main.go"), "cmd/main.go",
		"// Code generated by forge. DO NOT EDIT.\npackage main\n")

	mustMkdir(t, filepath.Join(dir, "handlers", "renamed"))
	stale := filepath.Join(dir, "handlers", "renamed", "handlers_crud_gen.go")
	mustWriteStamped(t, stale, "handlers/renamed/handlers_crud_gen.go",
		"// Code generated by forge. DO NOT EDIT.\npackage renamed\n")

	cs := &checksums.FileChecksums{}

	ctx := newCleanupCtx(dir, cs, []codegen.ServiceDef{{Name: "KeptService"}}, nil)
	removed, _, err := cleanupStaleArtifacts(ctx)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	// Only the renamed handler should be flagged stale; cmd/main.go
	// excluded by UpgradeManagedPaths.
	want := []string{stale}
	if !reflect.DeepEqual(removed, want) {
		t.Errorf("removed = %v, want %v", removed, want)
	}
}

