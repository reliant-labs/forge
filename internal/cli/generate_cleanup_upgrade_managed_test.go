// File: internal/cli/generate_cleanup_upgrade_managed_test.go
//
// Tests that `forge generate`'s stale-artifact sweep does NOT flag
// files managed by `forge upgrade` as stale. The regression these
// tests pin: pre-fix, `forge generate` warned that 7 actively-used
// files (cmd/main.go, .golangci.yml, .github/CODEOWNERS, etc.) were
// "stale" and would-be-deleted with --force-cleanup. The user's only
// workaround was hand-flipping `forked: true` in checksums.json, which
// silenced the warning but also broke `forge upgrade`'s ability to
// re-emit the file on the next version bump.
//
// The fix consults generator.UpgradeManagedPaths() — the union over
// every kind/binary combination of the upgrade-managed file list —
// and excludes those paths from both the deletion candidate list AND
// the inverse "tracked but missing" finding.
package cli

import (
	"path/filepath"
	"reflect"
	"sort"
	"testing"

	"github.com/reliant-labs/forge/internal/checksums"
	"github.com/reliant-labs/forge/internal/codegen"
	"github.com/reliant-labs/forge/internal/generator"
)

// TestCleanupStaleArtifacts_SkipsUpgradeManagedPaths is the headline
// pin: cmd/main.go, .golangci.yml, and .github/CODEOWNERS are all in
// the manifest AND none are written by this `forge generate` run AND
// no owner gate covers them — pre-fix the cleanup pass would have
// flagged them as stale. Post-fix they're excluded because
// UpgradeManagedPaths() lists them.
func TestCleanupStaleArtifacts_SkipsUpgradeManagedPaths(t *testing.T) {
	resetCleanupRunState(t)
	dir := t.TempDir()

	managed := []string{
		"cmd/main.go",
		"cmd/db.go",
		"cmd/version.go",
		".golangci.yml",
		".github/CODEOWNERS",
		".github/pull_request_template.md",
		".github/workflows/e2e.yml",
	}
	// Put each managed file on disk so the stat-skip branch isn't
	// what's saving us — we want to prove the UpgradeManagedPaths
	// exclusion fires before the stat check.
	files := map[string]checksums.FileChecksumEntry{}
	for _, p := range managed {
		full := filepath.Join(dir, p)
		mustMkdir(t, filepath.Dir(full))
		mustWrite(t, full, "// Code generated by forge. DO NOT EDIT.\npackage main\n")
		files[p] = checksums.FileChecksumEntry{Hash: "h", History: []string{"h"}, Tier: 1}
	}
	cs := &checksums.FileChecksums{Files: files}

	ctx := newCleanupCtx(dir, cs, []codegen.ServiceDef{{Name: "SomeService"}}, nil)
	removed, missing, err := cleanupStaleArtifacts(ctx)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	if len(removed) != 0 {
		t.Errorf("removed = %v, want empty (upgrade-managed paths must not be flagged stale)", removed)
	}
	if len(missing) != 0 {
		t.Errorf("missing = %v, want empty", missing)
	}
}

// TestUpgradeManagedPaths_CoversReportedFiles confirms the helper
// returns at least the 7 paths the cp-forge audit-cleanup agent
// reported as false-positive stale candidates. Pins the contract that
// these paths are recognized as upgrade-managed (and so cleanup
// excludes them). If a future refactor moves any of these to be
// `forge generate`-emitted, that emitter will also call
// WrittenThisRun for the path and the exclusion becomes redundant
// (safe) — but we still want explicit coverage here so the
// inventory doesn't silently shrink.
func TestUpgradeManagedPaths_CoversReportedFiles(t *testing.T) {
	got := generator.UpgradeManagedPaths()
	want := []string{
		"cmd/main.go",
		"cmd/db.go",
		"cmd/version.go",
		".golangci.yml",
		".github/CODEOWNERS",
		".github/pull_request_template.md",
		".github/workflows/e2e.yml",
		// Tier-1 cmd files that also live under upgrade ownership.
		"cmd/server.go",
		"cmd/otel.go",
		"buf.yaml",
		// Tier-2 scaffolds also upgrade-managed.
		"Taskfile.yml",
		"Dockerfile",
		"docker-compose.yml",
		".gitignore",
		"deploy/alloy-config.alloy",
	}
	var missing []string
	for _, p := range want {
		if !got[p] {
			missing = append(missing, p)
		}
	}
	if len(missing) > 0 {
		sort.Strings(missing)
		t.Errorf("UpgradeManagedPaths() missing expected entries: %v", missing)
	}
}

// TestCleanupStaleArtifacts_StillFlagsRealStaleAfterExclusion is the
// negative case: the exclusion is narrowly scoped to upgrade-managed
// paths. A real codegen-emitted file that the run didn't re-write
// must still surface as a candidate so the user can either
// --force-cleanup or rename their service back.
func TestCleanupStaleArtifacts_StillFlagsRealStaleAfterExclusion(t *testing.T) {
	resetCleanupRunState(t)
	dir := t.TempDir()

	// Mix one upgrade-managed path with one real stale codegen file.
	mustMkdir(t, filepath.Join(dir, "cmd"))
	mustWrite(t, filepath.Join(dir, "cmd", "main.go"), "// Code generated by forge. DO NOT EDIT.\npackage main\n")

	mustMkdir(t, filepath.Join(dir, "handlers", "renamed"))
	stale := filepath.Join(dir, "handlers", "renamed", "handlers_crud_gen.go")
	mustWrite(t, stale, "// Code generated by forge. DO NOT EDIT.\npackage renamed\n")

	cs := &checksums.FileChecksums{Files: map[string]checksums.FileChecksumEntry{
		"cmd/main.go":                          {Hash: "h", Tier: 1},
		"handlers/renamed/handlers_crud_gen.go": {Hash: "h", Tier: 1},
	}}

	ctx := newCleanupCtx(dir, cs, []codegen.ServiceDef{{Name: "KeptService"}}, nil)
	removed, _, err := cleanupStaleArtifacts(ctx)
	if err != nil {
		t.Fatalf("cleanup: %v", err)
	}
	// Only the renamed handler should be flagged stale; cmd/main.go
	// excluded by UpgradeManagedPaths.
	want := []string{stale}
	if !reflect.DeepEqual(removed, want) {
		t.Errorf("removed = %v, want %v", removed, want)
	}
}

// TestTrackedMissingFiles_SkipsUpgradeManagedPaths pins the same
// exclusion for the inverse finding. A CLI-kind project has no
// .github/workflows/e2e.yml on disk but it's tracked in checksums
// (the union path set is conservative). Pre-fix, trackedMissingFiles
// would have flagged it as "missing on disk" and the audit category
// would have lit up red. Post-fix, the exclusion keeps the audit
// honest.
func TestTrackedMissingFiles_SkipsUpgradeManagedPaths(t *testing.T) {
	dir := t.TempDir()
	// Nothing on disk; the manifest tracks an upgrade-managed path
	// + one real codegen path.
	mustMkdir(t, filepath.Join(dir, "handlers", "live"))
	livePath := filepath.Join(dir, "handlers", "live", "handlers_crud_gen.go")
	mustWrite(t, livePath, "// Code generated by forge. DO NOT EDIT.\npackage live\n")

	cs := &generator.FileChecksums{Files: map[string]generator.FileChecksumEntry{
		".github/workflows/e2e.yml":           {Hash: "h", Tier: 1}, // upgrade-managed
		"handlers/live/handlers_crud_gen.go":  {Hash: "h", Tier: 1}, // codegen — exists
		"handlers/gone/handlers_crud_gen.go":  {Hash: "h", Tier: 1}, // codegen — gone (real signal)
	}}

	missing := trackedMissingFiles(dir, cs)
	want := []string{"handlers/gone/handlers_crud_gen.go"}
	if !reflect.DeepEqual(missing, want) {
		t.Errorf("trackedMissingFiles = %v, want %v (upgrade-managed paths must be excluded)", missing, want)
	}
}
