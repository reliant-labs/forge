// newSkillsPipelineCtx builds a minimal pipelineContext for exercising
// stepAgentSkills against a temp project root.
func newSkillsPipelineCtx(t *testing.T) *pipelineContext {
	t.Helper()
	checksums.ResetSkipWrite()
	t.Cleanup(checksums.ResetSkipWrite)
	root := t.TempDir()
	return &pipelineContext{
		ProjectDir: root,
		AbsPath:    root,
		Checksums:  &checksums.FileChecksums{},
	}
}

// TestStepAgentSkillsWritesTier1 verifies the core delivery contract:
// every forge-shipped skill lands under .claude/skills/<flat>/SKILL.md,
// certified Tier-1 (verifying embedded forge:hash marker), with
// frontmatter at byte 0 and the generated-by banner after it.
func TestStepAgentSkillsWritesTier1(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	skills, err := listForgeShippedSkills()
	if err != nil {
		t.Fatalf("listForgeShippedSkills: %v", err)
	}
	if len(skills) == 0 {
		t.Fatal("no forge-shipped skills found")
	}

	for _, s := range skills {
		rel := agentSkillRelPath(s.Path)
		if s.Relevance == SkillRelevanceMigration {
			// One-time migration skills are not delivered to projects.
			if _, err := os.Stat(filepath.Join(ctx.AbsPath, rel)); !os.IsNotExist(err) {
				t.Errorf("migration skill %q was delivered to %s; want skipped", s.Path, rel)
			}
			if checksums.WrittenThisRun[filepath.ToSlash(rel)] {
				t.Errorf("migration skill %q marked written this run; want skipped", s.Path)
			}
			continue
		}
		body, err := os.ReadFile(filepath.Join(ctx.AbsPath, rel))
		if err != nil {
			t.Fatalf("skill %q not written to %s: %v", s.Path, rel, err)
		}
		if !bytes.HasPrefix(body, []byte("---\n")) {
			t.Errorf("%s: frontmatter must stay at byte 0", rel)
		}
		if !bytes.Contains(body, []byte("Code generated by forge")) {
			t.Errorf("%s: missing generated-by banner", rel)
		}
		// Tier-1 certification is embedded in the file now (the manifest
		// entry with Tier=1 is gone): the marker must verify.
		if got := checksums.Verify(body); got != checksums.Pristine {
			t.Errorf("%s: Verify = %v, want Pristine (embedded forge:hash marker)", rel, got)
		}
	}
}

// TestStepAgentSkillsBannerAfterFrontmatter pins the banner placement:
// the line directly after the frontmatter close must be the banner.
func TestStepAgentSkillsBannerAfterFrontmatter(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}
	body, err := os.ReadFile(filepath.Join(ctx.AbsPath, agentSkillRelPath("db")))
	if err != nil {
		t.Fatalf("read db skill: %v", err)
	}
	s := string(body)
	end := strings.Index(s[4:], "\n---")
	if end < 0 {
		t.Fatal("db skill has no frontmatter close")
	}
	after := s[4+end+1:]
	// Skip the closing "---" line itself.
	nl := strings.IndexByte(after, '\n')
	bannerLine := after[nl+1:]
	if !strings.HasPrefix(bannerLine, "<!-- Code generated by forge. DO NOT EDIT. -->") {
		t.Errorf("line after frontmatter close is not the banner: %q", bannerLine[:min(len(bannerLine), 80)])
	}
}

// TestStepAgentSkillsRefreshesStaleScaffoldCopy verifies the tier
// transition: a pre-existing skill file with NO certification marker
// (scaffold-era render) whose content differs from the fresh render is
// overwritten — NOT preserved as a precious user edit — and the file is
// certified after the run.
func TestStepAgentSkillsRefreshesStaleScaffoldCopy(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	rel := agentSkillRelPath("db")
	full := filepath.Join(ctx.AbsPath, rel)
	if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
		t.Fatal(err)
	}
	stale := []byte("---\nname: db\n---\n# Ancient scaffold-era guidance\n")
	if err := os.WriteFile(full, stale, 0o644); err != nil {
		t.Fatal(err)
	}

	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	got, err := os.ReadFile(full)
	if err != nil {
		t.Fatal(err)
	}
	if bytes.Contains(got, []byte("Ancient scaffold-era guidance")) {
		t.Error("stale scaffold copy was preserved; want overwrite")
	}
	if checksums.Verify(got) != checksums.Pristine {
		t.Errorf("refreshed skill is not certified (no verifying forge:hash marker)")
	}
}

// TestStepAgentSkillsStaleNoticePrinted pins the loud half of the
// scaffold-era transition: when an on-disk SKILL.md has NO marker and
// its body differs from the fresh render, the step announces the
// refresh. (Successor of the manifest-era Tier-2 → Tier-1 migration
// test: there are no tier entries to flip anymore — the unmarked-file
// overwrite IS the migration.)
func TestStepAgentSkillsStaleNoticePrinted(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	rel := agentSkillRelPath("testing")
	full := filepath.Join(ctx.AbsPath, rel)
	if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
		t.Fatal(err)
	}
	old := []byte("---\nname: testing\n---\nold tier-2 body\n")
	if err := os.WriteFile(full, old, 0o644); err != nil {
		t.Fatal(err)
	}

	out := captureStdout(t, func() {
		if err := stepAgentSkills(ctx); err != nil {
			t.Errorf("stepAgentSkills: %v", err)
		}
	})
	if !strings.Contains(out, "refreshed stale skill: "+rel) {
		t.Errorf("missing stale-skill refresh notice for %s; got:\n%s", rel, out)
	}

	got, err := os.ReadFile(full)
	if err != nil {
		t.Fatal(err)
	}
	if bytes.Contains(got, []byte("old tier-2 body")) {
		t.Error("Tier-2 era content survived; want fresh render")
	}
	if checksums.Verify(got) != checksums.Pristine {
		t.Errorf("migrated skill is not certified (no verifying forge:hash marker)")
	}
}

// TestStepAgentSkillsNeverTouchesUserSkillDirs pins the scoping contract:
// directories under .claude/skills/ that are not forge-shipped skills are
// left alone (no overwrite, no certification claimed, no GC — the user
// file carries neither marker nor banner).
func TestStepAgentSkillsNeverTouchesUserSkillDirs(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	userSkill := filepath.Join(ctx.AbsPath, ".claude", "skills", "my-own-skill", "SKILL.md")
	if err := os.MkdirAll(filepath.Dir(userSkill), 0o755); err != nil {
		t.Fatal(err)
	}
	userBody := []byte("---\nname: my-own-skill\n---\nmine\n")
	if err := os.WriteFile(userSkill, userBody, 0o644); err != nil {
		t.Fatal(err)
	}

	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	got, err := os.ReadFile(userSkill)
	if err != nil {
		t.Fatalf("user skill removed: %v", err)
	}
	if !bytes.Equal(got, userBody) {
		t.Error("user-created skill content was modified")
	}
	if checksums.WrittenThisRun[".claude/skills/my-own-skill/SKILL.md"] {
		t.Error("user-created skill must not be claimed as forge-written")
	}
}

// TestStepAgentSkillsIdempotent verifies a second run rewrites the same
// bytes (no churn). The embedded forge:hash marker is deterministic, so
// byte-stability still holds.
func TestStepAgentSkillsIdempotent(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatal(err)
	}
	rel := agentSkillRelPath("db")
	first, err := os.ReadFile(filepath.Join(ctx.AbsPath, rel))
	if err != nil {
		t.Fatal(err)
	}
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatal(err)
	}
	second, err := os.ReadFile(filepath.Join(ctx.AbsPath, rel))
	if err != nil {
		t.Fatal(err)
	}
	if !bytes.Equal(first, second) {
		t.Error("second run produced different bytes — skill render is not deterministic")
	}
}

// TestStepAgentSkillsGCsRetiredSkill verifies the transition contract:
// a skill forge previously delivered (here: a migration skill from
// before migration skills were excluded from delivery, or any renamed/
// retired skill) is removed from disk on the next regen. Both delivery
// vintages are covered: the legacy banner-only render and the current
// stamped (pristine-marker) render.
func TestStepAgentSkillsGCsRetiredSkill(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)

	// Legacy pre-marker delivery: frontmatter + forge banner, no marker.
	legacyRel := agentSkillRelPath("migrations/v0.x-to-contractkit")
	legacyFull := filepath.Join(ctx.AbsPath, legacyRel)
	if err := os.MkdirAll(filepath.Dir(legacyFull), 0o755); err != nil {
		t.Fatal(err)
	}
	legacyBody := []byte("---\nname: migrations/v0.x-to-contractkit\n---\n<!-- Code generated by forge v0.0.1. DO NOT EDIT -->\nold guidance\n")
	if err := os.WriteFile(legacyFull, legacyBody, 0o644); err != nil {
		t.Fatal(err)
	}

	// Current-era delivery: stamped pristine marker.
	stampedRel := agentSkillRelPath("api-handlers")
	stampedFull := filepath.Join(ctx.AbsPath, stampedRel)
	if err := os.MkdirAll(filepath.Dir(stampedFull), 0o755); err != nil {
		t.Fatal(err)
	}
	stampedBody, ok := checksums.Stamp(stampedRel,
		[]byte("---\nname: api-handlers\n---\n<!-- Code generated by forge. DO NOT EDIT. -->\nold guidance\n"))
	if !ok {
		t.Fatal("SKILL.md should be stampable")
	}
	if err := os.WriteFile(stampedFull, stampedBody, 0o644); err != nil {
		t.Fatal(err)
	}

	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	for _, full := range []string{legacyFull, stampedFull} {
		if _, err := os.Stat(full); !os.IsNotExist(err) {
			t.Errorf("retired skill %s still on disk; want removed", full)
		}
		if _, err := os.Stat(filepath.Dir(full)); !os.IsNotExist(err) {
			t.Errorf("retired skill dir %s still on disk; want pruned", filepath.Dir(full))
		}
	}
}

// TestStepAgentSkillsGCKeepsUserReplacedFile pins the certification
// backstop: a retired path whose content the user replaced keeps its
// file — both the no-banner-no-marker shape and the hand-edited
// (Modified marker) shape are the user's bytes now.
func TestStepAgentSkillsGCKeepsUserReplacedFile(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)

	// Fully replaced: no marker, no banner.
	replacedRel := agentSkillRelPath("migrations/v0.1-to-v0.2")
	replacedFull := filepath.Join(ctx.AbsPath, replacedRel)
	if err := os.MkdirAll(filepath.Dir(replacedFull), 0o755); err != nil {
		t.Fatal(err)
	}
	userBody := []byte("---\nname: mine\n---\nhand-written replacement, no banner\n")
	if err := os.WriteFile(replacedFull, userBody, 0o644); err != nil {
		t.Fatal(err)
	}

	// Hand-edited after delivery: marker present but stale (Modified).
	editedRel := agentSkillRelPath("migrations/v0.x-to-testkit")
	editedFull := filepath.Join(ctx.AbsPath, editedRel)
	if err := os.MkdirAll(filepath.Dir(editedFull), 0o755); err != nil {
		t.Fatal(err)
	}
	editedBody, ok := checksums.StampWithValue(editedRel,
		[]byte("---\nname: testkit\n---\n<!-- Code generated by forge. DO NOT EDIT. -->\nuser-tuned guidance\n"),
		checksums.BodyHash([]byte("what forge originally wrote")))
	if !ok {
		t.Fatal("SKILL.md should be stampable")
	}
	if err := os.WriteFile(editedFull, editedBody, 0o644); err != nil {
		t.Fatal(err)
	}

	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	got, err := os.ReadFile(replacedFull)
	if err != nil {
		t.Fatalf("user-replaced file was removed: %v", err)
	}
	if !bytes.Equal(got, userBody) {
		t.Error("user-replaced file content changed")
	}
	got, err = os.ReadFile(editedFull)
	if err != nil {
		t.Fatalf("hand-edited (Modified marker) retired skill was removed: %v", err)
	}
	if !bytes.Equal(got, editedBody) {
		t.Error("hand-edited retired skill content changed")
	}
}

// TestStepAgentSkillsGCToleratesEmptyRetiredDir verifies the GC walks
// disk state only (there is no manifest of delivered skills anymore): a
// leftover per-skill directory with no SKILL.md is simply skipped.
// (Successor of the manifest-era "tracked entry whose file is already
// gone loses its entry" test — with no manifest, gone IS gone.)
func TestStepAgentSkillsGCToleratesEmptyRetiredDir(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	emptyDir := filepath.Join(ctx.AbsPath, ".claude", "skills", "retired-empty")
	if err := os.MkdirAll(emptyDir, 0o755); err != nil {
		t.Fatal(err)
	}
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}
	// The empty dir is left alone (nothing certified inside it).
	if _, err := os.Stat(emptyDir); err != nil {
		t.Errorf("empty user dir removed: %v", err)
	}
}
