// newSkillsPipelineCtx builds a minimal pipelineContext for exercising
// stepAgentSkills against a temp project root.
func newSkillsPipelineCtx(t *testing.T) *pipelineContext {
	t.Helper()
	root := t.TempDir()
	return &pipelineContext{
		ProjectDir: root,
		AbsPath:    root,
		Checksums:  &checksums.FileChecksums{Files: map[string]checksums.FileChecksumEntry{}},
	}
}

// TestStepAgentSkillsWritesTier1 verifies the core delivery contract:
// every forge-shipped skill lands under .claude/skills/<flat>/SKILL.md,
// tracked as Tier-1, with frontmatter at byte 0 and the generated-by
// banner immediately after it.
func TestStepAgentSkillsWritesTier1(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	skills, err := listForgeShippedSkills()
	if err != nil {
		t.Fatalf("listForgeShippedSkills: %v", err)
	}
	if len(skills) == 0 {
		t.Fatal("no forge-shipped skills found")
	}

	for _, s := range skills {
		rel := agentSkillRelPath(s.Path)
		body, err := os.ReadFile(filepath.Join(ctx.AbsPath, rel))
		if err != nil {
			t.Fatalf("skill %q not written to %s: %v", s.Path, rel, err)
		}
		if !bytes.HasPrefix(body, []byte("---\n")) {
			t.Errorf("%s: frontmatter must stay at byte 0", rel)
		}
		if !bytes.Contains(body, []byte("Code generated by forge")) {
			t.Errorf("%s: missing generated-by banner", rel)
		}
		entry, ok := ctx.Checksums.Files[rel]
		if !ok {
			t.Errorf("%s: no checksum entry recorded", rel)
		} else if entry.Tier != 1 {
			t.Errorf("%s: Tier = %d, want 1", rel, entry.Tier)
		}
	}
}

// TestStepAgentSkillsBannerAfterFrontmatter pins the banner placement:
// the line directly after the frontmatter close must be the banner.
func TestStepAgentSkillsBannerAfterFrontmatter(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}
	body, err := os.ReadFile(filepath.Join(ctx.AbsPath, agentSkillRelPath("db")))
	if err != nil {
		t.Fatalf("read db skill: %v", err)
	}
	s := string(body)
	end := strings.Index(s[4:], "\n---")
	if end < 0 {
		t.Fatal("db skill has no frontmatter close")
	}
	after := s[4+end+1:]
	// Skip the closing "---" line itself.
	nl := strings.IndexByte(after, '\n')
	bannerLine := after[nl+1:]
	if !strings.HasPrefix(bannerLine, "<!-- Code generated by forge ") {
		t.Errorf("line after frontmatter close is not the banner: %q", bannerLine[:min(len(bannerLine), 80)])
	}
}

// TestStepAgentSkillsRefreshesStaleScaffoldCopy verifies the tier
// transition: a pre-existing, untracked (scaffold-era) skill file whose
// content differs from the fresh render is overwritten — NOT preserved
// as a precious user edit.
func TestStepAgentSkillsRefreshesStaleScaffoldCopy(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	rel := agentSkillRelPath("db")
	full := filepath.Join(ctx.AbsPath, rel)
	if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
		t.Fatal(err)
	}
	stale := []byte("---\nname: db\n---\n# Ancient scaffold-era guidance\n")
	if err := os.WriteFile(full, stale, 0o644); err != nil {
		t.Fatal(err)
	}

	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	got, err := os.ReadFile(full)
	if err != nil {
		t.Fatal(err)
	}
	if bytes.Contains(got, []byte("Ancient scaffold-era guidance")) {
		t.Error("stale scaffold copy was preserved; want overwrite")
	}
	if entry := ctx.Checksums.Files[rel]; entry.Tier != 1 {
		t.Errorf("Tier = %d after migration, want 1", entry.Tier)
	}
}

// TestStepAgentSkillsMigratesTier2Entry verifies that an entry previously
// tracked as Tier-2 (scaffold-once) migrates to Tier-1 and gets the fresh
// render even though its on-disk content matches the old recorded hash.
func TestStepAgentSkillsMigratesTier2Entry(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	rel := agentSkillRelPath("testing")
	full := filepath.Join(ctx.AbsPath, rel)
	if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
		t.Fatal(err)
	}
	old := []byte("---\nname: testing\n---\nold tier-2 body\n")
	if err := os.WriteFile(full, old, 0o644); err != nil {
		t.Fatal(err)
	}
	ctx.Checksums.RecordFile(rel, old)
	entry := ctx.Checksums.Files[rel]
	entry.Tier = 2
	ctx.Checksums.Files[rel] = entry

	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	got, err := os.ReadFile(full)
	if err != nil {
		t.Fatal(err)
	}
	if bytes.Contains(got, []byte("old tier-2 body")) {
		t.Error("Tier-2 era content survived; want fresh render")
	}
	if e := ctx.Checksums.Files[rel]; e.Tier != 1 {
		t.Errorf("Tier = %d after migration, want 1", e.Tier)
	}
}

// TestStepAgentSkillsNeverTouchesUserSkillDirs pins the scoping contract:
// directories under .claude/skills/ that are not forge-shipped skills are
// left alone.
func TestStepAgentSkillsNeverTouchesUserSkillDirs(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	userSkill := filepath.Join(ctx.AbsPath, ".claude", "skills", "my-own-skill", "SKILL.md")
	if err := os.MkdirAll(filepath.Dir(userSkill), 0o755); err != nil {
		t.Fatal(err)
	}
	userBody := []byte("---\nname: my-own-skill\n---\nmine\n")
	if err := os.WriteFile(userSkill, userBody, 0o644); err != nil {
		t.Fatal(err)
	}

	if err := stepAgentSkills(ctx); err != nil {
		t.Fatalf("stepAgentSkills: %v", err)
	}

	got, err := os.ReadFile(userSkill)
	if err != nil {
		t.Fatalf("user skill removed: %v", err)
	}
	if !bytes.Equal(got, userBody) {
		t.Error("user-created skill content was modified")
	}
	if _, tracked := ctx.Checksums.Files[filepath.Join(".claude", "skills", "my-own-skill", "SKILL.md")]; tracked {
		t.Error("user-created skill must not be checksum-tracked")
	}
}

// TestStepAgentSkillsIdempotent verifies a second run rewrites the same
// bytes (no churn).
func TestStepAgentSkillsIdempotent(t *testing.T) {
	ctx := newSkillsPipelineCtx(t)
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatal(err)
	}
	rel := agentSkillRelPath("db")
	first, err := os.ReadFile(filepath.Join(ctx.AbsPath, rel))
	if err != nil {
		t.Fatal(err)
	}
	if err := stepAgentSkills(ctx); err != nil {
		t.Fatal(err)
	}
	second, err := os.ReadFile(filepath.Join(ctx.AbsPath, rel))
	if err != nil {
		t.Fatal(err)
	}
	if !bytes.Equal(first, second) {
		t.Error("second run produced different bytes — skill render is not deterministic")
	}
}
