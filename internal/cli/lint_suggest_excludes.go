// File: internal/cli/lint_suggest_excludes.go
//
// `forge lint --suggest-excludes` walks `internal/` looking for packages that
// will trip the require-contract analyzer (exported methods on a type, no
// contract.go) but where the package is, by inspection, a legitimate
// no-contract case — analyzer-style packages, embed-only wrappers, packages
// whose only exported surface is registered with an external runtime
// (cobra commands, http handlers, prometheus collectors, etc.).
//
// The command emits a YAML snippet the user can paste into
// `forge.yaml`'s `contracts.exclude:` block. It does NOT auto-add the
// entries: contracts.exclude is the user's policy file and silent edits
// would mask real architectural decisions. The bulk-port pain point is
// the discovery loop ("run lint, see 30 errors, copy 30 lines"); this
// command compresses that loop into a single invocation that prints the
// 30 lines for review.
//
// Heuristics (a package is suggested when ANY apply, AND the package has
// no contract.go AND has at least one exported method on a struct):
//
//   1. Filename heuristic: the package contains a file matching
//      `analyzer.go`, `*_analyzer.go`, `*lint*.go`, or `*linter*.go` —
//      these are golang.org/x/tools-shape analyzers, not forge services.
//
//   2. Embed heuristic: every exported type-with-methods in the package
//      embeds a struct from a third-party package (e.g. cobra.Command,
//      pflag.FlagSet). The package is an embed wrapper, not a service.
//
//   3. Generated-only heuristic: every .go file in the package is a
//      `_gen.go` or carries the canonical `// Code generated by` header.
//      Generated packages have no contract.go on purpose.
//
//   4. Convention heuristic: the package path matches a known
//      convention-only directory (`internal/db/`, `internal/auth/`,
//      `internal/metrics/`, `internal/middleware/`, `internal/observability/`,
//      `internal/planlimits/`, `internal/checksums/`, `internal/buildinfo/`,
//      `internal/naming/`). These are forge convention packages —
//      utilities the bootstrap template doesn't bind to.
//
// The output is deterministic — package paths sort alphabetically and
// each entry carries the heuristic that flagged it as a comment.

package cli

import (
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/reliant-labs/forge/internal/config"
)

// excludeSuggestion describes a package the heuristic flagged as a
// contracts.exclude candidate.
type excludeSuggestion struct {
	// RelPath is the module-relative slash-form path
	// (e.g. "internal/db/orm"). Matches the format `contracts.exclude`
	// expects.
	RelPath string
	// Reason is a one-line summary of which heuristic fired. Surfaces in
	// the YAML output as a `# reason: ...` comment so the user can
	// triage suggestions before pasting.
	Reason string
}

// runSuggestExcludes is the command entry point. It loads the project's
// existing contracts.exclude (so already-excluded packages aren't
// re-suggested) and prints the YAML snippet. Returns nil even when zero
// suggestions surface — the command is informational, not gating.
func runSuggestExcludes(cfg *config.ProjectConfig) error {
	root, err := os.Getwd()
	if err != nil {
		return fmt.Errorf("get working directory: %w", err)
	}

	existing := map[string]bool{}
	if cfg != nil {
		for _, e := range cfg.Contracts.Exclude {
			existing[filepath.ToSlash(e)] = true
		}
	}

	suggestions, err := scanForExcludeCandidates(root, existing)
	if err != nil {
		return fmt.Errorf("scan internal/ for exclude candidates: %w", err)
	}

	if len(suggestions) == 0 {
		fmt.Println("# forge lint --suggest-excludes: no candidates found.")
		fmt.Println("# Either every internal package has a contract.go, or every package")
		fmt.Println("# missing one is already listed under contracts.exclude in forge.yaml.")
		return nil
	}

	fmt.Println("# forge lint --suggest-excludes: candidate packages for contracts.exclude.")
	fmt.Println("# Review and paste the entries you want into forge.yaml. Nothing is auto-added.")
	fmt.Println("# Heuristics fired are listed inline; multiple may apply per package.")
	fmt.Println()
	fmt.Println("contracts:")
	fmt.Println("  exclude:")
	for _, s := range suggestions {
		fmt.Printf("    - %s  # %s\n", s.RelPath, s.Reason)
	}
	return nil
}

// scanForExcludeCandidates walks rootDir/internal/ and returns suggestions
// for packages that match the no-contract heuristics. Already-excluded
// paths (in `existing`) are filtered out.
func scanForExcludeCandidates(rootDir string, existing map[string]bool) ([]excludeSuggestion, error) {
	internalDir := filepath.Join(rootDir, "internal")
	if _, err := os.Stat(internalDir); os.IsNotExist(err) {
		return nil, nil
	}

	var out []excludeSuggestion
	walkErr := filepath.WalkDir(internalDir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if !d.IsDir() {
			return nil
		}
		if d.Name() == "testdata" {
			return filepath.SkipDir
		}
		rel, relErr := filepath.Rel(rootDir, path)
		if relErr != nil {
			return relErr
		}
		relSlash := filepath.ToSlash(rel)
		if existing[relSlash] {
			// Already excluded — don't re-suggest.
			return nil
		}
		// Skip the internal/ root itself (no Go files there).
		if relSlash == "internal" {
			return nil
		}

		ent, hasContract, hasExportedMethods, scanErr := scanPackageFiles(path)
		if scanErr != nil {
			// Treat per-package failures as best-effort — don't blank the report.
			return nil
		}
		if hasContract {
			return nil
		}
		if !hasExportedMethods {
			// No exported methods — the require-contract analyzer wouldn't
			// flag this package, so there's nothing to suggest excluding.
			return nil
		}

		reason := classifyExcludeCandidate(relSlash, ent)
		if reason == "" {
			// Has exported methods, no contract.go, but no heuristic fired
			// — leave it alone. The user should add a contract.go (or
			// hand-add to exclude after deciding).
			return nil
		}
		out = append(out, excludeSuggestion{RelPath: relSlash, Reason: reason})
		return nil
	})
	if walkErr != nil {
		return nil, walkErr
	}

	sort.SliceStable(out, func(i, j int) bool {
		return out[i].RelPath < out[j].RelPath
	})
	return out, nil
}

// pkgEntries holds the per-package scan summary used by classifyExcludeCandidate.
type pkgEntries struct {
	// FileNames lists every non-test .go file in the package (basename
	// only). Used by the filename heuristic.
	FileNames []string
	// EmbeddedThirdParty is true when every type that exposes an exported
	// method embeds a non-stdlib type (e.g. cobra.Command). Heuristic 2.
	EmbeddedThirdParty bool
	// AllGenerated is true when every .go file in the package starts with
	// the canonical `// Code generated by` header or has the `_gen.go`
	// suffix. Heuristic 3.
	AllGenerated bool
}

// scanPackageFiles inspects a single directory's .go files. It returns:
//   - ent: aggregated entry counts/flags used by classifyExcludeCandidate
//   - hasContract: whether contract.go is present
//   - hasExportedMethods: whether any struct has an exported method (the
//     same trigger used by the require-contract analyzer)
//
// Test files (_test.go) are ignored — they're not analyzed by
// require-contract and their methods don't count toward the
// "needs a contract" determination.
func scanPackageFiles(pkgDir string) (pkgEntries, bool, bool, error) {
	entries, err := os.ReadDir(pkgDir)
	if err != nil {
		return pkgEntries{}, false, false, err
	}

	var ent pkgEntries
	hasContract := false
	hasExportedMethods := false

	// Track per-type embeds for the embed heuristic. The map key is the
	// receiver type name; the value is true if the type embeds a
	// third-party (non-stdlib) struct. We also track every type that has
	// an exported method so we can require ALL such types to be embed-y.
	embedFlags := map[string]bool{}    // typeName -> embeds third-party
	exportedOn := map[string]bool{}    // typeName -> has exported method
	allGoFilesGenerated := true
	hasAnyGoFile := false

	fset := token.NewFileSet()
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		name := e.Name()
		if !strings.HasSuffix(name, ".go") {
			continue
		}
		hasAnyGoFile = true
		if name == "contract.go" {
			hasContract = true
		}
		if strings.HasSuffix(name, "_test.go") {
			continue
		}
		ent.FileNames = append(ent.FileNames, name)

		path := filepath.Join(pkgDir, name)
		isGenerated := strings.HasSuffix(name, "_gen.go")
		// Read the first ~512 bytes for the generated-code header check.
		// Avoids parsing the whole file when we're just looking for the marker.
		if !isGenerated {
			head, _ := readFileHead(path, 512)
			if strings.Contains(string(head), "// Code generated by") {
				isGenerated = true
			}
		}
		if !isGenerated {
			allGoFilesGenerated = false
		}

		file, parseErr := parser.ParseFile(fset, path, nil, parser.SkipObjectResolution)
		if parseErr != nil {
			// Skip un-parseable files for the analysis pass; the file is
			// still counted toward FileNames.
			continue
		}
		// Build a third-party-import set so we can classify each struct
		// embed as stdlib vs third-party. Stdlib imports don't have a
		// "/" or domain dot in the early segments, but this is fragile;
		// we approximate "third-party" as any import whose path contains
		// a "." (covers github.com/..., k8s.io/..., google.golang.org/...).
		thirdPartyImports := map[string]bool{}
		for _, imp := range file.Imports {
			pathLit := strings.Trim(imp.Path.Value, `"`)
			if strings.Contains(pathLit, ".") {
				// Last-segment alias is the import name unless aliased.
				ident := filepath.Base(pathLit)
				if imp.Name != nil && imp.Name.Name != "_" && imp.Name.Name != "." {
					ident = imp.Name.Name
				}
				thirdPartyImports[ident] = true
			}
		}

		// Classify struct types: does any embedded field reference a
		// third-party package?
		for _, decl := range file.Decls {
			switch d := decl.(type) {
			case *ast.GenDecl:
				if d.Tok != token.TYPE {
					continue
				}
				for _, spec := range d.Specs {
					ts, ok := spec.(*ast.TypeSpec)
					if !ok {
						continue
					}
					st, ok := ts.Type.(*ast.StructType)
					if !ok || st.Fields == nil {
						continue
					}
					for _, f := range st.Fields.List {
						// Embedded field — no Names, just a Type.
						if len(f.Names) != 0 {
							continue
						}
						// Type is *ast.Ident (same package), *ast.SelectorExpr (pkg.Type),
						// or *ast.StarExpr wrapping one of those.
						sel := unwrapSelector(f.Type)
						if sel == nil {
							continue
						}
						if pkgIdent, ok := sel.X.(*ast.Ident); ok {
							if thirdPartyImports[pkgIdent.Name] {
								embedFlags[ts.Name.Name] = true
							}
						}
					}
				}
			case *ast.FuncDecl:
				if d.Recv == nil || len(d.Recv.List) == 0 {
					continue
				}
				if !d.Name.IsExported() {
					continue
				}
				// Receiver may be `T` or `*T`.
				recvType := d.Recv.List[0].Type
				if star, ok := recvType.(*ast.StarExpr); ok {
					recvType = star.X
				}
				if id, ok := recvType.(*ast.Ident); ok {
					hasExportedMethods = true
					exportedOn[id.Name] = true
				}
			}
		}
	}
	if !hasAnyGoFile {
		allGoFilesGenerated = false
	}
	ent.AllGenerated = allGoFilesGenerated

	// Embed heuristic: every type that exposes an exported method also
	// embeds a third-party struct.
	if len(exportedOn) > 0 {
		allEmbed := true
		for tn := range exportedOn {
			if !embedFlags[tn] {
				allEmbed = false
				break
			}
		}
		ent.EmbeddedThirdParty = allEmbed
	}

	return ent, hasContract, hasExportedMethods, nil
}

// unwrapSelector returns the underlying *ast.SelectorExpr from an embed
// field type, peeling off one level of pointer if present. Returns nil
// when the field is a same-package embed (*ast.Ident alone) which doesn't
// affect the third-party heuristic.
func unwrapSelector(expr ast.Expr) *ast.SelectorExpr {
	if star, ok := expr.(*ast.StarExpr); ok {
		expr = star.X
	}
	if sel, ok := expr.(*ast.SelectorExpr); ok {
		return sel
	}
	return nil
}

// readFileHead reads up to n bytes from the start of path. Used by the
// generated-code header heuristic so the whole file doesn't have to be
// parsed twice.
func readFileHead(path string, n int) ([]byte, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer func() { _ = f.Close() }()
	buf := make([]byte, n)
	read, err := f.Read(buf)
	if err != nil && read == 0 {
		return nil, err
	}
	return buf[:read], nil
}

// classifyExcludeCandidate returns the heuristic-summary string when one
// of the heuristics fires, else "".
//
// relSlash is the module-relative slash-form path (e.g. "internal/db/orm");
// ent is the per-package summary from scanPackageFiles.
func classifyExcludeCandidate(relSlash string, ent pkgEntries) string {
	// Heuristic 1: filename pattern.
	for _, fn := range ent.FileNames {
		lower := strings.ToLower(fn)
		switch {
		case lower == "analyzer.go", strings.HasSuffix(lower, "_analyzer.go"):
			return "analyzer-style package (filename: " + fn + ")"
		case strings.Contains(lower, "lint"), strings.Contains(lower, "linter"):
			return "linter-style package (filename: " + fn + ")"
		}
	}
	// Heuristic 2: every exported-method type embeds a third-party struct.
	if ent.EmbeddedThirdParty {
		return "embed-only wrapper (every exported-method type embeds a third-party struct)"
	}
	// Heuristic 3: every file is generated.
	if ent.AllGenerated {
		return "generated-only package (every .go file is _gen.go or carries // Code generated by header)"
	}
	// Heuristic 4: convention-only directory.
	for _, prefix := range conventionPrefixes() {
		if relSlash == prefix || strings.HasPrefix(relSlash+"/", prefix+"/") {
			return "convention-only directory (forge utility package: " + prefix + ")"
		}
	}
	return ""
}

// conventionPrefixes returns the slash-form module-relative prefixes the
// classify pass treats as forge convention utilities. Kept as a function
// rather than a global so tests can hook a fake list (currently only the
// canonical list is used).
func conventionPrefixes() []string {
	return []string{
		"internal/auth",
		"internal/buildinfo",
		"internal/checksums",
		"internal/db",
		"internal/metrics",
		"internal/middleware",
		"internal/naming",
		"internal/observability",
		"internal/planlimits",
	}
}
