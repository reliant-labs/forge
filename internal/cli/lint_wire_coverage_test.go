import (
	"strings"
	"testing"
)

// TestScanWireGen_FindsTODOs asserts that the wire-coverage scanner
// reports one finding per `// TODO: wire <field>` marker emitted by
// wire_gen, attributes each to the enclosing wire*Deps function, and
// returns them in stable file/line order.
func TestScanWireGen_FindsTODOs(t *testing.T) {
	t.Parallel()
	src := `// Code generated by forge. DO NOT EDIT.
package app

import (
	"log/slog"

	"example.com/proj/handlers/billing"
	"example.com/proj/handlers/user"
)

var _ = slog.LevelInfo

// wireBillingDeps assembles the full billing.Deps.
func wireBillingDeps(app *App) billing.Deps {
	return billing.Deps{
		Stripe: nil, // TODO: wire Stripe
	}
}

// wireUserDeps assembles the full user.Deps.
func wireUserDeps(app *App) user.Deps {
	return user.Deps{
		Repo:  nil, // TODO: wire Repo
		Cache: nil, // TODO: wire Cache
	}
}
`
	findings, err := scanWireGen(strings.NewReader(src), "pkg/app/wire_gen.go", "")
	if err != nil {
		t.Fatalf("scanWireGen: %v", err)
	}
	if len(findings) != 3 {
		t.Fatalf("expected 3 findings, got %d: %+v", len(findings), findings)
	}

	want := []struct {
		field, function string
	}{
		{"Stripe", "wireBillingDeps"},
		{"Repo", "wireUserDeps"},
		{"Cache", "wireUserDeps"},
	}
	for i, w := range want {
		if findings[i].Field != w.field {
			t.Errorf("findings[%d].Field = %q, want %q", i, findings[i].Field, w.field)
		}
		if findings[i].Function != w.function {
			t.Errorf("findings[%d].Function = %q, want %q", i, findings[i].Function, w.function)
		}
	}
}

// TestScanWireGen_NoTODOs asserts that a wire_gen.go without any
// `// TODO: wire X` markers returns zero findings — the case where
// every Deps field resolved cleanly to a producer.
func TestScanWireGen_NoTODOs(t *testing.T) {
	t.Parallel()
	src := `package app

func wireAPIDeps(app *App) struct{} {
	return struct{}{}
}
`
	findings, err := scanWireGen(strings.NewReader(src), "pkg/app/wire_gen.go", "")
	if err != nil {
		t.Fatalf("scanWireGen: %v", err)
	}
	if len(findings) != 0 {
		t.Fatalf("expected 0 findings, got %d: %+v", len(findings), findings)
	}
}

// TestFormatWireCoverage_SummaryShape asserts the printed summary
// line shape — `<N> unresolved deps across <M> components: c1, c2`
// when findings have function attributions, and a fallback shape
// without `across` when no function could be attributed.
func TestFormatWireCoverage_SummaryShape(t *testing.T) {
	t.Parallel()
	var buf strings.Builder
	formatWireCoverage(&buf, []wireCoverageFinding{
		{File: "pkg/app/wire_gen.go", Line: 10, Field: "Stripe", Function: "wireBillingDeps"},
		{File: "pkg/app/wire_gen.go", Line: 20, Field: "Repo", Function: "wireUserDeps"},
		{File: "pkg/app/wire_gen.go", Line: 21, Field: "Cache", Function: "wireUserDeps"},
	})
	out := buf.String()
	if !strings.Contains(out, "3 unresolved Deps field(s) across 2 component(s):") {
		t.Errorf("expected 3-across-2 summary, got:\n%s", out)
	}
	if !strings.Contains(out, "wireBillingDeps") || !strings.Contains(out, "wireUserDeps") {
		t.Errorf("summary should list both component functions, got:\n%s", out)
	}
}
