func TestGenerateConfigLoader_BasicFields(t *testing.T) {
	targetDir := t.TempDir()

	messages := []ConfigMessage{
		{
			Name: "AppConfig",
			Fields: []ConfigField{
				{
					Name:         "port",
					GoName:       "Port",
					GoType:       "int32",
					ProtoType:    "int32",
					EnvVar:       "PORT",
					Flag:         "port",
					DefaultValue: "8080",
					Description:  "HTTP server port",
				},
				{
					Name:         "log_level",
					GoName:       "LogLevel",
					GoType:       "string",
					ProtoType:    "string",
					EnvVar:       "LOG_LEVEL",
					Flag:         "log-level",
					DefaultValue: "info",
					Description:  "Log level",
				},
				{
					Name:        "database_url",
					GoName:      "DatabaseUrl",
					GoType:      "string",
					ProtoType:   "string",
					EnvVar:      "DATABASE_URL",
					Flag:        "database-url",
					Required:    true,
					Description: "PostgreSQL connection string",
				},
			},
		},
	}

	if err := GenerateConfigLoader(messages, targetDir, nil); err != nil {
		t.Fatalf("GenerateConfigLoader() error = %v", err)
	}

	data, err := os.ReadFile(filepath.Join(targetDir, "pkg", "config", "config.go"))
	if err != nil {
		t.Fatalf("ReadFile() error = %v", err)
	}

	content := string(data)

	// Must contain generated file header
	if !strings.Contains(content, "Code generated by forge. DO NOT EDIT.") {
		t.Error("config.go should contain generated file header")
	}

	// Must contain Config struct with all fields
	if !strings.Contains(content, "type Config struct") {
		t.Error("config.go should contain Config struct")
	}
	if !strings.Contains(content, "Port int32") {
		t.Error("config.go should contain Port int32 field")
	}
	if !strings.Contains(content, "LogLevel string") {
		t.Error("config.go should contain LogLevel string field")
	}
	if !strings.Contains(content, "DatabaseUrl string") {
		t.Error("config.go should contain DatabaseUrl string field")
	}

	// Must contain RegisterFlags
	if !strings.Contains(content, "func RegisterFlags(cmd *cobra.Command)") {
		t.Error("config.go should contain RegisterFlags function")
	}
	if !strings.Contains(content, `cmd.Flags().Int32("port"`) {
		t.Error("config.go should register port as Int32 flag")
	}
	if !strings.Contains(content, `cmd.Flags().String("log-level"`) {
		t.Error("config.go should register log-level as String flag")
	}
	if !strings.Contains(content, `cmd.Flags().String("database-url"`) {
		t.Error("config.go should register database-url as String flag")
	}

	// Must contain Load function
	if !strings.Contains(content, "func Load(cmd *cobra.Command) (*Config, error)") {
		t.Error("config.go should contain Load function")
	}

	// Must contain env var reading via LookupEnv
	if !strings.Contains(content, `os.LookupEnv("PORT")`) {
		t.Error("config.go should read PORT env var via LookupEnv")
	}
	if !strings.Contains(content, `os.LookupEnv("LOG_LEVEL")`) {
		t.Error("config.go should read LOG_LEVEL env var via LookupEnv")
	}

	// Must contain required validation for database_url
	if !strings.Contains(content, "required config field DatabaseUrl") {
		t.Error("config.go should validate required field DatabaseUrl")
	}

	// Must contain strconv import (for int32 parsing)
	if !strings.Contains(content, `"strconv"`) {
		t.Error("config.go should import strconv for int32 parsing")
	}

	// Must contain default value in else branch (not zero-value check)
	if !strings.Contains(content, "cfg.Port = 8080") {
		t.Error("config.go should set default value for Port")
	}
	if !strings.Contains(content, `cfg.LogLevel = "info"`) {
		t.Error("config.go should set default value for LogLevel")
	}

	// Must NOT contain zero-value checks (the old buggy pattern)
	if strings.Contains(content, "if cfg.Port == 0") {
		t.Error("config.go should NOT use zero-value check for Port default")
	}
	if strings.Contains(content, `if cfg.LogLevel == ""`) {
		t.Error("config.go should NOT use zero-value check for LogLevel default")
	}

	// Must use cmd.Flags().Changed() instead of bare cmd.Flags().GetXxx()
	if !strings.Contains(content, `cmd.Flags().Changed("port")`) {
		t.Error("config.go should check cmd.Flags().Changed for port")
	}
	if !strings.Contains(content, `cmd.Flags().Changed("log-level")`) {
		t.Error("config.go should check cmd.Flags().Changed for log-level")
	}
}

