// TestGenerateConfigLoader_NoFlagSkipsRegistration verifies that a config
// field with no `flag:` annotation (typically a sensitive secret) does NOT
// emit a `cmd.Flags().XXX("", ...)` registration. pflag panics on empty
// flag names ("redefined" error), so emitting them is a P0 codegen bug.
//
// The field MUST still appear in the Config struct and the env-var loader;
// only the CLI flag plumbing is skipped.
func TestGenerateConfigLoader_NoFlagSkipsRegistration(t *testing.T) {
	targetDir := t.TempDir()

	messages := []ConfigMessage{
		{
			Name: "AppConfig",
			Fields: []ConfigField{
				{
					Name:        "database_password",
					GoName:      "DatabasePassword",
					GoType:      "string",
					ProtoType:   "string",
					EnvVar:      "DB_PASSWORD",
					// Flag is intentionally empty — sensitive, env-only.
					Description: "Database password (sourced from db-credentials Secret).",
					Sensitive:   true,
				},
				{
					Name:         "log_level",
					GoName:       "LogLevel",
					GoType:       "string",
					ProtoType:    "string",
					EnvVar:       "LOG_LEVEL",
					Flag:         "log-level",
					DefaultValue: "info",
					Description:  "Log level",
				},
			},
		},
	}

	if err := GenerateConfigLoader(messages, targetDir, nil); err != nil {
		t.Fatalf("GenerateConfigLoader() error = %v", err)
	}

	data, err := os.ReadFile(filepath.Join(targetDir, "pkg", "config", "config.go"))
	if err != nil {
		t.Fatalf("ReadFile() error = %v", err)
	}
	content := string(data)

	// MUST NOT register an empty-named flag — pflag panics on this.
	if strings.Contains(content, `cmd.Flags().String(""`) {
		t.Error("config.go must not register a flag with an empty name")
	}
	if strings.Contains(content, `cmd.Flags().Changed("")`) {
		t.Error("config.go must not query Changed on an empty flag name")
	}
	if strings.Contains(content, `cmd.Flags().GetString("")`) {
		t.Error("config.go must not GetString on an empty flag name")
	}

	// The flagged field MUST still register normally.
	if !strings.Contains(content, `cmd.Flags().String("log-level"`) {
		t.Error("config.go should still register the flagged field")
	}

	// Sensitive field MUST still appear in the Config struct.
	if !strings.Contains(content, "DatabasePassword string") {
		t.Error("config.go should still declare the sensitive field on Config")
	}

	// Sensitive field MUST still be loadable from its env var.
	if !strings.Contains(content, `os.LookupEnv("DB_PASSWORD")`) {
		t.Error("config.go should still read the sensitive field from env")
	}
}

func TestGenerateConfigLoader_PortRangeCheck(t *testing.T) {
	targetDir := t.TempDir()

	messages := []ConfigMessage{
		{
			Name: "AppConfig",
			Fields: []ConfigField{
				{
					Name:         "port",
					GoName:       "Port",
					GoType:       "int32",
					ProtoType:    "int32",
					EnvVar:       "PORT",
					Flag:         "port",
					DefaultValue: "8080",
					Description:  "HTTP server port",
				},
			},
		},
	}

	if err := GenerateConfigLoader(messages, targetDir, nil); err != nil {
		t.Fatalf("GenerateConfigLoader() error = %v", err)
	}

	data, err := os.ReadFile(filepath.Join(targetDir, "pkg", "config", "config.go"))
	if err != nil {
		t.Fatalf("ReadFile() error = %v", err)
	}

	content := string(data)

	// Port must be validated via ParseUint with bitSize=16 (1-65535).
	if !strings.Contains(content, `strconv.ParseUint(v, 10, 16)`) {
		t.Error("config.go should validate PORT with ParseUint bitSize=16")
	}

	// Port parsing must NOT use the unchecked int32 path (which would allow
	// values > 65535 to silently wrap/truncate).
	if strings.Contains(content, `strconv.ParseInt(v, 10, 32)`) {
		t.Error("config.go should not use ParseInt 32-bit path for PORT")
	}

	// Must reject port == 0 explicitly.
	if !strings.Contains(content, "must be in [1, 65535]") {
		t.Error("config.go should emit a clear error for out-of-range PORT")
	}
}
