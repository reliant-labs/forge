// Package codegen — config_projection_gen.go emits the VALUE-projection
// half that pairs with the TYPE half in config_schema_gen.go.
//
// Where config_schema_gen.go projects the SHAPE (`schema AppConfig`), this
// emitter projects the BEHAVIOR: one generated KCL function that turns a
// typed `AppConfig` value into the agnostic-core env model an agnostic
// `forge.Service` consumes —
//
//   - appConfigEnvMap(c) -> {str: forge.EnvSource}
//     the agnostic-core env MAP (kcl/core.k), keyed by ENV_VAR name, one
//     entry per config field with a non-empty env_var:
//       * non-sensitive -> {value = <the value read off c>}
//                          (lowered INLINE — the typed field converted to a
//                          string, no ConfigMap object or reference)
//       * sensitive     -> {from_secret = {name = c.<field>.name,
//                                          key  = c.<field>.key}}
//                          (reads the typed ConfigSecretRef off the value)
//
// This REPLACES the old `appConfigEnvVars(...) -> [forge.EnvVar]` list
// projection. The env MAP is the idiomatic authoring shape: a service
// consumes config the native way —
//
//	env = appConfigEnvMap(app_config) | { <service extras> }
//
// — composing config-first with native KCL map-merge `|` (last-wins), so a
// service extra of the same env-var NAME overrides the config entry, and
// duplicate keys are structurally impossible (no `env_merge`). The k8s
// adapter then projects the map to `[EnvVar]` via `env_project` (core.k).
//
// Non-sensitive config values are lowered INLINE as `{value = ...}` rather
// than through a ConfigMap object + reference — simpler is better: it drops
// the whole ConfigMap object and its cross-reference wiring. Only sensitive
// fields still route out-of-band, through `from_secret`.
//
// The per-field metadata (env-var name, sensitive flag, value expression)
// is BAKED into the generated code from the proto — the function is
// generated, not generic — so the emitted KCL is a straight-line map/dict
// literal with no runtime reflection.
//
// This is ADDITIVE: a standalone emitter + test, NOT wired into the generate
// pipeline, and it does NOT touch deploy_config_gen.go (the current Go
// projector). The runtime Go loader (pkg/config/loader.go, which binds env by
// each field's env_var) is unchanged.
//
// The sensitive branch reads the typed ConfigSecretRef off the AppConfig value
// (from_secret.name = c.<field>.name, key = c.<field>.key). The default backend
// (name = "<project>-secrets", key = lower(env_var)) is supplied by the
// AppConfig field's SCHEMA DEFAULT (see config_schema_gen.go), and a per-env
// override flows through as a ConfigSecretRef the migration (config_k_gen.go)
// writes into config.k — so an author who overrides the backing Secret/key
// gets that here unchanged.
package codegen

import (
	"fmt"
	"strings"
)

// GenerateConfigProjectionKCL emits a KCL file declaring the config
// projection function (appConfigEnvMap) as a top-level lambda — the
// idiomatic forge-KCL function form (see kcl/base.k).
//
// The function references the `AppConfig` schema emitted by
// GenerateConfigSchemaKCL (co-located in the same KCL package) and the
// `forge.EnvSource` schema from the forge module, so the file carries its
// own `import forge`.
//
// Fields are visited in proto order. A field with an empty env_var (a
// component config-block reference, or any field with no env binding) is
// skipped — mirroring the runtime loader, which binds env only for fields
// that declare an env_var.
func GenerateConfigProjectionKCL(fields []ConfigField) (string, error) {
	var b strings.Builder

	// 2-line generated-file header, matching the Phase-1 emitter's stamp
	// convention (see config_schema_gen.go).
	b.WriteString("# Code generated by forge. DO NOT EDIT.\n")
	b.WriteString("# Config projection function (typed AppConfig -> {str: forge.EnvSource}) projected from proto/config/v1/config.proto — add or change fields in the PROTO, never here.\n\n")

	// The function references forge.EnvSource AND the AppConfig schema from the
	// sibling config_schema.k module. KCL does not share top-level symbols
	// across separately-imported modules, so AppConfig is imported + qualified
	// as config_schema.AppConfig (see ConfigSchemaModule).
	b.WriteString("import forge\n")
	b.WriteString(fmt.Sprintf("import %s\n\n", ConfigSchemaModule))

	b.WriteString(renderAppConfigEnvMap(fields))

	return b.String(), nil
}

// renderAppConfigEnvMap emits the appConfigEnvMap lambda: the agnostic-core
// env MAP `{str: forge.EnvSource}` (kcl/core.k) keyed by ENV_VAR name, one
// entry per field with a non-empty env_var. Sensitive fields route to the
// `from_secret` channel (reading the typed ConfigSecretRef off the value);
// non-sensitive fields lower INLINE to the `value` channel — the typed field
// read off `c` and converted to a string (see kclConfigValueExpr), NOT a
// ConfigMap reference. Simpler is better: there is no ConfigMap object or
// cross-reference to maintain.
//
// A service consumes this the idiomatic way — config first, service extras
// merged/overriding via native map-merge:
//
//	env = appConfigEnvMap(app_config) | { "EXTRA" = {value = "x"} }
func renderAppConfigEnvMap(fields []ConfigField) string {
	type kv struct{ key, expr string }
	var entries []kv
	for _, f := range fields {
		// Empty env_var == no env binding (block-reference messages and any
		// unbound field). Matches the runtime loader and renderEnvVarEntry.
		if f.EnvVar == "" {
			continue
		}
		if f.Sensitive {
			// Read the typed ConfigSecretRef off the AppConfig value: its
			// name/key ARE the from_secret name/key. The AppConfig field's
			// SCHEMA DEFAULT supplies the default backend (<project>-secrets /
			// lower(env_var)); an author who set a ConfigSecretRef override
			// (the ${NAME#KEY} case) flows through here unchanged.
			entries = append(entries, kv{
				key:  f.EnvVar,
				expr: fmt.Sprintf(`{from_secret = {name = c.%s.name, key = c.%s.key}}`, f.Name, f.Name),
			})
			continue
		}
		// Non-sensitive -> INLINE value channel. The typed field is read off
		// `c` and converted to a string via kclConfigValueExpr (the SAME
		// value-formatting that previously fed the ConfigMap data), lowered
		// directly as `{value = ...}` — no ConfigMap object, no reference.
		entries = append(entries, kv{
			key:  f.EnvVar,
			expr: fmt.Sprintf(`{value = %s}`, kclConfigValueExpr(f, "c")),
		})
	}

	var b strings.Builder
	b.WriteString("# appConfigEnvMap projects a typed AppConfig into the agnostic-core env\n")
	b.WriteString("# MAP — one forge.EnvSource per field that declares an env_var, keyed by\n")
	b.WriteString("# ENV_VAR name. A service merges it with `|`: `appConfigEnvMap(cfg) | {…}`.\n")
	b.WriteString(fmt.Sprintf("appConfigEnvMap = lambda c: %s.AppConfig -> {str: forge.EnvSource} {\n", ConfigSchemaModule))
	if len(entries) == 0 {
		b.WriteString("    {}\n")
	} else {
		b.WriteString("    {\n")
		for _, e := range entries {
			b.WriteString(fmt.Sprintf("        %q = %s\n", e.key, e.expr))
		}
		b.WriteString("    }\n")
	}
	b.WriteString("}\n")
	return b.String()
}

// kclConfigValueExpr renders the KCL expression that reads field f off the
// typed AppConfig receiver `recv` as a STRING (env values are strings).
// String-typed fields — including durations, which forge carries as strings
// ("30s") — inline directly; int/float scalars are wrapped in KCL's str(...)
// builtin.
//
// Bools are the exception: KCL's str(True) yields "True"/"False"
// (capitalized), but the runtime Go loader parses env values with
// strconv.ParseBool and the canonical lowercase form is "true"/"false". So a
// bool is projected as an explicit `"true" if c.<field> else "false"`
// conditional to emit the lowercase form, NOT str(...).
//
// This reuses kclTypeForProtoConfig so the str/non-str decision stays
// identical to the schema emitter's type mapping.
func kclConfigValueExpr(f ConfigField, recv string) string {
	ref := recv + "." + f.Name
	switch kclTypeForProtoConfig(f) {
	case "str":
		return ref
	case "bool":
		return `"true" if ` + ref + ` else "false"`
	default:
		return "str(" + ref + ")"
	}
}
