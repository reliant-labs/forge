// Package codegen — config_schema_gen.go emits a typed KCL
// `schema AppConfig` projected from the proto config fields
// (the `(forge.v1.config)`-annotated fields of proto/config/v1/config.proto).
//
// This is the TYPE half of the config-as-KCL story. Where
// deploy_config_gen.go projects per-env VALUE literals ([forge.EnvVar] +
// [forge.ConfigMap]) from the sibling `config.<env>.yaml`, this emitter
// projects the SHAPE: one KCL schema whose fields mirror the proto config
// fields, so an env authors its config as a typed `AppConfig { ... }`
// instance in `deploy/kcl/<env>/config.k` and KCL type-checks it against
// the proto's own contract (required fields, types, defaults) at load time.
//
// Phase 1 (this file) is ADDITIVE: it is a standalone emitter + test and
// is NOT yet wired into the generate pipeline. The runtime Go loader
// (pkg/config/loader.go), which binds env by each field's `env_var`
// annotation via descriptor reflection, is unchanged — the KCL schema
// describes the same fields, so the two stay coherent by construction.
package codegen

import (
	"fmt"
	"strconv"
	"strings"
)

// ConfigSchemaModule is the KCL module name (and filename stem) the config
// schema is emitted under: deploy/kcl/config_schema.k. Both the projection
// file (config_projection.k) and each per-env values file (config.k) import it
// by THIS name and qualify the AppConfig / ConfigSecretRef schemas as
// `config_schema.AppConfig` etc. — KCL does not share top-level symbols across
// separately-imported modules, so the reference must be qualified.
const ConfigSchemaModule = "config_schema"

// GenerateConfigSchemaKCL emits a KCL file declaring `schema AppConfig`,
// one field per proto config field in proto order. Each field's KCL type
// is mapped from its proto type (see kclTypeForProtoConfig) and its
// default is projected from the proto annotation (see
// kclConfigDefaultLiteral). Fields with a description get a leading `#`
// doc comment.
//
// Component config-block references — a message-typed field whose target
// message carries its OWN config fields — arrive from forge's descriptor
// extractor as ProtoType == "message" WITH MessageType set to the block's
// name (e.g. "TraderConfig") and NO env_var of their own. They carry no
// scalar KCL type and env binds on the referenced message's leaves, so
// they are skipped in Phase 1 with a comment marking the omission.
//
// NOTE the discriminator is MessageType, not ProtoType == "message":
// forge's extractor (internal/cli/forge_descriptor.go) collapses EVERY
// message kind to ProtoType "message", so a `google.protobuf.Duration`
// LEAF field also has ProtoType "message" (with GoType "string",
// MessageType unset, env_var set). Those are real scalar-carried config
// leaves and MUST be emitted (as `str`), not skipped — keying the skip on
// MessageType is what tells the two apart.
func GenerateConfigSchemaKCL(fields []ConfigField, projectName string) (string, error) {
	var b strings.Builder

	// 2-line generated-file header. Line 1 is the standard forge stamp;
	// line 2 tells the reader where to author values and where to add
	// fields (the proto, never this generated file).
	b.WriteString("# Code generated by forge. DO NOT EDIT.\n")
	b.WriteString("# Typed config schema projected from proto/config/v1/config.proto — author per-env values in deploy/kcl/<env>/config.k as an `AppConfig { ... }` instance; add or change fields in the PROTO, never here.\n\n")

	// ConfigSecretRef is the typed model for a SENSITIVE config field: a
	// reference to the Secret NAME + KEY that backs it, never the secret
	// VALUE. It is named ConfigSecretRef (not SecretRef) to avoid clashing
	// with the forge module's own secret schemas (RenderedSecretKey et al.).
	// Every sensitive field defaults to `{ name = "<project>-secrets", key =
	// "<lower(env_var)>" }` — the same default backend the Go projector wires
	// — so an AppConfig instance need only spell a ConfigSecretRef for fields
	// that point at a DIFFERENT Secret/key (the ${NAME#KEY} override case).
	b.WriteString("schema ConfigSecretRef:\n")
	b.WriteString("    name: str\n")
	b.WriteString("    key: str\n\n")

	b.WriteString("schema AppConfig:\n")

	// Count the fields we will actually emit (message block-references are
	// skipped) so the empty case emits a valid placeholder schema.
	emitted := 0
	var body strings.Builder
	for _, f := range fields {
		if f.MessageType != "" {
			// Block-reference to a component config message: no scalar
			// KCL type, env binds on its leaves. Out of scope for Phase 1.
			// (A duration/WKT leaf has ProtoType "message" too but NO
			// MessageType — it is emitted below as a str.)
			fmt.Fprintf(&body, "    # (omitted) %s references config message %q — its leaf fields bind env; nested schemas are not yet projected.\n", f.Name, f.MessageType)
			continue
		}
		if d := strings.TrimSpace(f.Description); d != "" {
			// Collapse embedded newlines so the doc comment stays a single
			// KCL comment line.
			d = strings.Join(strings.Fields(d), " ")
			fmt.Fprintf(&body, "    # %s\n", d)
		}
		if f.Sensitive {
			// A sensitive field is typed as a ConfigSecretRef, not a str: its
			// value is a Secret name+key reference, never an inline value. It
			// ALWAYS carries the default-backend literal (even when required),
			// so the author only spells a ConfigSecretRef to override the
			// backing Secret/key. This mirrors the Go projector, which wires a
			// default secret_ref for every sensitive field regardless of
			// per-env value.
			secretName := fmt.Sprintf("%s-secrets", projectName)
			secretKey := strings.ToLower(f.EnvVar)
			fmt.Fprintf(&body, "    %s: ConfigSecretRef = ConfigSecretRef { name = %q, key = %q }\n",
				f.Name, secretName, secretKey)
			emitted++
			continue
		}
		kt := kclTypeForProtoConfig(f)
		if lit, ok := kclConfigDefaultLiteral(f); ok {
			fmt.Fprintf(&body, "    %s: %s = %s\n", f.Name, kt, lit)
		} else {
			// Required field with no default: no `= ...` means KCL requires
			// the author to set it in their AppConfig instance.
			fmt.Fprintf(&body, "    %s: %s\n", f.Name, kt)
		}
		emitted++
	}

	if emitted == 0 {
		// A KCL schema suite must be non-empty. A docstring-only suite is
		// valid KCL and doubles as the placeholder note.
		b.WriteString("    # No (forge.v1.config)-annotated config fields were found.\n")
		b.WriteString("    \"\"\"No config fields projected from proto/config/v1/config.proto yet — add `(forge.v1.config)`-annotated fields to the proto to populate this schema.\"\"\"\n")
		return b.String(), nil
	}

	b.WriteString(body.String())
	return b.String(), nil
}

// kclTypeForProtoConfig maps a config field's proto type to its KCL type.
//
//	string                    -> str
//	int32/int64/uint32/uint64 -> int
//	bool                      -> bool
//	float/double              -> float
//	google.protobuf.Duration  -> str  (forge carries durations as strings
//	                                    like "30m"/"5s")
//
// Unknown scalar spellings fall back to `str` — the conservative choice
// (any value round-trips through a string) rather than emitting an
// undefined KCL type.
func kclTypeForProtoConfig(f ConfigField) string {
	// Durations render as strings regardless of how the descriptor spells
	// them (proto message name or the Go time.Duration alias).
	if f.ProtoType == "google.protobuf.Duration" || f.GoType == "time.Duration" || f.GoType == "*durationpb.Duration" {
		return "str"
	}
	switch f.ProtoType {
	case "string":
		return "str"
	case "int32", "int64", "uint32", "uint64":
		return "int"
	case "bool":
		return "bool"
	case "float", "double":
		return "float"
	case "message":
		// A message-typed LEAF that reached here (MessageType unset, so
		// it's not a block reference) is a well-known scalar-carried type —
		// today only google.protobuf.Duration, which forge carries as a
		// string ("30m"/"5s"). GoType confirms it ("string").
		return "str"
	default:
		return "str"
	}
}

// kclConfigDefaultLiteral returns the KCL default literal for a config
// field and whether one should be emitted.
//
//   - DefaultValue set  -> the literal in the field's KCL type
//     (str/duration quoted, int/float bare, bool True/False).
//   - Required, no default -> ("", false): emit no default so KCL requires
//     the author to set the field.
//   - Neither required nor default -> the type-zero literal ("" / 0 /
//     False / 0.0) so an AppConfig instance is easy to construct.
func kclConfigDefaultLiteral(f ConfigField) (string, bool) {
	kt := kclTypeForProtoConfig(f)

	if f.DefaultValue != "" {
		switch kt {
		case "str":
			// Covers plain strings AND durations (carried as strings).
			return strconv.Quote(f.DefaultValue), true
		case "int", "float":
			// Numeric literals are emitted bare, straight from the proto.
			return f.DefaultValue, true
		case "bool":
			if isKCLTruthy(f.DefaultValue) {
				return "True", true
			}
			return "False", true
		}
	}

	if f.Required {
		// No default + required: KCL makes an undefaulted field mandatory.
		return "", false
	}

	// Neither required nor default: emit the type-zero so callers can
	// instantiate AppConfig without spelling every field.
	switch kt {
	case "str":
		return `""`, true
	case "int":
		return "0", true
	case "bool":
		return "False", true
	case "float":
		return "0.0", true
	default:
		return `""`, true
	}
}

// isKCLTruthy interprets a proto default_value string as a bool.
func isKCLTruthy(v string) bool {
	switch strings.ToLower(strings.TrimSpace(v)) {
	case "true", "1", "yes", "t":
		return true
	default:
		return false
	}
}
