// representativeConfigFields is a mix that exercises every type/default
// branch: string-with-default, int-with-default, bool, a required
// sensitive string (no default), a duration, and a documented float.
func representativeConfigFields() []ConfigField {
	return []ConfigField{
		{
			Name:         "log_level",
			ProtoType:    "string",
			GoType:       "string",
			EnvVar:       "LOG_LEVEL",
			DefaultValue: "info",
			Description:  "Log level (debug, info, warn, error)",
		},
		{
			Name:         "port",
			ProtoType:    "int32",
			GoType:       "int32",
			EnvVar:       "PORT",
			DefaultValue: "8080",
			Description:  "HTTP server port",
		},
		{
			Name:         "cors_allow_credentials",
			ProtoType:    "bool",
			GoType:       "bool",
			EnvVar:       "CORS_ALLOW_CREDENTIALS",
			DefaultValue: "false",
		},
		{
			Name:        "database_url",
			ProtoType:   "string",
			GoType:      "string",
			EnvVar:      "DATABASE_URL",
			Required:    true,
			Sensitive:   true,
			Description: "PostgreSQL connection string",
		},
		{
			Name:         "shutdown_timeout",
			ProtoType:    "google.protobuf.Duration",
			GoType:       "time.Duration",
			EnvVar:       "SHUTDOWN_TIMEOUT",
			DefaultValue: "30s",
			Description:  "Max drain time during graceful shutdown",
		},
		{
			Name:      "sample_rate",
			ProtoType: "double",
			GoType:    "float64",
			EnvVar:    "SAMPLE_RATE",
		},
	}
}

func TestGenerateConfigSchemaKCL_ExactBlock(t *testing.T) {
	got, err := GenerateConfigSchemaKCL(representativeConfigFields(), "myproj")
	if err != nil {
		t.Fatalf("GenerateConfigSchemaKCL: %v", err)
	}

	want := "# Code generated by forge. DO NOT EDIT.\n" +
		"# Typed config schema projected from proto/config/v1/config.proto — author per-env values in deploy/kcl/<env>/config.k as an `AppConfig { ... }` instance; add or change fields in the PROTO, never here.\n" +
		"\n" +
		"schema ConfigSecretRef:\n" +
		"    name: str\n" +
		"    key: str\n" +
		"\n" +
		"schema AppConfig:\n" +
		"    # Log level (debug, info, warn, error)\n" +
		"    log_level: str = \"info\"\n" +
		"    # HTTP server port\n" +
		"    port: int = 8080\n" +
		"    cors_allow_credentials: bool = False\n" +
		"    # PostgreSQL connection string\n" +
		"    database_url: ConfigSecretRef = ConfigSecretRef { name = \"myproj-secrets\", key = \"database_url\" }\n" +
		"    # Max drain time during graceful shutdown\n" +
		"    shutdown_timeout: str = \"30s\"\n" +
		"    sample_rate: float = 0.0\n"

	if got != want {
		t.Fatalf("schema mismatch\n--- got ---\n%s\n--- want ---\n%s", got, want)
	}
}

func TestGenerateConfigSchemaKCL_Ordering(t *testing.T) {
	// Fields must appear in proto (input) order, not sorted.
	fields := []ConfigField{
		{Name: "zebra", ProtoType: "string", DefaultValue: "z"},
		{Name: "alpha", ProtoType: "string", DefaultValue: "a"},
		{Name: "mid", ProtoType: "string", DefaultValue: "m"},
	}
	got, err := GenerateConfigSchemaKCL(fields, "proj")
	if err != nil {
		t.Fatal(err)
	}
	iZ := strings.Index(got, "zebra:")
	iA := strings.Index(got, "alpha:")
	iM := strings.Index(got, "mid:")
	if !(iZ < iA && iA < iM) {
		t.Fatalf("fields not in proto order: zebra=%d alpha=%d mid=%d\n%s", iZ, iA, iM, got)
	}
}

func TestGenerateConfigSchemaKCL_Empty(t *testing.T) {
	got, err := GenerateConfigSchemaKCL(nil, "proj")
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(got, "schema AppConfig:") {
		t.Fatalf("empty case missing schema decl:\n%s", got)
	}
	if !strings.Contains(got, "No (forge.v1.config)-annotated config fields") {
		t.Fatalf("empty case missing placeholder note:\n%s", got)
	}
	// A docstring keeps the suite non-empty and thus valid KCL.
	if !strings.Contains(got, `"""`) {
		t.Fatalf("empty case should emit a docstring for a valid KCL suite:\n%s", got)
	}
}

func TestGenerateConfigSchemaKCL_MessageBlockRefSkipped(t *testing.T) {
	fields := []ConfigField{
		{Name: "port", ProtoType: "int32", DefaultValue: "8080"},
		{Name: "trader", ProtoType: "message", MessageType: "TraderConfig"},
	}
	got, err := GenerateConfigSchemaKCL(fields, "proj")
	if err != nil {
		t.Fatal(err)
	}
	if strings.Contains(got, "trader: ") {
		t.Fatalf("message block-ref should not emit a typed field:\n%s", got)
	}
	if !strings.Contains(got, "(omitted) trader references config message") {
		t.Fatalf("message block-ref should emit an omission comment:\n%s", got)
	}
}

// TestGenerateConfigSchemaKCL_RealDurationShape pins the ACTUAL shape
// forge's descriptor extractor produces for a google.protobuf.Duration
// config leaf: ProtoType "message" (every message kind collapses to
// "message"), GoType "string", MessageType UNSET, env_var set. It must be
// emitted as a `str` default — NOT skipped as a block reference.
func TestGenerateConfigSchemaKCL_RealDurationShape(t *testing.T) {
	fields := []ConfigField{
		{
			Name:         "shutdown_timeout",
			GoName:       "ShutdownTimeout",
			ProtoType:    "message", // as forge_descriptor.go actually emits
			GoType:       "string",
			EnvVar:       "SHUTDOWN_TIMEOUT",
			DefaultValue: "30s",
			Description:  "Graceful shutdown drain time",
		},
	}
	got, err := GenerateConfigSchemaKCL(fields, "proj")
	if err != nil {
		t.Fatal(err)
	}
	if strings.Contains(got, "(omitted)") {
		t.Fatalf("duration leaf must not be skipped as a block ref:\n%s", got)
	}
	if !strings.Contains(got, `shutdown_timeout: str = "30s"`) {
		t.Fatalf("duration leaf should emit `str = \"30s\"`:\n%s", got)
	}
}

func TestKCLTypeForProtoConfig(t *testing.T) {
	cases := []struct {
		f    ConfigField
		want string
	}{
		{ConfigField{ProtoType: "string"}, "str"},
		{ConfigField{ProtoType: "int32"}, "int"},
		{ConfigField{ProtoType: "int64"}, "int"},
		{ConfigField{ProtoType: "uint32"}, "int"},
		{ConfigField{ProtoType: "uint64"}, "int"},
		{ConfigField{ProtoType: "bool"}, "bool"},
		{ConfigField{ProtoType: "float"}, "float"},
		{ConfigField{ProtoType: "double"}, "float"},
		{ConfigField{ProtoType: "google.protobuf.Duration"}, "str"},
		{ConfigField{ProtoType: "message", GoType: "time.Duration"}, "str"},
		{ConfigField{ProtoType: "message", GoType: "string"}, "str"}, // real duration-leaf shape
		{ConfigField{ProtoType: "somethingWeird"}, "str"},
	}
	for _, c := range cases {
		if got := kclTypeForProtoConfig(c.f); got != c.want {
			t.Errorf("kclTypeForProtoConfig(%+v) = %q, want %q", c.f, got, c.want)
		}
	}
}

func TestKCLConfigDefaultLiteral(t *testing.T) {
	cases := []struct {
		name    string
		f       ConfigField
		wantLit string
		wantOK  bool
	}{
		{"str default", ConfigField{ProtoType: "string", DefaultValue: "info"}, `"info"`, true},
		{"int default", ConfigField{ProtoType: "int32", DefaultValue: "8080"}, "8080", true},
		{"float default", ConfigField{ProtoType: "double", DefaultValue: "0.25"}, "0.25", true},
		{"bool default true", ConfigField{ProtoType: "bool", DefaultValue: "true"}, "True", true},
		{"bool default false", ConfigField{ProtoType: "bool", DefaultValue: "false"}, "False", true},
		{"duration default", ConfigField{ProtoType: "google.protobuf.Duration", DefaultValue: "30s"}, `"30s"`, true},
		{"required no default", ConfigField{ProtoType: "string", Required: true}, "", false},
		{"str zero", ConfigField{ProtoType: "string"}, `""`, true},
		{"int zero", ConfigField{ProtoType: "int32"}, "0", true},
		{"bool zero", ConfigField{ProtoType: "bool"}, "False", true},
		{"float zero", ConfigField{ProtoType: "double"}, "0.0", true},
	}
	for _, c := range cases {
		t.Run(c.name, func(t *testing.T) {
			lit, ok := kclConfigDefaultLiteral(c.f)
			if lit != c.wantLit || ok != c.wantOK {
				t.Errorf("kclConfigDefaultLiteral = (%q, %v), want (%q, %v)", lit, ok, c.wantLit, c.wantOK)
			}
		})
	}
}
