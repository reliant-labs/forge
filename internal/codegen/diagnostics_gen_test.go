// scaffoldMarkerToken is the FORGE_SCAFFOLD marker without the leading
// "// ", spliced into raw-string test fixtures so that the scaffolds
// linter (which matches "// FORGE_SCAFFOLD:" at line start) doesn't
// fire on this test file's source bytes.
const scaffoldMarkerToken = "FORGE_SCAFFOLD:"

// TestGenerateDiagnostics_NoComponentsSkips asserts the diagnostics
// codegen is a no-op for a project with no services / workers /
// operators — there's no pkg/app/ either in that shape, so writing the
// file would create an orphan.
func TestGenerateDiagnostics_NoComponentsSkips(t *testing.T) {
	projectDir := t.TempDir()
	if err := GenerateDiagnostics(nil, nil, nil, "example.com/proj", projectDir, nil, nil); err != nil {
		t.Fatalf("GenerateDiagnostics: %v", err)
	}
	if _, err := os.Stat(filepath.Join(projectDir, "pkg", "app", "diagnostics_gen.go")); !os.IsNotExist(err) {
		t.Errorf("expected no diagnostics_gen.go to be written; got err=%v", err)
	}
}

// TestGenerateDiagnostics_EmptyEntriesEmitsCleanFile asserts that even
// with zero detected entries, the file is still emitted with a clean
// init() body — checked into VCS so reviewers can observe the
// no-unwired-scaffolds state without grepping for absence.
func TestGenerateDiagnostics_EmptyEntriesEmitsCleanFile(t *testing.T) {
	projectDir := t.TempDir()
	// One service so the guard at the top of GenerateDiagnostics passes;
	// no handler directory + no nil deps → zero entries.
	services := []ServiceDef{
		{Name: "APIService", ModulePath: "example.com/proj"},
	}
	if err := GenerateDiagnostics(services, nil, nil, "example.com/proj", projectDir, nil, nil); err != nil {
		t.Fatalf("GenerateDiagnostics: %v", err)
	}
	data, err := os.ReadFile(filepath.Join(projectDir, "pkg", "app", "diagnostics_gen.go"))
	if err != nil {
		t.Fatalf("read diagnostics_gen.go: %v", err)
	}
	content := string(data)
	for _, want := range []string{
		"// Code generated by forge. DO NOT EDIT.",
		"package app",
		"import _ \"github.com/reliant-labs/forge/pkg/diagnostics\"",
		"func init() {",
		"No unwired scaffolds detected",
	} {
		if !strings.Contains(content, want) {
			t.Errorf("diagnostics_gen.go missing %q\n--- content ---\n%s", want, content)
		}
	}
	// Must NOT contain any Register calls — the clean signal is the
	// absence of registrations, not their suppression.
	if strings.Contains(content, "RegisterStub") || strings.Contains(content, "RegisterNilDep") {
		t.Errorf("clean diagnostics_gen.go should have no Register* calls\n--- content ---\n%s", content)
	}
}

// TestGenerateDiagnostics_NilDepEmitsRegisterCall asserts a nil-dep
// entry passed in (from wire_gen's UnresolvedFields) emits a
// RegisterNilDep line in the init() body with the component +
// dep-name + file + line as the codegen captured them.
func TestGenerateDiagnostics_NilDepEmitsRegisterCall(t *testing.T) {
	projectDir := t.TempDir()
	services := []ServiceDef{
		{Name: "APIService", ModulePath: "example.com/proj"},
	}
	nilDeps := []DiagnosticEntry{
		{
			Kind:      "nil-dep",
			Symbol:    "wireAPIDeps.Repo",
			File:      "pkg/app/wire_gen.go",
			Line:      0,
			Component: "wireAPIDeps",
			DepName:   "Repo",
		},
	}
	if err := GenerateDiagnostics(services, nil, nil, "example.com/proj", projectDir, nilDeps, nil); err != nil {
		t.Fatalf("GenerateDiagnostics: %v", err)
	}
	data, err := os.ReadFile(filepath.Join(projectDir, "pkg", "app", "diagnostics_gen.go"))
	if err != nil {
		t.Fatalf("read diagnostics_gen.go: %v", err)
	}
	content := string(data)
	want := `diagnostics.Default.RegisterNilDep("wireAPIDeps", "Repo", "pkg/app/wire_gen.go", 0)`
	if !strings.Contains(content, want) {
		t.Errorf("diagnostics_gen.go missing %q\n--- content ---\n%s", want, content)
	}
}

// TestGenerateDiagnostics_StubMarkerScan asserts the stub-impl
// scanner picks up the `// forge:gen unwired-stub symbol=...` marker
// from handlers/<pkg>/*.go and emits a RegisterStub call.
func TestGenerateDiagnostics_StubMarkerScan(t *testing.T) {
	projectDir := t.TempDir()
	handlerDir := filepath.Join(projectDir, "internal", "handlers", "api")
	if err := os.MkdirAll(handlerDir, 0o755); err != nil {
		t.Fatal(err)
	}
	// Handler file with two methods — one fresh stub (marker present),
	// one already implemented (no marker). The scanner should find
	// exactly one entry.
	source := `package api

import (
	"context"

	"connectrpc.com/connect"
)

// Ping implements the Ping RPC.
// ` + scaffoldMarkerToken + ` implement business logic for Ping.
// Remove this comment after editing.
// forge:gen unwired-stub symbol=api.Ping
func (s *Service) Ping(ctx context.Context, req *connect.Request[any]) (*connect.Response[any], error) {
	return nil, connect.NewError(connect.CodeUnimplemented, nil)
}

// Echo is implemented; no marker.
func (s *Service) Echo(ctx context.Context, req *connect.Request[any]) (*connect.Response[any], error) {
	return connect.NewResponse[any](nil), nil
}
`
	if err := os.WriteFile(filepath.Join(handlerDir, "handlers.go"), []byte(source), 0o644); err != nil {
		t.Fatal(err)
	}

	services := []ServiceDef{
		{Name: "APIService", ModulePath: "example.com/proj"},
	}
	if err := GenerateDiagnostics(services, nil, nil, "example.com/proj", projectDir, nil, nil); err != nil {
		t.Fatalf("GenerateDiagnostics: %v", err)
	}
	data, err := os.ReadFile(filepath.Join(projectDir, "pkg", "app", "diagnostics_gen.go"))
	if err != nil {
		t.Fatalf("read diagnostics_gen.go: %v", err)
	}
	content := string(data)
	if !strings.Contains(content, `RegisterStub("api.Ping",`) {
		t.Errorf("missing Ping stub registration\n--- content ---\n%s", content)
	}
	if strings.Contains(content, `RegisterStub("api.Echo",`) {
		t.Errorf("unexpected Echo stub registration\n--- content ---\n%s", content)
	}
}

// TestNilDepEntriesFromWireData_SkipsResolved asserts the helper
// produces zero entries for a WireGenServiceData with no
// UnresolvedFields (the resolved-clean case). The opposite case is
// covered by TestGenerateDiagnostics_NilDepEmitsRegisterCall.
func TestNilDepEntriesFromWireData_SkipsResolved(t *testing.T) {
	wireData := []WireGenServiceData{
		{
			FieldName:        "API",
			UnresolvedFields: nil,
		},
	}
	entries := NilDepEntriesFromWireData(wireData, "wire")
	if len(entries) != 0 {
		t.Errorf("expected 0 entries for resolved wire data, got %d: %+v", len(entries), entries)
	}
}

// TestNilDepEntriesFromWireData_ComponentNames asserts the wireFuncPrefix
// shapes the Component/Symbol correctly for services vs workers vs
// operators — the operator-facing log line names the wire function
// the operator can grep in wire_gen.go.
func TestNilDepEntriesFromWireData_ComponentNames(t *testing.T) {
	wireData := []WireGenServiceData{
		{
			FieldName: "CalibratorRefit",
			UnresolvedFields: []WireUnresolved{
				{Name: "PgUnsettled", Type: "*pg.UnsettledRepo"},
			},
		},
	}
	entries := NilDepEntriesFromWireData(wireData, "wireWorker")
	if len(entries) != 1 {
		t.Fatalf("expected 1 entry, got %d", len(entries))
	}
	e := entries[0]
	if e.Component != "wireWorkerCalibratorRefitDeps" {
		t.Errorf("Component = %q, want wireWorkerCalibratorRefitDeps", e.Component)
	}
	if e.DepName != "PgUnsettled" {
		t.Errorf("DepName = %q, want PgUnsettled", e.DepName)
	}
	if e.Symbol != "wireWorkerCalibratorRefitDeps.PgUnsettled" {
		t.Errorf("Symbol = %q, want wireWorkerCalibratorRefitDeps.PgUnsettled", e.Symbol)
	}
}

// TestExtractStubSymbol_Variants exercises the marker-parse helper
// against the canonical line shape, a bare-symbol fallback, and an
// empty marker — the latter two are tolerated so future template
// edits don't silently drop entries.
func TestExtractStubSymbol_Variants(t *testing.T) {
	cases := []struct {
		name        string
		line        string
		fallbackPkg string
		want        string
	}{
		{
			name:        "canonical",
			line:        "// forge:gen unwired-stub symbol=api.Ping",
			fallbackPkg: "api",
			want:        "api.Ping",
		},
		{
			name:        "bare-symbol-fallback",
			line:        "// forge:gen unwired-stub symbol=Ping",
			fallbackPkg: "api",
			want:        "api.Ping",
		},
		{
			name:        "quoted-symbol",
			line:        `// forge:gen unwired-stub symbol="api.Ping"`,
			fallbackPkg: "api",
			want:        "api.Ping",
		},
		{
			name:        "missing-symbol",
			line:        "// forge:gen unwired-stub",
			fallbackPkg: "api",
			want:        "",
		},
	}
	for _, c := range cases {
		t.Run(c.name, func(t *testing.T) {
			got := extractStubSymbol(c.line, c.fallbackPkg)
			if got != c.want {
				t.Errorf("extractStubSymbol(%q) = %q, want %q", c.line, got, c.want)
			}
		})
	}
}
