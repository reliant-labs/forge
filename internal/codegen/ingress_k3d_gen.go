// Package codegen — ingress_k3d_gen.go writes deploy/k3d-ports.yaml,
// a small YAML fragment carrying the host→cluster port mappings the
// k3d loadbalancer needs to expose each Gateway listener.
//
// The fragment is derived from the dev environment's KCL ingress
// topology, NOT hand-written. `forge cluster up` merges this
// fragment into the user-owned deploy/k3d.yaml at create time so
// k3d's ports block reflects the current gateway listeners. Keeping
// the merge at cluster-up time (rather than baking ports into
// k3d.yaml directly) lets the user edit `deploy/k3d.yaml` freely
// without forge clobbering it, while still keeping the listener
// ports in lockstep with whatever the project's Gateway resources
// declare.
//
// Shape (one entry per listener across all dev-env gateways):
//
//   # Generated by `forge generate`. Do not edit — regenerated from
//   # deploy/kcl/dev/ingress.k.
//   ports:
//     - port: 18080:18080
//       nodeFilters: [loadbalancer]
//     - port: 19190:19190
//       nodeFilters: [loadbalancer]
//
// Empty gateway list → no file written. Stale file from a previous
// generate (e.g. ingress feature was just disabled) is cleaned up by
// the caller, not here.
package codegen

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/reliant-labs/forge/internal/checksums"
)

// K3dListener is the minimal slice of a Gateway listener that the
// k3d-ports fragment needs. Callers project from
// internal/cli.GatewayEntity.Listeners → []K3dListener so this
// package stays free of the cli dependency.
type K3dListener struct {
	GatewayName  string // for de-dup logs and comment lines
	ListenerName string
	Port         int
}

// K3dPortsGenInput is the per-project input for [GenerateK3dPorts].
type K3dPortsGenInput struct {
	ProjectDir string                   // project root; output relative to here
	Listeners  []K3dListener            // every dev-env listener, in any order
	Checksums  *checksums.FileChecksums // when set, the rendered fragment is recorded
}

// GenerateK3dPorts writes deploy/k3d-ports.yaml. Returns nil when
// there are no listeners — the caller is expected to ensure a stale
// file is removed in that case (handled in the pipeline gate, not
// here).
//
// The function is idempotent: same input → byte-identical output.
// Listeners are sorted by (port, gateway, name) so a hand-rearranged
// ingress.k doesn't produce a no-op diff.
func GenerateK3dPorts(in K3dPortsGenInput) error {
	if in.ProjectDir == "" {
		return fmt.Errorf("k3d ports gen: ProjectDir required")
	}
	if len(in.Listeners) == 0 {
		return nil
	}

	// Stable sort: by port primarily, then by gateway+listener name as
	// a tiebreaker. Ports must be unique within a single gateway by
	// schema check, but two gateways might use distinct ports — the
	// sort keeps output deterministic regardless of input order.
	sorted := make([]K3dListener, len(in.Listeners))
	copy(sorted, in.Listeners)
	sort.SliceStable(sorted, func(i, j int) bool {
		if sorted[i].Port != sorted[j].Port {
			return sorted[i].Port < sorted[j].Port
		}
		if sorted[i].GatewayName != sorted[j].GatewayName {
			return sorted[i].GatewayName < sorted[j].GatewayName
		}
		return sorted[i].ListenerName < sorted[j].ListenerName
	})

	// Dedup by port. Two gateways binding the same host port is a
	// k3d-side error (port already mapped) — log via comment and pick
	// the first.
	seen := map[int]bool{}
	var body strings.Builder
	body.WriteString("# Generated by `forge generate`. Do not edit — regenerated from\n")
	body.WriteString("# deploy/kcl/dev/ingress.k. `forge cluster up` merges this into\n")
	body.WriteString("# deploy/k3d.yaml's `ports:` block at cluster-create time.\n")
	body.WriteString("ports:\n")
	for _, l := range sorted {
		if seen[l.Port] {
			body.WriteString(fmt.Sprintf("  # port %d declared by multiple listeners — skipped duplicate (gateway=%s listener=%s)\n",
				l.Port, l.GatewayName, l.ListenerName))
			continue
		}
		seen[l.Port] = true
		body.WriteString(fmt.Sprintf("  # %s/%s\n", l.GatewayName, l.ListenerName))
		body.WriteString(fmt.Sprintf("  - port: %d:%d\n", l.Port, l.Port))
		body.WriteString("    nodeFilters:\n")
		body.WriteString("      - loadbalancer\n")
	}

	outPath := filepath.Join(in.ProjectDir, "deploy", "k3d-ports.yaml")
	if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
		return fmt.Errorf("create deploy dir: %w", err)
	}

	rel := filepath.Join("deploy", "k3d-ports.yaml")
	if in.Checksums != nil {
		if err := writeForgeOwned(in.ProjectDir, rel, []byte(body.String()), in.Checksums); err != nil {
			return fmt.Errorf("write %s: %w", rel, err)
		}
		return nil
	}
	return writeUserScaffold(outPath, []byte(body.String()))
}

// RemoveK3dPorts deletes deploy/k3d-ports.yaml when present. Used by
// the generator gate when the ingress feature is off, or when the
// dev env has no gateways — the previous-generate's file would
// otherwise become a stale port-mapping the user might forget about.
func RemoveK3dPorts(projectDir string) error {
	outPath := filepath.Join(projectDir, "deploy", "k3d-ports.yaml")
	if err := os.Remove(outPath); err != nil && !os.IsNotExist(err) {
		return fmt.Errorf("remove %s: %w", outPath, err)
	}
	return nil
}
