package contract

import "text/template"

// templateData is the input to the mock template. Earlier versions of
// this generator emitted three additional shim wrappers
// (middleware/tracing/metrics), but those were superseded by the
// observe.* Connect interceptors and helper APIs. The struct shape is
// preserved so future per-package generators can reuse it without
// re-deriving Imports / InterfaceNames.
type templateData struct {
	Package        string
	Imports        []string
	Interfaces     []InterfaceDef
	InterfaceNames map[string]bool // local interface names (for zeroValue)
}

// contractkitImport is the canonical import path injected for the mock
// template (Recorder embed + MockNotSet). Holding it in one place keeps
// the "is contractkit needed" guard in the generator easy to audit.
const contractkitImport = "github.com/reliant-labs/forge/pkg/contractkit"

// mockTmpl generates mock_gen.go with function-field mocks.
//
// The mock embeds contractkit.Recorder by value so that methods like
// CallCount and Calls are promoted onto MockX without a constructor.
// Each generated method records its arguments and either delegates to
// the user-set XxxFunc or returns the canonical
// "MockX.<Method>Func not set" error from contractkit.MockNotSet —
// preserving the exact behavioural fingerprint that dogfood tests
// assert against.
var mockTmpl = template.Must(template.New("mock").Parse(`// Code generated by forge. DO NOT EDIT.
// Source: contract.go in this package.
//
// To customize: edit contract.go (the interface IS the public surface) and
// re-run "forge generate". This file is regenerated unconditionally.
package {{ .Package }}

{{ if .Imports -}}
import (
{{ range .Imports }}	"{{ . }}"
{{ end }})
{{ end -}}

{{ range $iface := .Interfaces -}}
// Mock{{ $iface.Name }} is a test mock for the {{ $iface.Name }} interface.
//
// The embedded contractkit.Recorder records every call so tests can
// assert call counts and captured arguments. Set XxxFunc fields to
// override per-method behaviour; unset methods return the canonical
// "Mock{{ $iface.Name }}.<Method>Func not set" error.
type Mock{{ $iface.Name }} struct {
	contractkit.Recorder
{{ range $iface.Methods }}	{{ .Name }}Func {{ .FuncFieldType }}
{{ end }}}

{{ range $m := $iface.Methods -}}
func (m *Mock{{ $iface.Name }}) {{ $m.Name }}({{ $m.ParamSignature }}){{ with $m.ResultSignature }} {{ . }}{{ end }} {
	m.Record("{{ $m.Name }}"{{ with $m.RecordArgs }}, {{ . }}{{ end }})
	if m.{{ $m.Name }}Func != nil {
		{{ if $m.HasResults }}return {{ end }}m.{{ $m.Name }}Func({{ $m.CallArgs }}){{ if not $m.HasResults }}
		return{{ end }}
	}
{{ if $m.HasResults }}	return {{ $m.ZeroResults (printf "Mock%s" $iface.Name) $.InterfaceNames }}
{{ end }}}

{{ end -}}
// Compile-time check.
var _ {{ $iface.Name }} = (*Mock{{ $iface.Name }})(nil)

{{ end -}}
`))

