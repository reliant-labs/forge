package contract

import "text/template"

// templateData is the input to both mock and middleware templates.
type templateData struct {
	Package    string
	Imports    []string
	Interfaces []InterfaceDef
}

// mockTmpl generates mock_gen.go with function-field mocks.
var mockTmpl = template.Must(template.New("mock").Parse(`// Code generated by forge. DO NOT EDIT.
package {{ .Package }}

{{ if .Imports -}}
import (
{{ range .Imports }}	"{{ . }}"
{{ end }})
{{ end -}}

{{ range $iface := .Interfaces -}}
// Mock{{ $iface.Name }} is a test mock for the {{ $iface.Name }} interface.
type Mock{{ $iface.Name }} struct {
{{ range $iface.Methods }}	{{ .Name }}Func {{ .FuncFieldType }}
{{ end }}}

{{ range $m := $iface.Methods -}}
func (m *Mock{{ $iface.Name }}) {{ $m.Name }}({{ $m.ParamSignature }}){{ with $m.ResultSignature }} {{ . }}{{ end }} {
	if m.{{ $m.Name }}Func != nil {
		{{ if $m.HasResults }}return {{ end }}m.{{ $m.Name }}Func({{ $m.CallArgs }}){{ if not $m.HasResults }}
		return{{ end }}
	}
{{ if $m.HasResults }}	return {{ $m.ZeroResults (printf "Mock%s" $iface.Name) }}
{{ end }}}

{{ end -}}
// Compile-time check.
var _ {{ $iface.Name }} = (*Mock{{ $iface.Name }})(nil)

{{ end -}}
`))

// middlewareTmpl generates middleware_gen.go with logging/tracing wrappers.
var middlewareTmpl = template.Must(template.New("middleware").Parse(`// Code generated by forge. DO NOT EDIT.
package {{ .Package }}

{{ if .Imports -}}
import (
{{ range .Imports }}	"{{ . }}"
{{ end }})
{{ end -}}

{{ range $iface := .Interfaces -}}
// Instrumented{{ $iface.Name }} wraps {{ $iface.Name }} with logging and duration tracking.
type Instrumented{{ $iface.Name }} struct {
	inner  {{ $iface.Name }}
	logger *slog.Logger
}

// NewInstrumented{{ $iface.Name }} creates a new instrumented wrapper.
func NewInstrumented{{ $iface.Name }}(inner {{ $iface.Name }}, logger *slog.Logger) {{ $iface.Name }} {
	return &Instrumented{{ $iface.Name }}{inner: inner, logger: logger}
}

{{ range $m := $iface.Methods -}}
func (w *Instrumented{{ $iface.Name }}) {{ $m.Name }}({{ $m.ParamSignature }}){{ with $m.ResultSignature }} {{ . }}{{ end }} {
	start := time.Now()
{{ if $m.HasResults }}	{{ $m.ResultNames }} := w.inner.{{ $m.Name }}({{ $m.CallArgs }})
{{ if $m.LastResultIsError }}	w.logger.Info("{{ $m.Name }}", "duration", time.Since(start), "error", {{ $m.ErrorResultName }})
{{ else }}	w.logger.Info("{{ $m.Name }}", "duration", time.Since(start))
{{ end }}	return {{ $m.ResultNames }}
{{ else }}	w.inner.{{ $m.Name }}({{ $m.CallArgs }})
	w.logger.Info("{{ $m.Name }}", "duration", time.Since(start))
{{ end }}}

{{ end -}}
// Compile-time check.
var _ {{ $iface.Name }} = (*Instrumented{{ $iface.Name }})(nil)

{{ end -}}
`))

// tracingTmpl generates tracing_gen.go with OpenTelemetry tracing wrappers.
var tracingTmpl = template.Must(template.New("tracing").Parse(`// Code generated by forge. DO NOT EDIT.
package {{ .Package }}

{{ if .Imports -}}
import (
{{ range .Imports }}	"{{ . }}"
{{ end }})
{{ end -}}

{{ range $iface := .Interfaces -}}
// Traced{{ $iface.Name }} wraps a {{ $iface.Name }} implementation with OpenTelemetry tracing.
// Every method call creates a span with the method name, records duration,
// and captures errors as span events.
type Traced{{ $iface.Name }} struct {
	inner  {{ $iface.Name }}
	tracer trace.Tracer
}

// NewTraced{{ $iface.Name }} creates a tracing wrapper around the given {{ $iface.Name }}.
func NewTraced{{ $iface.Name }}(inner {{ $iface.Name }}, tracer trace.Tracer) {{ $iface.Name }} {
	return &Traced{{ $iface.Name }}{inner: inner, tracer: tracer}
}

{{ range $m := $iface.Methods -}}
func (w *Traced{{ $iface.Name }}) {{ $m.Name }}({{ $m.ParamSignature }}){{ with $m.ResultSignature }} {{ . }}{{ end }} {
	ctx, span := w.tracer.Start(ctx, "{{ $iface.Name }}.{{ $m.Name }}")
	defer span.End()

{{ if $m.HasResults }}	{{ $m.ResultNames }} := w.inner.{{ $m.Name }}({{ $m.CallArgs }})
{{ if $m.LastResultIsError }}	if {{ $m.ErrorResultName }} != nil {
		span.RecordError({{ $m.ErrorResultName }})
		span.SetStatus(codes.Error, {{ $m.ErrorResultName }}.Error())
	}
{{ end }}	return {{ $m.ResultNames }}
{{ else }}	w.inner.{{ $m.Name }}({{ $m.CallArgs }})
{{ end }}}

{{ end -}}
// Compile-time check.
var _ {{ $iface.Name }} = (*Traced{{ $iface.Name }})(nil)

{{ end -}}
`))