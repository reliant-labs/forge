package generator

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

// GeneratePlanDBTypes writes internal/db/types.go with type aliases that map
// entity names to the generated service proto types. This lets the rest of the
// codebase refer to db.Organization while the underlying type lives in the
// service proto package.
func GeneratePlanDBTypes(root, modulePath, serviceName string, entityNames []string) error {
	if len(entityNames) == 0 {
		return nil
	}

	dbDir := filepath.Join(root, "internal", "db")
	if err := os.MkdirAll(dbDir, 0755); err != nil {
		return fmt.Errorf("create db directory: %w", err)
	}

	importPath := fmt.Sprintf("%s/gen/services/%s/v1", modulePath, serviceName)

	var b strings.Builder
	b.WriteString("// Code generated by forge. DO NOT EDIT.\n")
	b.WriteString("package db\n\n")
	fmt.Fprintf(&b, "import (\n\tpb \"%s\"\n)\n\n", importPath)
	b.WriteString("// Type aliases for entity types.\n")
	b.WriteString("// These start as aliases to service proto types. If your DB schema diverges\n")
	b.WriteString("// from the API schema, replace the alias with a concrete struct and add\n")
	b.WriteString("// converter functions.\n")
	for _, name := range entityNames {
		fmt.Fprintf(&b, "type %s = pb.%s\n", name, name)
	}

	return os.WriteFile(filepath.Join(dbDir, "types.go"), []byte(b.String()), 0644)
}
