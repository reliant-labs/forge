package generator

import (
	"fmt"
	"os"
	"path"
	"path/filepath"
	"sort"
	"strings"
)

// GeneratePlanDBTypes writes internal/db/types.go with type aliases that map
// entity names to the proto-generated Go types.
//
// Entities can live in different proto packages — service protos
// (proto/services/<svc>/v1/...) and dedicated entity protos (proto/db/v1/...
// emitted by entity-shipping packs like stripe and twilio). The generated
// internal/db package needs an import alias per distinct proto Go package
// so that aliases resolve correctly.
//
// The function used to assume all entities lived in a single service proto
// and aliased everything to gen/services/<svc>/v1; that broke as soon as a
// pack contributed entities under proto/db/v1/. The current implementation
// groups entities by their proto package (derived from each entity's
// ProtoFile) and emits one import alias per group.
func GeneratePlanDBTypes(root, modulePath, serviceName string, entityNames []string) error {
	return GeneratePlanDBTypesFromEntities(root, modulePath, serviceName, entityNamesAsEntries(entityNames, serviceName))
}

// EntityImport pairs an entity name with the proto file it lives in.
// ProtoFile is relative to the project's proto/ root (e.g.
// "services/api/v1/api.proto" or "db/v1/twilio_entities.proto").
type EntityImport struct {
	Name      string
	ProtoFile string
}

// GeneratePlanDBTypesFromEntities is the import-aware variant that lets
// callers pass per-entity ProtoFile paths so this function can group
// entities into their correct proto Go packages.
func GeneratePlanDBTypesFromEntities(root, modulePath, fallbackServiceName string, entries []EntityImport) error {
	if len(entries) == 0 {
		return nil
	}

	dbDir := filepath.Join(root, "internal", "db")
	if err := os.MkdirAll(dbDir, 0755); err != nil {
		return fmt.Errorf("create db directory: %w", err)
	}

	// Group entity names by import path. Each proto file maps to one
	// gen/<path>/v1 Go package; we alias each entity through whichever
	// alias prefix wins for its group.
	groups := map[string][]string{}    // importPath → entity names
	aliasFor := map[string]string{}    // importPath → alias identifier (pb, pbX, ...)
	groupOrder := []string{}

	for _, e := range entries {
		ip := importPathForProtoFile(modulePath, e.ProtoFile, fallbackServiceName)
		if _, seen := groups[ip]; !seen {
			groupOrder = append(groupOrder, ip)
		}
		groups[ip] = append(groups[ip], e.Name)
	}

	// Assign aliases. The first import gets "pb" for backwards compatibility
	// with older test expectations; subsequent imports get pb1, pb2, …
	sort.Strings(groupOrder)
	for i, ip := range groupOrder {
		if i == 0 {
			aliasFor[ip] = "pb"
		} else {
			aliasFor[ip] = fmt.Sprintf("pb%d", i)
		}
	}

	var b strings.Builder
	b.WriteString("// Code generated by forge. DO NOT EDIT.\n")
	b.WriteString("package db\n\n")
	b.WriteString("import (\n")
	for _, ip := range groupOrder {
		fmt.Fprintf(&b, "\t%s \"%s\"\n", aliasFor[ip], ip)
	}
	b.WriteString(")\n\n")
	b.WriteString("// Type aliases for entity types.\n")
	b.WriteString("// These start as aliases to the proto-generated types. If your DB schema\n")
	b.WriteString("// diverges from the API/proto schema, replace the alias with a concrete\n")
	b.WriteString("// struct and add converter functions.\n")
	for _, ip := range groupOrder {
		names := groups[ip]
		sort.Strings(names)
		alias := aliasFor[ip]
		for _, name := range names {
			fmt.Fprintf(&b, "type %s = %s.%s\n", name, alias, name)
		}
	}

	return os.WriteFile(filepath.Join(dbDir, "types.go"), []byte(b.String()), 0644)
}

// importPathForProtoFile maps a proto file (relative to proto/) to the
// generated Go import path.
//
//	services/api/v1/api.proto       → <module>/gen/services/api/v1
//	db/v1/twilio_entities.proto     → <module>/gen/db/v1
//
// Falls back to gen/services/<fallback>/v1 when the proto file path is
// empty or unrecognised, preserving prior behaviour for callers that
// don't carry per-entity file paths.
func importPathForProtoFile(modulePath, protoFile, fallbackService string) string {
	pf := filepath.ToSlash(protoFile)
	if pf == "" {
		return fmt.Sprintf("%s/gen/services/%s/v1", modulePath, fallbackService)
	}
	dir := path.Dir(pf)
	if dir == "." || dir == "/" {
		return fmt.Sprintf("%s/gen/services/%s/v1", modulePath, fallbackService)
	}
	return fmt.Sprintf("%s/gen/%s", modulePath, dir)
}

// entityNamesAsEntries adapts the legacy entityNames slice (no per-entity
// proto file) into EntityImport entries pinned to the service proto.
// Used to keep the original GeneratePlanDBTypes signature working.
func entityNamesAsEntries(entityNames []string, serviceName string) []EntityImport {
	out := make([]EntityImport, len(entityNames))
	for i, n := range entityNames {
		out[i] = EntityImport{
			Name:      n,
			ProtoFile: fmt.Sprintf("services/%s/v1/%s.proto", serviceName, serviceName),
		}
	}
	return out
}
