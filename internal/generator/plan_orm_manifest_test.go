// ── M3: ORM outputs are forge-certified ────────────────────────────────
//
// Kalshi fr-4dfef712e9, reproduced verbatim: `forge disown
// internal/db/trade_orm.go` refused because the ORM emitter never
// registered its outputs — the escape hatch the file's own header
// advertises ("forge disown to take ownership") was unusable, and
// `forge audit` saw every *_orm.go as an orphan. In the self-certifying
// era the registration IS the embedded forge:hash marker: every output
// must verify Pristine on disk.

func ormRel(name string) string {
	return filepath.Join("internal", "db", name)
}

func TestGeneratePlanORM_CertifiesOutputs(t *testing.T) {
	checksums.ResetSkipWrite()
	root := t.TempDir()
	cs := &FileChecksums{}

	entities := []config.PlanEntity{
		{Name: "Item", Fields: []config.PlanEntityField{
			{Name: "id", Type: "string", PrimaryKey: true, NotNull: true},
			{Name: "name", Type: "string", NotNull: true},
		}},
		{Name: "Order", Fields: []config.PlanEntityField{
			{Name: "id", Type: "string", PrimaryKey: true, NotNull: true},
		}},
	}

	if err := GeneratePlanORM(root, "example.com/app", "api", entities, cs); err != nil {
		t.Fatalf("GeneratePlanORM: %v", err)
	}

	insp := checksums.NewInspector(root, cs)
	for _, rel := range []string{ormRel("orm_shared.go"), ormRel("item_orm.go"), ormRel("order_orm.go")} {
		content, err := os.ReadFile(filepath.Join(root, rel))
		if err != nil {
			t.Errorf("%s not written: %v", rel, err)
			continue
		}
		// Self-certification replaces the manifest entry: a verifying
		// marker is what makes `forge disown` and `forge audit` see the
		// file as forge-owned Tier-1.
		if got := checksums.Verify(content); got != checksums.Pristine {
			t.Errorf("%s does not self-certify (Verify=%v) — forge disown/audit can't see it", rel, got)
		}
		if !insp.IsTier1(filepath.ToSlash(rel)) {
			t.Errorf("%s not classified Tier-1 (forge-owned, regenerated every run)", rel)
		}
	}
}

// TestGeneratePlanORM_DisownedFileLeftAlone pins the disown contract on
// ORM files: once a path is disowned, regeneration must never touch
// the user's content — not even with the emitter's force semantics.
func TestGeneratePlanORM_DisownedFileLeftAlone(t *testing.T) {
	checksums.ResetSkipWrite()
	root := t.TempDir()
	cs := &FileChecksums{}

	entities := []config.PlanEntity{
		{Name: "Item", Fields: []config.PlanEntityField{
			{Name: "id", Type: "string", PrimaryKey: true, NotNull: true},
		}},
	}
	if err := GeneratePlanORM(root, "example.com/app", "api", entities, cs); err != nil {
		t.Fatalf("GeneratePlanORM: %v", err)
	}

	rel := ormRel("item_orm.go")
	full := filepath.Join(root, rel)

	// User disowns and hand-patches the file.
	userContent := "package db\n\n// hand-patched after disown\n"
	if err := os.WriteFile(full, []byte(userContent), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := cs.DisownPaths(root, []string{rel}, "hand-patched ORM"); err != nil {
		t.Fatalf("DisownPaths: %v", err)
	}

	if err := GeneratePlanORM(root, "example.com/app", "api", entities, cs); err != nil {
		t.Fatalf("GeneratePlanORM after disown: %v", err)
	}
	got, err := os.ReadFile(full)
	if err != nil {
		t.Fatal(err)
	}
	if string(got) != userContent {
		t.Error("regenerate overwrote a disowned ORM file — disown not honored")
	}

	// Re-adoption: delete the file, regenerate — pristine render returns,
	// disowned record cleared.
	if err := os.Remove(full); err != nil {
		t.Fatal(err)
	}
	if err := GeneratePlanORM(root, "example.com/app", "api", entities, cs); err != nil {
		t.Fatalf("GeneratePlanORM re-adoption: %v", err)
	}
	got, err = os.ReadFile(full)
	if err != nil {
		t.Fatalf("re-adoption did not re-emit %s: %v", rel, err)
	}
	if !strings.Contains(string(got), "Code generated by forge") {
		t.Error("re-adoption should restore the pristine render")
	}
	if checksums.Verify(got) != checksums.Pristine {
		t.Errorf("re-adopted render must self-certify; Verify = %v", checksums.Verify(got))
	}
	if cs.IsDisowned(rel) {
		t.Errorf("disowned record not cleared after re-adoption: %+v", cs.Disowned[rel])
	}
}

// TestGeneratePlanORM_StaleSweepRespectsOwnership pins the stale-file
// sweep: an ORM file for a removed entity is deleted — unless the user
// disowned it, in which case it's theirs and the sweep must not touch
// it (and the ownership record survives).
func TestGeneratePlanORM_StaleSweepRespectsOwnership(t *testing.T) {
	checksums.ResetSkipWrite()
	root := t.TempDir()
	cs := &FileChecksums{}

	idOnly := func(name string) config.PlanEntity {
		return config.PlanEntity{Name: name, Fields: []config.PlanEntityField{
			{Name: "id", Type: "string", PrimaryKey: true, NotNull: true},
		}}
	}
	if err := GeneratePlanORM(root, "example.com/app", "api",
		[]config.PlanEntity{idOnly("Item"), idOnly("Gone"), idOnly("Kept")}, cs); err != nil {
		t.Fatalf("GeneratePlanORM: %v", err)
	}

	// Disown kept_orm.go — the user owns it now (marker stripped,
	// recorded in cs.Disowned).
	keptRel := ormRel("kept_orm.go")
	if err := cs.DisownPaths(root, []string{keptRel}, "keeping it"); err != nil {
		t.Fatalf("DisownPaths: %v", err)
	}

	// Regenerate with Gone and Kept both removed from the entity set.
	if err := GeneratePlanORM(root, "example.com/app", "api",
		[]config.PlanEntity{idOnly("Item")}, cs); err != nil {
		t.Fatalf("GeneratePlanORM after removal: %v", err)
	}

	goneRel := ormRel("gone_orm.go")
	if _, err := os.Stat(filepath.Join(root, goneRel)); !os.IsNotExist(err) {
		t.Errorf("stale %s not removed (stat err=%v)", goneRel, err)
	}

	if _, err := os.Stat(filepath.Join(root, keptRel)); err != nil {
		t.Errorf("disowned %s deleted by the stale sweep — user content destroyed (stat err=%v)", keptRel, err)
	}
	if !cs.IsDisowned(keptRel) {
		t.Errorf("disowned %s lost its ownership record", keptRel)
	}
}

// TestGeneratePlanORM_StaleSweepKeepsHandEditedFiles: the sweep reads
// ownership from the file itself — a marker that fails verification
// means the user edited the file, so it is not forge's to delete even
// without a disown record.
func TestGeneratePlanORM_StaleSweepKeepsHandEditedFiles(t *testing.T) {
	checksums.ResetSkipWrite()
	root := t.TempDir()
	cs := &FileChecksums{}

	idOnly := func(name string) config.PlanEntity {
		return config.PlanEntity{Name: name, Fields: []config.PlanEntityField{
			{Name: "id", Type: "string", PrimaryKey: true, NotNull: true},
		}}
	}
	if err := GeneratePlanORM(root, "example.com/app", "api",
		[]config.PlanEntity{idOnly("Item"), idOnly("Edited")}, cs); err != nil {
		t.Fatalf("GeneratePlanORM: %v", err)
	}
	editedRel := ormRel("edited_orm.go")
	full := filepath.Join(root, editedRel)
	prior, err := os.ReadFile(full)
	if err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(full, append(prior, []byte("// hand patch\n")...), 0o644); err != nil {
		t.Fatal(err)
	}

	if err := GeneratePlanORM(root, "example.com/app", "api",
		[]config.PlanEntity{idOnly("Item")}, cs); err != nil {
		t.Fatalf("GeneratePlanORM after removal: %v", err)
	}
	if _, err := os.Stat(full); err != nil {
		t.Errorf("hand-edited %s deleted by the stale sweep (stat err=%v)", editedRel, err)
	}
}

