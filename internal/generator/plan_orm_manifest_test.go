// TestGeneratePlanORM_AdoptsUntrackedExistingFiles pins the first-regen
// upgrade path: an ORM file written by pre-manifest forge exists on
// disk but has no checksum entry. The emitter overwrites it with the
// fresh render (the same regenerate-every-run behavior the file always
// had) and adopts it into the manifest as Tier-1, so every FUTURE edit
// is drift-guard-visible and `forge disown` becomes possible.
func TestGeneratePlanORM_AdoptsUntrackedExistingFiles(t *testing.T) {
	root := t.TempDir()
	rel := ormRel("item_orm.go")
	full := filepath.Join(root, rel)
	if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
		t.Fatal(err)
	}
	// Pre-manifest render with a hand-patch in it.
	if err := os.WriteFile(full, []byte("package db\n\n// hand-patch from the pre-manifest era\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	cs := &FileChecksums{Files: map[string]FileChecksumEntry{}}
	entities := []config.PlanEntity{
		{Name: "Item", Fields: []config.PlanEntityField{
			{Name: "id", Type: "string", PrimaryKey: true, NotNull: true},
		}},
	}
	if err := GeneratePlanORM(root, "example.com/app", "api", entities, cs); err != nil {
		t.Fatalf("GeneratePlanORM: %v", err)
	}

	got, err := os.ReadFile(full)
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(got), "Code generated by forge") {
		t.Error("untracked stale render should be replaced by the fresh render")
	}
	entry, ok := cs.Files[rel]
	if !ok || entry.Tier != 1 {
		t.Errorf("adopted entry = %+v ok=%v, want tracked tier=1", entry, ok)
	}
}
