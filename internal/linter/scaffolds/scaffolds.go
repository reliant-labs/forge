// Package scaffolds enforces the forge ownership boundary between
// pure-generated, scaffold-with-placeholders, and pure user-space files.
//
// Two rules:
//
//  1. A scaffold-with-placeholders file (anything containing
//     "FORGE_SCAFFOLD:" markers) must NOT be committed with the markers
//     still present. Either fill in the placeholders and remove the
//     markers, or remove the file from the commit. A committed scaffold
//     full of TODOs is the single biggest source of "did the LLM finish
//     the task?" ambiguity.
//
//  2. A pure forge-space "_gen" file MUST carry the canonical
//     "Code generated by forge. DO NOT EDIT." header AND a "Source:"
//     marker pointing at the input that drove its generation. Without
//     these two lines the LLM has no signal that the file is forge-owned
//     and may try to "fix" it in place.
//
// Findings with severity=error gate the build. Findings with
// severity=warning print but do not fail.
package scaffolds

import (
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"regexp"
	"strings"
)

// Severity indicates how important a finding is.
type Severity string

const (
	SeverityError   Severity = "error"
	SeverityWarning Severity = "warning"
)

// Finding represents a single lint finding.
type Finding struct {
	Rule     string   `json:"rule"`
	Severity Severity `json:"severity"`
	Path     string   `json:"path"`
	Message  string   `json:"message"`
}

// Result holds all findings from a lint run.
type Result struct {
	Findings []Finding `json:"findings"`
}

// HasErrors reports whether any finding is at error severity.
func (r Result) HasErrors() bool {
	for _, f := range r.Findings {
		if f.Severity == SeverityError {
			return true
		}
	}
	return false
}

// FormatText renders the findings as human-readable output.
func (r Result) FormatText() string {
	if len(r.Findings) == 0 {
		return "No scaffold ownership issues found.\n"
	}

	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("Found %d scaffold ownership issue(s):\n\n", len(r.Findings)))

	for _, f := range r.Findings {
		icon := "❌"
		if f.Severity == SeverityWarning {
			icon = "⚠️ "
		}
		sb.WriteString(fmt.Sprintf("  %s [%s] %s: %s\n", icon, f.Rule, f.Path, f.Message))
	}
	sb.WriteString("\n")
	return sb.String()
}

// LintResult bundles findings and any walk error.
type LintResult = Result

// scaffoldMarker is the literal marker comment forge templates emit.
// Matching the leading "// " (Go line-comment prefix) avoids firing on
// references to the marker name in string literals, doc comments, or
// imports — only the comment form is the actual scaffold placeholder.
const scaffoldMarker = "// FORGE_SCAFFOLD:"

// generatedHeader is the canonical header forge codegen emits.
const generatedHeader = "// Code generated by forge. DO NOT EDIT."

// generatedHeaderRE matches forge-emitted generated-file headers, including
// pack-suffixed variants like `// Code generated by forge generate
// (jwt-auth pack). DO NOT EDIT.`. The "by forge" segment is required so
// that an `_gen.go` file claiming a non-forge origin still trips the lint
// — forge owns the regen story for files in its space.
var generatedHeaderRE = regexp.MustCompile(`(?m)^// Code generated by forge[^\n]* DO NOT EDIT\.$`)

// sourceHeader is the second line every pure-generated forge file must carry,
// pointing at the input that drove the file (a proto file, a contract.go,
// forge.yaml, etc).
const sourceHeader = "// Source:"

// LintRoot walks root and applies both rules. Files matching skipDir paths
// (".git", "node_modules", "gen", "vendor") are skipped to avoid heavy
// directories that never contain forge-managed source.
func LintRoot(root string) (Result, error) {
	var result Result

	err := filepath.WalkDir(root, func(path string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() {
			if shouldSkipDir(d.Name()) {
				return filepath.SkipDir
			}
			return nil
		}
		if !isGoFile(path) {
			return nil
		}
		findings, ferr := lintFile(path, root)
		if ferr != nil {
			// Read errors are fatal — surface them rather than silently skip.
			return ferr
		}
		result.Findings = append(result.Findings, findings...)
		return nil
	})
	if err != nil {
		return result, err
	}
	return result, nil
}

func shouldSkipDir(name string) bool {
	switch name {
	case ".git", "node_modules", "gen", "vendor", "bin", "dist", ".forge", ".next":
		return true
	case "testdata":
		// `testdata/` is the canonical home for analyzer fixtures and
		// expected-fail files; the surrounding test suite asserts the
		// linter fires on them, so a top-level `forge lint --scaffolds`
		// must NOT replay those errors against the project itself.
		return true
	}
	return false
}

func isGoFile(path string) bool {
	return strings.HasSuffix(path, ".go")
}

// lintFile applies both rules to a single file. The relRoot is used to
// produce relative paths in findings.
func lintFile(path, relRoot string) ([]Finding, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("read %s: %w", path, err)
	}

	rel := relPath(path, relRoot)
	var findings []Finding

	// Rule 1: scaffold markers must not survive commit. We require the
	// marker to appear at the start of a line (after optional whitespace)
	// so string literals like
	//
	//	mustWriteFile(t, ..., "...\n// FORGE_SCAFFOLD: replace me\n")
	//
	// in test sources don't trip the rule. A real scaffold placeholder
	// always lives on its own line as a Go line comment.
	if count := countScaffoldMarkers(data); count > 0 {
		findings = append(findings, Finding{
			Rule:     "scaffold-not-customized",
			Severity: SeverityError,
			Path:     rel,
			Message: fmt.Sprintf(
				"file still contains %d FORGE_SCAFFOLD marker(s); fill in the placeholders and remove the comments before committing",
				count,
			),
		})
	}

	// Rule 2: every "_gen.go" file that claims to be generated must
	// carry the canonical header AND a "Source:" pointer. The "claims
	// to be generated" gate is important because forge's own internal
	// codebase has hand-written generators (e.g. internal/codegen/crud_gen.go
	// and its test) that legitimately use the _gen naming idiom for
	// "this file generates code with _gen.go suffix" — they don't claim
	// to be machine-generated themselves.
	if isGenFilename(rel) && claimsGenerated(data) {
		head := firstLines(data, 16)
		headBytes := []byte(head)
		hasCanonical := bytesContains(headBytes, []byte(generatedHeader)) ||
			generatedHeaderRE.Match(headBytes)
		if !hasCanonical {
			findings = append(findings, Finding{
				Rule:     "gen-missing-header",
				Severity: SeverityError,
				Path:     rel,
				Message:  `_gen file claims to be generated but lacks a canonical "// Code generated ... DO NOT EDIT." header`,
			})
		}
		if !bytesContains([]byte(head), []byte(sourceHeader)) {
			findings = append(findings, Finding{
				Rule:     "gen-missing-source",
				Severity: SeverityWarning,
				Path:     rel,
				Message:  `_gen file is missing a "// Source: <input>" line; add one so users know what drives the regeneration`,
			})
		}
	}

	return findings, nil
}

// isGenFilename returns true for paths whose final basename ends in
// "_gen.go" or "_gen_test.go". The test suffix variant covers
// handlers_crud_gen_test.go.
func isGenFilename(path string) bool {
	base := filepath.Base(path)
	return strings.HasSuffix(base, "_gen.go") || strings.HasSuffix(base, "_gen_test.go")
}

// claimsGenerated returns true when the file's leading comments contain
// the substring "Code generated" — the conventional signal that a file
// is machine-generated (matches the Go convention from
// https://golang.org/s/generatedcode). Hand-written files that happen to
// share a "_gen" naming convention (e.g. forge's own internal/codegen
// generators) won't include this phrase and are therefore skipped.
func claimsGenerated(data []byte) bool {
	head := firstLines(data, 16)
	return strings.Contains(head, "Code generated")
}

// firstLines returns the first n lines of data as a single string, with
// trailing whitespace per line preserved. Used to bound the header scan.
func firstLines(data []byte, n int) string {
	lines := strings.SplitN(string(data), "\n", n+1)
	if len(lines) > n {
		lines = lines[:n]
	}
	return strings.Join(lines, "\n")
}

// relPath returns path relative to root, falling back to path if Rel
// fails. Used solely for human-friendly finding output.
func relPath(path, root string) string {
	rel, err := filepath.Rel(root, path)
	if err != nil {
		return path
	}
	return rel
}

// bytesContains is a wrapper around strings.Contains that operates on
// byte slices without allocating a string copy of the entire haystack.
func bytesContains(haystack, needle []byte) bool {
	return strings.Contains(string(haystack), string(needle))
}

// bytesCount counts non-overlapping occurrences of needle in haystack.
func bytesCount(haystack, needle []byte) int {
	return strings.Count(string(haystack), string(needle))
}

// countScaffoldMarkers counts lines whose first non-whitespace content
// is the scaffoldMarker prefix. Restricting to line starts dodges the
// false-positive case of fixture writers that build scaffold-marker
// strings inside Go test sources via concatenation:
//
//	mustWriteFile(t, path, "package x\n// FORGE_SCAFFOLD: ...\n")
//
// The marker substring is real in the file's text but the line layout
// of the marker is broken by the escape sequences, so it doesn't appear
// at a line start.
func countScaffoldMarkers(data []byte) int {
	count := 0
	for _, line := range strings.Split(string(data), "\n") {
		trimmed := strings.TrimLeft(line, " \t")
		if strings.HasPrefix(trimmed, scaffoldMarker) {
			count++
		}
	}
	return count
}
