// TestCmdServerTemplate_CallsInstallGeneratedAuth pins the call-site
// half of the generated-auth honesty contract: when forge.yaml declares
// an auth provider, the generated runServer CALLS
// middleware.InstallGeneratedAuth (outside dev mode), and for
// header-carried providers it mounts middleware.GeneratedAuthInterceptor
// in the project chain. Pre-M6 the auth_gen.go constructor had ZERO
// call sites (the K2 review's generated-but-unwired class).
func TestCmdServerTemplate_CallsInstallGeneratedAuth(t *testing.T) {
	render := func(t *testing.T, provider string, external bool) string {
		t.Helper()
		data := struct {
			Module               string
			ConfigFields         map[string]bool
			AuthProvider         string
			AuthProviderExternal bool
		}{
			Module:               "example.com/myproject",
			ConfigFields:         map[string]bool{},
			AuthProvider:         provider,
			AuthProviderExternal: external,
		}
		content, err := ProjectTemplates().Render("cmd-server.go.tmpl", data)
		if err != nil {
			t.Fatalf("render cmd-server.go.tmpl (provider=%q): %v", provider, err)
		}
		return string(content)
	}

	t.Run("jwt", func(t *testing.T) {
		s := render(t, "jwt", false)
		if !strings.Contains(s, "middleware.InstallGeneratedAuth()") {
			t.Error("runServer must call middleware.InstallGeneratedAuth() when auth.provider is set")
		}
		if !strings.Contains(s, "if !cfg.Mode().IsDev() {") {
			t.Error("the generated-auth install must be skipped in dev mode (dev-claims passthrough)")
		}
		if strings.Contains(s, "GeneratedAuthInterceptor()") {
			t.Error("jwt providers wire through SetTokenValidator — no parallel interceptor in the chain")
		}
	})

	t.Run("api_key", func(t *testing.T) {
		s := render(t, "api_key", true)
		if !strings.Contains(s, "middleware.InstallGeneratedAuth()") {
			t.Error("runServer must call middleware.InstallGeneratedAuth() when auth.provider is set")
		}
		if !strings.Contains(s, "middleware.GeneratedAuthInterceptor()") {
			t.Error("header-carried providers must mount the generated interceptor in the project chain")
		}
	})

	t.Run("none", func(t *testing.T) {
		s := render(t, "", false)
		if strings.Contains(s, "InstallGeneratedAuth") {
			t.Error("no provider configured: runServer must not reference the (ungenerated) auth_gen.go surface")
		}
	})
}

