// AuthTemplateData mirrors codegen.AuthTemplateData for template rendering tests.
type AuthTemplateData struct {
	Provider    string
	JWT         config.JWTConfig
	APIKey      config.APIKeyConfig
	Module      string
	SkipMethods []string
}

func TestRenderAuthGenTemplate_JWT(t *testing.T) {
	data := AuthTemplateData{
		Provider: "jwt",
		JWT: config.JWTConfig{
			Issuer:        "https://auth.example.com",
			Audience:      "my-api",
			SigningMethod: "RS256",
		},
		Module: "example.com/myproject",
	}

	content, err := MiddlewareTemplates().Render("auth_gen.go.tmpl", data)
	if err != nil {
		t.Fatalf("MiddlewareTemplates().Render() error = %v", err)
	}

	s := string(content)

	fset := token.NewFileSet()
	if _, perr := parser.ParseFile(fset, "auth_gen.go", s, parser.AllErrors); perr != nil {
		t.Fatalf("rendered auth_gen.go does not parse: %v\n%s", perr, s)
	}

	checks := []struct {
		name     string
		contains string
		want     bool
	}{
		{"DO NOT EDIT header", "Code generated by forge. DO NOT EDIT.", true},
		{"Source pointer", "// Source:", true},
		{"library import", "github.com/reliant-labs/forge/pkg/auth", true},
		// JWT wires through the token-validator policy hook so the
		// user-owned NewAuthInterceptor (enrich/allow-list/dev-claims)
		// stays in the loop — no parallel interceptor.
		{"install hook", "func InstallGeneratedAuth() error {", true},
		{"validator install", "SetTokenValidator(v.Validate)", true},
		{"skip-list merge", "unauthenticatedProcedures[p] = struct{}{}", true},
		{"NewValidator wiring", "auth.NewValidator", true},
		{"provider literal", `Provider: "jwt"`, true},
		{"issuer config", "https://auth.example.com", true},
		{"audience config", "my-api", true},
		{"signing method", "RS256", true},
		// jwt mode has no interceptor of its own — authn owns the chain.
		{"no parallel interceptor", "GeneratedAuthInterceptor", false},
		{"no connect import", "connectrpc.com/connect", false},
	}

	for _, tc := range checks {
		found := strings.Contains(s, tc.contains)
		if found != tc.want {
			if tc.want {
				t.Errorf("%s: expected %q to be present in output", tc.name, tc.contains)
			} else {
				t.Errorf("%s: expected %q to NOT be present in output", tc.name, tc.contains)
			}
		}
	}
}

