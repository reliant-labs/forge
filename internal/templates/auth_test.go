func TestRenderAuthValidatorTemplate(t *testing.T) {
	data := AuthTemplateData{
		Provider: "api_key",
		Module:   "example.com/myproject",
	}

	content, err := RenderMiddlewareTemplate("middleware/auth_validator.go.tmpl", data)
	if err != nil {
		t.Fatalf("RenderMiddlewareTemplate() error = %v", err)
	}

	s := string(content)

	if !strings.Contains(s, "KeyValidator") {
		t.Error("auth_validator.go.tmpl missing KeyValidator interface")
	}
	if !strings.Contains(s, "ValidateKey") {
		t.Error("auth_validator.go.tmpl missing ValidateKey method")
	}
	if !strings.Contains(s, "*Claims") {
		t.Error("auth_validator.go.tmpl missing Claims return type")
	}
}
