package templates

import (
	"strings"
	"testing"

	"github.com/reliant-labs/forge/internal/config"
)

// AuthTemplateData mirrors codegen.AuthTemplateData for template rendering tests.
type AuthTemplateData struct {
	Provider    string
	JWT         config.JWTConfig
	APIKey      config.APIKeyConfig
	Module      string
	SkipMethods []string
}

func TestRenderAuthGenTemplate_JWT(t *testing.T) {
	data := AuthTemplateData{
		Provider: "jwt",
		JWT: config.JWTConfig{
			Issuer:        "https://auth.example.com",
			Audience:      "my-api",
			SigningMethod: "RS256",
		},
		Module: "example.com/myproject",
	}

	content, err := RenderMiddlewareTemplate("middleware/auth_gen.go.tmpl", data)
	if err != nil {
		t.Fatalf("RenderMiddlewareTemplate() error = %v", err)
	}

	s := string(content)

	checks := []struct {
		name     string
		contains string
		want     bool
	}{
		{"DO NOT EDIT header", "Code generated by forge generate. DO NOT EDIT.", true},
		{"JWT import", "golang-jwt/jwt/v5", true},
		{"interceptor function", "GeneratedAuthInterceptor", true},
		{"JWT authentication", "authenticateJWT", true},
		{"no API key logic", "authenticateAPIKey", false},
		{"issuer config", "https://auth.example.com", true},
		{"audience config", "my-api", true},
		{"signing method", "RS256", true},
		{"connect import", "connectrpc.com/connect", true},
	}

	for _, tc := range checks {
		found := strings.Contains(s, tc.contains)
		if found != tc.want {
			if tc.want {
				t.Errorf("%s: expected %q to be present in output", tc.name, tc.contains)
			} else {
				t.Errorf("%s: expected %q to NOT be present in output", tc.name, tc.contains)
			}
		}
	}
}

