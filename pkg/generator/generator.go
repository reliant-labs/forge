package generator

import (
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/proto"

	ormv1 "github.com/reliant-labs/forge/pkg/gen/forge/orm/v1"
)

// GenerateFile generates ORM code for a protobuf file
func GenerateFile(gen *protogen.Plugin, file *protogen.File) error {
	if len(file.Messages) == 0 {
		return nil
	}

	filename := file.GeneratedFilenamePrefix + ".pb.orm.go"
	g := gen.NewGeneratedFile(filename, file.GoImportPath)

	// Generate file header
	g.P("// Code generated by protoc-gen-forge-orm. DO NOT EDIT.")
	g.P()
	g.P("package ", file.GoPackageName)
	g.P()

	// Imports
	g.P("import (")
	g.P(`  "time"`)
	g.P()
	g.P(`  "github.com/reliant-labs/forge/pkg/orm"`)
	g.P(`  "google.golang.org/protobuf/types/known/timestamppb"`)
	g.P(")")
	g.P()

	// Generate code for each message
	for _, message := range file.Messages {
		if err := generateMessageORM(g, message); err != nil {
			return err
		}
	}

	return nil
}

func generateMessageORM(g *protogen.GeneratedFile, message *protogen.Message) error {
	// Check if message has ORM options
	opts := message.Desc.Options()
	if opts == nil {
		return nil
	}

	msgOpts := proto.GetExtension(opts, ormv1.E_Message).(*ormv1.MessageOptions)
	if msgOpts != nil && msgOpts.SkipOrm {
		return nil
	}

	tableName := getTableName(message, msgOpts)

	// Generate table name constant
	g.P("const ", message.GoIdent.GoName, "TableName = \"", tableName, "\"")
	g.P()

	// Generate field name constants
	generateFieldConstants(g, message)

	// Generate TableName method
	generateTableNameFunc(g, message, tableName)

	// Generate schema function
	generateSchemaFunc(g, message, tableName, msgOpts)

	// Generate PrimaryKey function
	generatePrimaryKeyFunc(g, message)

	// Generate Values function
	generateValuesFunc(g, message)

	// Generate Scan function (public, part of Scanner interface)
	generateScanFunc(g, message)

	return nil
}

func generateFieldConstants(g *protogen.GeneratedFile, message *protogen.Message) {
	g.P("// Field names for ", message.GoIdent.GoName)
	g.P("const (")
	for _, field := range message.Fields {
		fieldOpts := getFieldOptions(field)
		columnName := getColumnName(field, fieldOpts)
		g.P("  ", message.GoIdent.GoName, "Field", field.GoName, " = \"", columnName, "\"")
	}
	g.P(")")
	g.P()
}

func generateSchemaFunc(g *protogen.GeneratedFile, message *protogen.Message, tableName string, msgOpts *ormv1.MessageOptions) {
	g.P("// Schema returns the table schema for ", message.GoIdent.GoName)
	g.P("func (*", message.GoIdent.GoName, ") Schema() orm.TableSchema {")
	g.P("  return orm.TableSchema{")
	g.P(`    Name: "`, tableName, `",`)
	g.P("    Fields: []orm.FieldSchema{")

	for _, field := range message.Fields {
		fieldOpts := getFieldOptions(field)
		columnName := getColumnName(field, fieldOpts)
		fieldType := getFieldType(field, fieldOpts)

		g.P("      {")
		g.P(`        Name: "`, columnName, `",`)
		g.P("        Type: orm.", fieldType, ",")
		if fieldOpts != nil {
			if fieldOpts.PrimaryKey {
				g.P("        PrimaryKey: true,")
			}
			if fieldOpts.Unique {
				g.P("        Unique: true,")
			}
			if fieldOpts.NotNull {
				g.P("        NotNull: true,")
			}
			if fieldOpts.DefaultValue != "" {
				g.P(`        DefaultValue: "`, fieldOpts.DefaultValue, `",`)
			}
		}
		g.P("      },")
	}

	g.P("    },")

	// Generate indexes
	if msgOpts != nil && len(msgOpts.Indexes) > 0 {
		g.P("    Indexes: []orm.IndexSchema{")
		for _, idx := range msgOpts.Indexes {
			g.P("      {")
			g.P(`        Name: "`, idx.Name, `",`)
			g.P("        Fields: []string{", strings.Join(wrapStrings(idx.Fields), ", "), "},")
			if idx.Unique {
				g.P("        Unique: true,")
			}
			g.P("      },")
		}
		g.P("    },")
	}

	g.P("  }")
	g.P("}")
	g.P()
}





func generateScanFunc(g *protogen.GeneratedFile, message *protogen.Message) {
	g.P("// Scan scans a database row into the message")
	g.P("func (m *", message.GoIdent.GoName, ") Scan(scanner interface{ Scan(...interface{}) error }) error {")
	g.P("  var (")

	// Declare temporary variables for scanning
	for _, field := range message.Fields {
		g.P("    ", getScanVarName(field), " ", getScanType(field))
	}

	g.P("  )")
	g.P()

	// Build scan arguments
	var scanArgs []string
	for _, field := range message.Fields {
		scanArgs = append(scanArgs, "&"+getScanVarName(field))
	}

	g.P("  err := scanner.Scan(")
	for i, arg := range scanArgs {
		if i < len(scanArgs)-1 {
			g.P("    ", arg, ",")
		} else {
			g.P("    ", arg, ",")
		}
	}
	g.P("  )")
	g.P("  if err != nil {")
	g.P("    return err")
	g.P("  }")
	g.P()

	// Convert scanned values to protobuf types
	for _, field := range message.Fields {
		g.P("  m.", field.GoName, " = ", convertFromDBValue(field, getScanVarName(field)))
	}

	g.P()
	g.P("  return nil")
	g.P("}")
	g.P()
}

// Helper functions

func getTableName(message *protogen.Message, msgOpts *ormv1.MessageOptions) string {
	if msgOpts != nil && msgOpts.Table != "" {
		return msgOpts.Table
	}
	return toSnakeCase(message.GoIdent.GoName) + "s"
}

func getColumnName(field *protogen.Field, fieldOpts *ormv1.FieldOptions) string {
	if fieldOpts != nil && fieldOpts.Column != "" {
		return fieldOpts.Column
	}
	return toSnakeCase(field.GoName)
}

func getFieldOptions(field *protogen.Field) *ormv1.FieldOptions {
	opts := field.Desc.Options()
	if opts == nil {
		return nil
	}
	return proto.GetExtension(opts, ormv1.E_Field).(*ormv1.FieldOptions)
}

func getPrimaryKeyField(message *protogen.Message) *protogen.Field {
	for _, field := range message.Fields {
		fieldOpts := getFieldOptions(field)
		if fieldOpts != nil && fieldOpts.PrimaryKey {
			return field
		}
	}
	return nil
}

func getFieldType(field *protogen.Field, fieldOpts *ormv1.FieldOptions) string {
	if fieldOpts != nil && fieldOpts.AutoIncrement {
		switch field.Desc.Kind().String() {
		case "int32":
			return "TypeSerial"
		case "int64":
			return "TypeBigSerial"
		}
	}

	switch field.Desc.Kind().String() {
	case "string":
		return "TypeText"
	case "int32":
		return "TypeInteger"
	case "int64":
		return "TypeBigInt"
	case "bool":
		return "TypeBoolean"
	case "bytes":
		return "TypeBytea"
	case "message":
		if field.Message != nil && field.Message.Desc.FullName() == "google.protobuf.Timestamp" {
			return "TypeTimestampTZ"
		}
		return "TypeJSONB"
	default:
		return "TypeText"
	}
}

func getGoType(field *protogen.Field) string {
	switch field.Desc.Kind().String() {
	case "string":
		return "string"
	case "int32":
		return "int32"
	case "int64":
		return "int64"
	case "bool":
		return "bool"
	case "bytes":
		return "[]byte"
	default:
		return "interface{}"
	}
}

func getScanType(field *protogen.Field) string {
	switch field.Desc.Kind().String() {
	case "string":
		return "string"
	case "int32":
		return "int32"
	case "int64":
		return "int64"
	case "bool":
		return "bool"
	case "bytes":
		return "[]byte"
	case "message":
		if field.Message != nil && field.Message.Desc.FullName() == "google.protobuf.Timestamp" {
			return "time.Time"
		}
		return "[]byte"
	default:
		return "interface{}"
	}
}

func getScanVarName(field *protogen.Field) string {
	return "db" + field.GoName
}

func convertToDBValue(field *protogen.Field, goValue string) string {
	if field.Desc.Kind().String() == "message" {
		if field.Message != nil && field.Message.Desc.FullName() == "google.protobuf.Timestamp" {
			return goValue + ".AsTime()"
		}
	}
	return goValue
}

func convertFromDBValue(field *protogen.Field, dbValue string) string {
	if field.Desc.Kind().String() == "message" {
		if field.Message != nil && field.Message.Desc.FullName() == "google.protobuf.Timestamp" {
			return "timestamppb.New(" + dbValue + ")"
		}
	}
	return dbValue
}

func toSnakeCase(s string) string {
	var result []rune
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			result = append(result, '_')
		}
		result = append(result, r)
	}
	return strings.ToLower(string(result))
}

func wrapStrings(strs []string) []string {
	result := make([]string, len(strs))
	for i, s := range strs {
		result[i] = `"` + s + `"`
	}
	return result
}

// Generate helper functions at end of file
func generateHelperFuncs(g *protogen.GeneratedFile) {
	g.P("// Helper functions")
	g.P()
	g.P("func columnsStr(columns []string) string {")
	g.P(`  return strings.Join(columns, ", ")`)
	g.P("}")
	g.P()
	g.P("func placeholdersStr(count int) string {")
	g.P("  placeholders := make([]string, count)")
	g.P("  for i := range placeholders {")
	g.P(`    placeholders[i] = fmt.Sprintf("$%d", i+1)`)
	g.P("  }")
	g.P(`  return strings.Join(placeholders, ", ")`)
	g.P("}")
	g.P()
	g.P("func updateSetStr(columns []string) string {")
	g.P("  sets := make([]string, len(columns))")
	g.P("  for i, col := range columns {")
	g.P(`    sets[i] = fmt.Sprintf("%s = EXCLUDED.%s", col, col)`)
	g.P("  }")
	g.P(`  return strings.Join(sets, ", ")`)
	g.P("}")
}

func generatePrimaryKeyFunc(g *protogen.GeneratedFile, message *protogen.Message) {
	pkField := getPrimaryKeyField(message)
	if pkField == nil {
		// No primary key - return nil
		g.P("// PrimaryKey returns the primary key value")
		g.P("func (m *", message.GoIdent.GoName, ") PrimaryKey() any {")
		g.P("  return nil")
		g.P("}")
		g.P()
		return
	}

	g.P("// PrimaryKey returns the primary key value")
	g.P("func (m *", message.GoIdent.GoName, ") PrimaryKey() any {")
	g.P("  return m.", pkField.GoName)
	g.P("}")
	g.P()
}

func generateValuesFunc(g *protogen.GeneratedFile, message *protogen.Message) {
	g.P("// Values returns the column names and values for this model")
	g.P("func (m *", message.GoIdent.GoName, ") Values() ([]string, []any) {")
	g.P("  columns := []string{")
	for _, field := range message.Fields {
		fieldOpts := getFieldOptions(field)
		columnName := getColumnName(field, fieldOpts)
		g.P(`    "`, columnName, `",`)
	}
	g.P("  }")
	g.P()
	g.P("  values := []any{")
	for _, field := range message.Fields {
		g.P("    ", convertToDBValue(field, "m."+field.GoName), ",")
	}
	g.P("  }")
	g.P()
	g.P("  return columns, values")
	g.P("}")
	g.P()
}

func generateTableNameFunc(g *protogen.GeneratedFile, message *protogen.Message, tableName string) {
	g.P("// TableName returns the database table name")
	g.P("func (*", message.GoIdent.GoName, ") TableName() string {")
	g.P(`  return "`, tableName, `"`)
	g.P("}")
	g.P()
}
